fn emit_validate_method_go(
    out: &mut String,
    model: &Model,
    validations: &[crate::lower::LoweredValidation],
) {
    let recv_var = receiver_letter(model);
    writeln!(
        out,
        "func ({recv_var} *{}) Validate() []ValidationError {{",
        model.name.0
    )
    .unwrap();
    writeln!(out, "\tvar errors []ValidationError").unwrap();
    for lv in validations {
        for check in &lv.checks {
            emit_check_inline_go(out, &recv_var, lv.attribute.as_str(), check);
        }
    }
    writeln!(out, "\treturn errors").unwrap();
    writeln!(out, "}}").unwrap();
}

/// Render Save/Destroy/Count/Find for a model against the test DB.
/// SQL strings come from LoweredPersistence; the per-target surface
/// is database/sql API shape, the `?N → ?` placeholder rewrite, and
/// class-method-as-package-function naming (`Article.count` →
/// `ArticleCount()`).
fn emit_persistence_methods_go(
    out: &mut String,
    model: &Model,
    has_validate: bool,
    app: &App,
) {
    let lp = crate::lower::lower_persistence(model, app);
    let class = lp.class.0.as_str();
    let recv = receiver_letter(model);

    let insert_sql = positional_placeholders_go(&lp.insert_sql);
    let update_sql = positional_placeholders_go(&lp.update_sql);
    let delete_sql = positional_placeholders_go(&lp.delete_sql);
    let select_by_id_sql = positional_placeholders_go(&lp.select_by_id_sql);

    let non_id_args: Vec<String> = lp
        .non_id_columns
        .iter()
        .map(|s| format!("{recv}.{}", go_field_name(s.as_str())))
        .collect();

    // ----- Save -----
    writeln!(out, "func ({recv} *{class}) Save() bool {{").unwrap();
    if has_validate {
        writeln!(out, "\tif len({recv}.Validate()) > 0 {{ return false }}").unwrap();
    }
    // belongs_to existence: FK must be non-zero and refer to an
    // existing row in the target table.
    for check in &lp.belongs_to_checks {
        let fk_field = go_field_name(check.foreign_key.as_str());
        let target = check.target_class.0.as_str();
        writeln!(
            out,
            "\tif {recv}.{fk_field} == 0 || {target}Find({recv}.{fk_field}) == nil {{ return false }}",
        )
        .unwrap();
    }
    writeln!(out, "\tdb := Conn()").unwrap();
    writeln!(out, "\tif {recv}.ID == 0 {{").unwrap();
    writeln!(
        out,
        "\t\tres, err := db.Exec({insert_sql:?}, {})",
        non_id_args.join(", "),
    )
    .unwrap();
    writeln!(out, "\t\tif err != nil {{ panic(err) }}").unwrap();
    writeln!(out, "\t\tid, _ := res.LastInsertId()").unwrap();
    writeln!(out, "\t\t{recv}.ID = id").unwrap();
    writeln!(out, "\t}} else {{").unwrap();
    writeln!(
        out,
        "\t\t_, err := db.Exec({update_sql:?}, {}, {recv}.ID)",
        non_id_args.join(", "),
    )
    .unwrap();
    writeln!(out, "\t\tif err != nil {{ panic(err) }}").unwrap();
    writeln!(out, "\t}}").unwrap();
    writeln!(out, "\treturn true").unwrap();
    writeln!(out, "}}").unwrap();

    // ----- Destroy -----
    writeln!(out).unwrap();
    writeln!(out, "func ({recv} *{class}) Destroy() {{").unwrap();
    for dc in &lp.dependent_children {
        let child_class = dc.child_class.0.as_str();
        let child_select = positional_placeholders_go(&dc.select_by_parent_sql);
        writeln!(out, "\t{{").unwrap();
        writeln!(
            out,
            "\t\trows, err := Conn().Query({child_select:?}, {recv}.ID)",
        )
        .unwrap();
        writeln!(out, "\t\tif err != nil {{ panic(err) }}").unwrap();
        writeln!(out, "\t\tvar children []{child_class}").unwrap();
        writeln!(out, "\t\tfor rows.Next() {{").unwrap();
        writeln!(out, "\t\t\tvar c {child_class}").unwrap();
        let scans: Vec<String> = dc
            .child_columns
            .iter()
            .map(|s| format!("&c.{}", go_field_name(s.as_str())))
            .collect();
        writeln!(out, "\t\t\tif err := rows.Scan({}); err != nil {{ panic(err) }}", scans.join(", ")).unwrap();
        writeln!(out, "\t\t\tchildren = append(children, c)").unwrap();
        writeln!(out, "\t\t}}").unwrap();
        writeln!(out, "\t\trows.Close()").unwrap();
        writeln!(out, "\t\tfor i := range children {{").unwrap();
        writeln!(out, "\t\t\tchildren[i].Destroy()").unwrap();
        writeln!(out, "\t\t}}").unwrap();
        writeln!(out, "\t}}").unwrap();
    }
    writeln!(
        out,
        "\tif _, err := Conn().Exec({delete_sql:?}, {recv}.ID); err != nil {{ panic(err) }}",
    )
    .unwrap();
    writeln!(out, "}}").unwrap();

    // ----- Count (package-level function, since Go has no class methods) -----
    writeln!(out).unwrap();
    writeln!(out, "func {class}Count() int64 {{").unwrap();
    writeln!(out, "\tvar n int64").unwrap();
    writeln!(
        out,
        "\tif err := Conn().QueryRow({:?}).Scan(&n); err != nil {{ panic(err) }}",
        lp.count_sql,
    )
    .unwrap();
    writeln!(out, "\treturn n").unwrap();
    writeln!(out, "}}").unwrap();

    // ----- Find (package-level function) -----
    writeln!(out).unwrap();
    writeln!(out, "func {class}Find(id int64) *{class} {{").unwrap();
    writeln!(
        out,
        "\trow := Conn().QueryRow({select_by_id_sql:?}, id)",
    )
    .unwrap();
    writeln!(out, "\tvar r {class}").unwrap();
    let scans: Vec<String> = lp
        .columns
        .iter()
        .map(|s| format!("&r.{}", go_field_name(s.as_str())))
        .collect();
    writeln!(
        out,
        "\tif err := row.Scan({}); err != nil {{ return nil }}",
        scans.join(", ")
    )
    .unwrap();
    writeln!(out, "\treturn &r").unwrap();
    writeln!(out, "}}").unwrap();

    // ----- All (package-level function) -----
    let select_all_sql = positional_placeholders_go(&lp.select_all_sql);
    writeln!(out).unwrap();
    writeln!(out, "func {class}All() []*{class} {{").unwrap();
    writeln!(out, "\trows, err := Conn().Query({select_all_sql:?})").unwrap();
    writeln!(out, "\tif err != nil {{ panic(err) }}").unwrap();
    writeln!(out, "\tdefer rows.Close()").unwrap();
    writeln!(out, "\tvar out []*{class}").unwrap();
    writeln!(out, "\tfor rows.Next() {{").unwrap();
    writeln!(out, "\t\tvar r {class}").unwrap();
    writeln!(
        out,
        "\t\tif err := rows.Scan({}); err != nil {{ panic(err) }}",
        scans.join(", ")
    )
    .unwrap();
    writeln!(out, "\t\tout = append(out, &r)").unwrap();
    writeln!(out, "\t}}").unwrap();
    writeln!(out, "\treturn out").unwrap();
    writeln!(out, "}}").unwrap();

    // ----- Last (package-level function) -----
    let select_last_sql = positional_placeholders_go(&lp.select_last_sql);
    writeln!(out).unwrap();
    writeln!(out, "func {class}Last() *{class} {{").unwrap();
    writeln!(
        out,
        "\trow := Conn().QueryRow({select_last_sql:?})",
    )
    .unwrap();
    writeln!(out, "\tvar r {class}").unwrap();
    writeln!(
        out,
        "\tif err := row.Scan({}); err != nil {{ return nil }}",
        scans.join(", ")
    )
    .unwrap();
    writeln!(out, "\treturn &r").unwrap();
    writeln!(out, "}}").unwrap();

    // ----- Reload (instance method) -----
    writeln!(out).unwrap();
    writeln!(out, "func ({recv} *{class}) Reload() {{").unwrap();
    writeln!(out, "\tfresh := {class}Find({recv}.ID)").unwrap();
    writeln!(out, "\tif fresh == nil {{ return }}").unwrap();
    writeln!(out, "\t*{recv} = *fresh").unwrap();
    writeln!(out, "}}").unwrap();

    // ----- has_many accessors (package-level functions: Model.assoc → ModelAssoc(parent)) -----
    for dc in &lp.dependent_children {
        let child_class = dc.child_class.0.as_str();
        let assoc_plural = crate::naming::pluralize_snake(child_class);
        let assoc_pascal = pascalize_word(&assoc_plural);
        let child_select = positional_placeholders_go(&dc.select_by_parent_sql);
        writeln!(out).unwrap();
        writeln!(
            out,
            "func ({recv} *{class}) {assoc_pascal}() []*{child_class} {{"
        )
        .unwrap();
        writeln!(
            out,
            "\trows, err := Conn().Query({child_select:?}, {recv}.ID)"
        )
        .unwrap();
        writeln!(out, "\tif err != nil {{ panic(err) }}").unwrap();
        writeln!(out, "\tdefer rows.Close()").unwrap();
        writeln!(out, "\tvar out []*{child_class}").unwrap();
        writeln!(out, "\tfor rows.Next() {{").unwrap();
        writeln!(out, "\t\tvar c {child_class}").unwrap();
        let cscans: Vec<String> = dc
            .child_columns
            .iter()
            .map(|s| format!("&c.{}", go_field_name(s.as_str())))
            .collect();
        writeln!(
            out,
            "\t\tif err := rows.Scan({}); err != nil {{ panic(err) }}",
            cscans.join(", ")
        )
        .unwrap();
        writeln!(out, "\t\tout = append(out, &c)").unwrap();
        writeln!(out, "\t}}").unwrap();
        writeln!(out, "\treturn out").unwrap();
        writeln!(out, "}}").unwrap();
    }
}

/// SQLite `?N` → database/sql `?`. `database/sql` doesn't support
/// numbered placeholders in the same positional style; drop the
/// trailing digits.
fn positional_placeholders_go(sql: &str) -> String {
    let mut out = String::new();
    let chars: Vec<char> = sql.chars().collect();
    let mut i = 0;
    while i < chars.len() {
        if chars[i] == '?' {
            out.push('?');
            i += 1;
            while i < chars.len() && chars[i].is_ascii_digit() {
                i += 1;
            }
        } else {
            out.push(chars[i]);
            i += 1;
        }
    }
    out
}

fn receiver_letter(model: &Model) -> String {
    model
        .name
        .0
        .as_str()
        .chars()
        .next()
        .unwrap_or('r')
        .to_ascii_lowercase()
        .to_string()
}

fn emit_check_inline_go(
    out: &mut String,
    recv: &str,
    attr: &str,
    check: &crate::lower::Check,
) {
    use crate::lower::Check;
    let field = go_field_name(attr);
    let msg = check.default_message();
    let push = |cond: &str| -> String {
        format!(
            "\tif {cond} {{\n\t\terrors = append(errors, NewValidationError({attr:?}, {msg:?}))\n\t}}"
        )
    };
    let block = match check {
        Check::Presence => push(&format!("{recv}.{field} == \"\"")),
        Check::Absence => push(&format!("{recv}.{field} != \"\"")),
        Check::MinLength { n } => push(&format!("len({recv}.{field}) < {n}")),
        Check::MaxLength { n } => push(&format!("len({recv}.{field}) > {n}")),
        Check::GreaterThan { threshold } => push(&format!("{recv}.{field} <= {threshold}")),
        Check::LessThan { threshold } => push(&format!("{recv}.{field} >= {threshold}")),
        Check::OnlyInteger => {
            format!("\t// OnlyInteger on {attr:?} — enforced by Go's type system")
        }
        Check::Inclusion { values } => {
            let parts: Vec<String> =
                values.iter().map(inclusion_value_to_go).collect();
            // Emit an inline membership check via a switch-style OR.
            let cond = parts
                .iter()
                .map(|p| format!("{recv}.{field} != {p}"))
                .collect::<Vec<_>>()
                .join(" && ");
            push(&cond)
        }
        Check::Format { pattern } => {
            format!("\t// TODO: Format check on {attr:?} requires runtime regex ({pattern:?})")
        }
        Check::Uniqueness { .. } => {
            format!("\t// TODO: Uniqueness on {attr:?} requires DB access at runtime")
        }
        Check::Custom { method } => {
            format!("\t{recv}.{}(errors)", go_field_name(method.as_str()))
        }
    };
    writeln!(out, "{block}").unwrap();
}

fn inclusion_value_to_go(v: &crate::lower::InclusionValue) -> String {
    use crate::lower::InclusionValue;
    match v {
        InclusionValue::Str { value } => format!("{value:?}"),
        InclusionValue::Int { value } => value.to_string(),
        InclusionValue::Float { value } => {
            let s = value.to_string();
            if s.contains('.') { s } else { format!("{s}.0") }
        }
        InclusionValue::Bool { value } => value.to_string(),
    }
}

fn emit_go_fixtures(app: &App) -> EmittedFile {
    // Single file `app/fixtures_test.go` keeps fixtures inside the
    // package's test build (not shipped in production). Go's
    // `_test.go` suffix excludes files from `go build`; `go test`
    // picks them up.
    let lowered = crate::lower::lower_fixtures(app);
    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "// FixtureIDs maps (fixture_name, label) → AUTOINCREMENT id").unwrap();
    writeln!(s, "// assigned when the fixture loaded. Reset by setupTest.").unwrap();
    writeln!(s, "var FixtureIDs = map[[2]string]int64{{}}").unwrap();
    writeln!(s).unwrap();
    writeln!(
        s,
        "// setupTest opens a fresh :memory: SQLite DB, runs the schema DDL,"
    )
    .unwrap();
    writeln!(s, "// and reloads every fixture in declaration order. Each generated").unwrap();
    writeln!(s, "// test calls this at the top of its body.").unwrap();
    writeln!(s, "func setupTest() {{").unwrap();
    writeln!(s, "\tSetupTestDB(CreateTables)").unwrap();
    writeln!(s, "\tFixtureIDs = map[[2]string]int64{{}}").unwrap();
    for f in &lowered.fixtures {
        let mod_ns = crate::naming::camelize(f.name.as_str());
        writeln!(s, "\t{mod_ns}LoadAll()").unwrap();
    }
    writeln!(s, "}}").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "func FixtureID(fixture, label string) int64 {{").unwrap();
    writeln!(s, "\tid, ok := FixtureIDs[[2]string{{fixture, label}}]").unwrap();
    writeln!(s, "\tif !ok {{").unwrap();
    writeln!(
        s,
        "\t\tpanic(\"fixture \" + fixture + \"/\" + label + \" not loaded\")"
    )
    .unwrap();
    writeln!(s, "\t}}").unwrap();
    writeln!(s, "\treturn id").unwrap();
    writeln!(s, "}}").unwrap();

    for fixture in &lowered.fixtures {
        let class_name = fixture.class.0.as_str();
        let ns = crate::naming::camelize(fixture.name.as_str());

        // LoadAll — insert each record via Save (validations +
        // INSERT via the juntos-style runtime) and record the
        // AUTOINCREMENT id in FixtureIDs.
        writeln!(s).unwrap();
        writeln!(s, "func {ns}LoadAll() {{").unwrap();
        for record in &fixture.records {
            let label = record.label.as_str();
            writeln!(s, "\t{{").unwrap();
            writeln!(s, "\t\tr := &{class_name}{{}}").unwrap();
            for field in &record.fields {
                let col = go_field_name(field.column.as_str());
                let val = match &field.value {
                    crate::lower::LoweredFixtureValue::Literal { ty, raw } => {
                        go_literal_for(raw, ty)
                    }
                    crate::lower::LoweredFixtureValue::FkLookup {
                        target_fixture,
                        target_label,
                    } => format!(
                        "FixtureID({:?}, {:?})",
                        target_fixture.as_str(),
                        target_label.as_str(),
                    ),
                };
                writeln!(s, "\t\tr.{col} = {val}").unwrap();
            }
            writeln!(
                s,
                "\t\tif !r.Save() {{ panic(\"fixture {}/{label} failed to save\") }}",
                fixture.name.as_str(),
            )
            .unwrap();
            writeln!(
                s,
                "\t\tFixtureIDs[[2]string{{{:?}, {:?}}}] = r.ID",
                fixture.name.as_str(),
                label,
            )
            .unwrap();
            writeln!(s, "\t}}").unwrap();
        }
        writeln!(s, "}}").unwrap();

        // Named-record getters: look up id + Find.
        for record in &fixture.records {
            let label = record.label.as_str();
            let label_pascal = go_field_name(label);
            writeln!(s).unwrap();
            writeln!(s, "func {ns}{label_pascal}() *{class_name} {{").unwrap();
            writeln!(
                s,
                "\tid := FixtureID({:?}, {:?})",
                fixture.name.as_str(),
                label,
            )
            .unwrap();
            writeln!(s, "\tr := {class_name}Find(id)").unwrap();
            writeln!(
                s,
                "\tif r == nil {{ panic(\"fixture {}/{label} not loaded\") }}",
                fixture.name.as_str(),
            )
            .unwrap();
            writeln!(s, "\treturn r").unwrap();
            writeln!(s, "}}").unwrap();
        }
    }

    EmittedFile {
        path: PathBuf::from("app/fixtures_test.go"),
        content: s,
    }
}

fn go_literal_for(value: &str, ty: &Ty) -> String {
    match ty {
        Ty::Str | Ty::Sym => format!("{value:?}"),
        Ty::Int => {
            if value.parse::<i64>().is_ok() {
                value.to_string()
            } else {
                format!("0 /* TODO: coerce {value:?} */")
            }
        }
        Ty::Float => {
            if value.parse::<f64>().is_ok() {
                value.to_string()
            } else {
                format!("0.0 /* TODO: coerce {value:?} */")
            }
        }
        Ty::Bool => match value {
            "true" | "1" => "true".into(),
            "false" | "0" => "false".into(),
            _ => format!("false /* TODO: coerce {value:?} */"),
        },
        Ty::Class { id, .. } if id.0.as_str() == "Time" => format!("{value:?}"),
        _ => format!("{value:?}"),
    }
}

fn emit_go_tests(tm: &TestModule, app: &App) -> EmittedFile {
    let fixture_names: Vec<crate::ident::Symbol> =
        app.fixtures.iter().map(|f| f.name.clone()).collect();
    let known_models: Vec<crate::ident::Symbol> =
        app.models.iter().map(|m| m.name.0.clone()).collect();
    let mut attrs_set: std::collections::BTreeSet<crate::ident::Symbol> =
        std::collections::BTreeSet::new();
    for m in &app.models {
        for attr in m.attributes.fields.keys() {
            attrs_set.insert(attr.clone());
        }
    }
    let model_attrs: Vec<crate::ident::Symbol> = attrs_set.into_iter().collect();

    let ctx = GoTestCtx {
        app,
        fixture_names: &fixture_names,
        known_models: &known_models,
        model_attrs: &model_attrs,
    };

    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    writeln!(s).unwrap();
    let is_controller_test = tm.name.0.as_str().ends_with("ControllerTest");
    if is_controller_test {
        // Controller tests reference fmt.Sprint via the form-body
        // flattener; testing is required for *testing.T regardless.
        writeln!(s, "import (").unwrap();
        writeln!(s, "\t\"fmt\"").unwrap();
        writeln!(s, "\t\"testing\"").unwrap();
        writeln!(s, ")").unwrap();
        writeln!(s).unwrap();
        writeln!(s, "var _ = fmt.Sprint").unwrap();
    } else {
        writeln!(s, "import \"testing\"").unwrap();
    }
    for test in &tm.tests {
        let fn_name = format!("Test{}", go_field_name(&test_name_snake(&test.name)));
        writeln!(s).unwrap();
        writeln!(s, "func {fn_name}(t *testing.T) {{").unwrap();
        if is_controller_test {
            // Pass-2: dispatch through TestClient via the shared
            // controller_test classifier + a Go render table.
            if !app.fixtures.is_empty() {
                writeln!(s, "\tsetupTest()").unwrap();
            }
            let body = emit_go_controller_test_body(test, app, &ctx);
            for line in body.lines() {
                writeln!(s, "\t{line}").unwrap();
            }
        } else if test_needs_runtime_unsupported_go(test) {
            writeln!(
                s,
                "\tt.Skip(\"Phase 3: needs persistence runtime\")"
            )
            .unwrap();
        } else {
            if !app.fixtures.is_empty() {
                // Every test starts on a fresh :memory: DB with all
                // fixtures loaded — Rails' transactional-fixture shape.
                writeln!(s, "\tsetupTest()").unwrap();
            }
            let body = emit_test_body_go(&test.body, ctx);
            for line in body.lines() {
                writeln!(s, "\t{line}").unwrap();
            }
        }
        writeln!(s, "}}").unwrap();
    }

    let filename = snake_case(tm.name.0.as_str());
    let filename = filename.replace("_test", "");
    EmittedFile {
        path: PathBuf::from(format!("app/{filename}_test.go")),
        content: s,
    }
}

fn test_name_snake(desc: &str) -> String {
    let mut s: String = desc
        .chars()
        .map(|c| if c.is_alphanumeric() { c.to_ascii_lowercase() } else { '_' })
        .collect();
    while s.contains("__") {
        s = s.replace("__", "_");
    }
    s.trim_matches('_').to_string()
}

// Pass-2 controller-test emit ------------------------------------------
//
// Walks each controller-test body via the shared `test_body_stmts`
// helper and dispatches each statement through
// `classify_controller_test_send` + a Go render table. Mirrors the
// Python and Elixir emitters; differences are Go's static typing
// (TestClient takes `*testing.T`; assertion methods don't take a
// receiver capture).

fn emit_go_controller_test_body(test: &Test, app: &App, ctx: &GoTestCtx) -> String {
    let mut out = String::new();
    out.push_str("client := NewTestClient(t)\n");
    // Prime ivars read without assignment via fixture lookup:
    // `@article` → `article := ArticlesOne()`.
    let walked = crate::lower::walk_controller_ivars(&test.body);
    for ivar in walked.ivars_read_without_assign() {
        let plural = crate::naming::pluralize_snake(ivar.as_str());
        if ctx.fixture_names.iter().any(|s| s.as_str() == plural) {
            out.push_str(&format!(
                "{} := {}{}()\n",
                ivar.as_str(),
                pascalize_word(&plural),
                pascalize_word("one"),
            ));
        }
    }
    // Acknowledge `client` so Go doesn't error if the test only uses
    // assertion-style helpers; in practice every classified test uses
    // the client at least once, but the fall-through path needs the
    // safety belt.
    out.push_str("_ = client\n");
    for stmt in crate::lower::test_body_stmts(&test.body) {
        let rendered = emit_go_ctrl_test_stmt(stmt, app, ctx);
        out.push_str(&rendered);
        out.push('\n');
    }
    out
}

fn emit_go_ctrl_test_stmt(stmt: &Expr, app: &App, ctx: &GoTestCtx) -> String {
    match &*stmt.node {
        ExprNode::Send { recv: None, method, args, block, .. } => {
            emit_go_ctrl_test_send(method.as_str(), args, block.as_ref(), app, ctx)
        }
        ExprNode::Send { recv: Some(r), method, args, .. } => {
            if method.as_str() == "reload" {
                let recv_s = match &*r.node {
                    ExprNode::Ivar { name } | ExprNode::Var { name, .. } => {
                        name.to_string()
                    }
                    _ => emit_go_ctrl_test_expr(r, app, ctx),
                };
                return format!("{recv_s}.Reload()");
            }
            let recv_s = emit_go_ctrl_test_expr(r, app, ctx);
            let args_s: Vec<String> = args
                .iter()
                .map(|a| emit_go_ctrl_test_expr(a, app, ctx))
                .collect();
            if args_s.is_empty() {
                // Field access — Go uses bare-name access for struct
                // fields, no parens.
                format!("{recv_s}.{}", go_field_name(method.as_str()))
            } else {
                format!(
                    "{recv_s}.{}({})",
                    go_method_name(method.as_str()),
                    args_s.join(", ")
                )
            }
        }
        ExprNode::Assign { target: LValue::Var { name, .. }, value } => {
            format!("{name} := {}", emit_go_ctrl_test_expr(value, app, ctx))
        }
        ExprNode::Assign { target: LValue::Ivar { name }, value } => {
            format!("{name} := {}", emit_go_ctrl_test_expr(value, app, ctx))
        }
        _ => emit_go_ctrl_test_expr(stmt, app, ctx),
    }
}

fn emit_go_ctrl_test_send(
    method: &str,
    args: &[Expr],
    block: Option<&Expr>,
    app: &App,
    ctx: &GoTestCtx,
) -> String {
    use crate::lower::ControllerTestSend;
    match crate::lower::classify_controller_test_send(method, args, block) {
        Some(ControllerTestSend::HttpGet { url }) => {
            let u = emit_go_url_expr(url, app, ctx);
            format!("resp := client.Get({u})")
        }
        Some(ControllerTestSend::HttpWrite { method, url, params }) => {
            let u = emit_go_url_expr(url, app, ctx);
            let body = params
                .map(|h| flatten_go_params_to_form(h, None, app, ctx))
                .unwrap_or_else(|| "map[string]string{}".to_string());
            let go_method = match method {
                "post" => "Post",
                "patch" => "Patch",
                "put" => "Put",
                _ => "Post",
            };
            format!("resp := client.{go_method}({u}, {body})")
        }
        Some(ControllerTestSend::HttpDelete { url }) => {
            let u = emit_go_url_expr(url, app, ctx);
            format!("resp := client.Delete({u})")
        }
        Some(ControllerTestSend::AssertResponse { sym }) => match sym.as_str() {
            "success" => "resp.AssertOk()".to_string(),
            "unprocessable_entity" => "resp.AssertUnprocessable()".to_string(),
            other => format!("resp.AssertStatus(200) // TODO: {other:?}"),
        },
        Some(ControllerTestSend::AssertRedirectedTo { url }) => {
            let u = emit_go_url_expr(url, app, ctx);
            format!("resp.AssertRedirectedTo({u})")
        }
        Some(ControllerTestSend::AssertSelect { selector, kind }) => {
            emit_go_assert_select_classified(selector, kind, app, ctx)
        }
        Some(ControllerTestSend::AssertDifference {
            method: _,
            count_expr,
            delta,
            block,
        }) => emit_go_assert_difference_classified(count_expr, delta, block, app, ctx),
        Some(ControllerTestSend::AssertEqual { expected, actual }) => {
            let e = emit_go_ctrl_test_expr(expected, app, ctx);
            let a = emit_go_ctrl_test_expr(actual, app, ctx);
            format!(
                "if {a} != {e} {{ t.Errorf(\"expected %v, got %v\", {e}, {a}) }}"
            )
        }
        None => {
            let args_s: Vec<String> = args
                .iter()
                .map(|a| emit_go_ctrl_test_expr(a, app, ctx))
                .collect();
            if args_s.is_empty() {
                method.to_string()
            } else {
                format!("{method}({})", args_s.join(", "))
            }
        }
    }
}

fn emit_go_url_expr(expr: &Expr, app: &App, ctx: &GoTestCtx) -> String {
    use crate::lower::UrlArg;
    let Some(helper) = crate::lower::classify_url_expr(expr) else {
        return emit_go_ctrl_test_expr(expr, app, ctx);
    };
    let helper_name = format!("{}Path", go_field_name(&helper.helper_base));
    let args_s: Vec<String> = helper
        .args
        .iter()
        .map(|a| match a {
            UrlArg::IvarOrVarId(name) => format!("{name}.ID"),
            UrlArg::ModelLast(class) => format!("{}Last().ID", class.as_str()),
            UrlArg::Raw(e) => emit_go_ctrl_test_expr(e, app, ctx),
        })
        .collect();
    format!("{helper_name}({})", args_s.join(", "))
}

fn emit_go_assert_select_classified(
    selector_expr: &Expr,
    kind: crate::lower::AssertSelectKind<'_>,
    app: &App,
    ctx: &GoTestCtx,
) -> String {
    use crate::lower::AssertSelectKind;
    let ExprNode::Lit { value: Literal::Str { value: selector } } =
        &*selector_expr.node
    else {
        return format!(
            "resp.AssertSelect({}) // TODO: dynamic selector",
            emit_go_ctrl_test_expr(selector_expr, app, ctx),
        );
    };
    match kind {
        AssertSelectKind::Text(expr) => {
            let text = emit_go_ctrl_test_expr(expr, app, ctx);
            format!("resp.AssertSelectText({selector:?}, {text})")
        }
        AssertSelectKind::Minimum(expr) => {
            let n = emit_go_ctrl_test_expr(expr, app, ctx);
            format!("resp.AssertSelectMin({selector:?}, int({n}))")
        }
        AssertSelectKind::SelectorBlock(b) => {
            let mut out = format!("resp.AssertSelect({selector:?})\n");
            let inner_body = match &*b.node {
                ExprNode::Lambda { body, .. } => body,
                _ => b,
            };
            for stmt in crate::lower::test_body_stmts(inner_body) {
                out.push_str(&emit_go_ctrl_test_stmt(stmt, app, ctx));
                out.push('\n');
            }
            out.trim_end().to_string()
        }
        AssertSelectKind::SelectorOnly => {
            format!("resp.AssertSelect({selector:?})")
        }
    }
}

fn emit_go_assert_difference_classified(
    count_expr_str: String,
    expected_delta: i64,
    block: Option<&Expr>,
    app: &App,
    ctx: &GoTestCtx,
) -> String {
    // `Article.count` → `ArticleCount()`. Mirrors the Go test send
    // rewrite for class-method calls.
    let count_expr = count_expr_str
        .split_once('.')
        .map(|(cls, m)| format!("{cls}{}()", pascalize_word(m)))
        .unwrap_or_else(|| count_expr_str.clone());
    let mut out = String::new();
    out.push_str(&format!("countBefore := {count_expr}\n"));
    if let Some(b) = block {
        let inner_body = match &*b.node {
            ExprNode::Lambda { body, .. } => body,
            _ => b,
        };
        for stmt in crate::lower::test_body_stmts(inner_body) {
            out.push_str(&emit_go_ctrl_test_stmt(stmt, app, ctx));
            out.push('\n');
        }
    }
    out.push_str(&format!("countAfter := {count_expr}\n"));
    out.push_str(&format!(
        "if countAfter - countBefore != {expected_delta} {{ t.Errorf(\"expected delta {expected_delta}, got %d\", countAfter - countBefore) }}",
    ));
    out
}

fn emit_go_ctrl_test_expr(expr: &Expr, app: &App, ctx: &GoTestCtx) -> String {
    match &*expr.node {
        ExprNode::Lit { value } => emit_literal(value),
        ExprNode::Ivar { name } => name.to_string(),
        ExprNode::Var { name, .. } => name.to_string(),
        ExprNode::Const { path } => {
            path.iter().map(|s| s.to_string()).collect::<Vec<_>>().join(".")
        }
        ExprNode::Send { recv: Some(r), method, args, .. } => {
            let m = method.as_str();
            // Class-level helpers: `Class.last`, `Class.count` →
            // package-level `ClassLast()` / `ClassCount()`.
            if m == "last" && args.is_empty() {
                if let ExprNode::Const { path } = &*r.node {
                    let class = path
                        .last()
                        .map(|s| s.as_str().to_string())
                        .unwrap_or_default();
                    return format!("{class}Last()");
                }
            }
            if m == "count" && args.is_empty() {
                if let ExprNode::Const { path } = &*r.node {
                    let class = path
                        .last()
                        .map(|s| s.as_str().to_string())
                        .unwrap_or_default();
                    return format!("{class}Count()");
                }
            }
            if args.is_empty() {
                let recv_s = match &*r.node {
                    ExprNode::Ivar { name } | ExprNode::Var { name, .. } => {
                        name.to_string()
                    }
                    _ => emit_go_ctrl_test_expr(r, app, ctx),
                };
                return format!("{recv_s}.{}", go_field_name(m));
            }
            let recv_s = emit_go_ctrl_test_expr(r, app, ctx);
            let args_s: Vec<String> =
                args.iter().map(|a| emit_go_ctrl_test_expr(a, app, ctx)).collect();
            format!(
                "{recv_s}.{}({})",
                go_method_name(m),
                args_s.join(", ")
            )
        }
        ExprNode::Send { recv: None, method, args, .. } => {
            if method.as_str().ends_with("_url") || method.as_str().ends_with("_path") {
                return emit_go_url_expr(expr, app, ctx);
            }
            let args_s: Vec<String> =
                args.iter().map(|a| emit_go_ctrl_test_expr(a, app, ctx)).collect();
            if args_s.is_empty() {
                method.to_string()
            } else {
                format!("{method}({})", args_s.join(", "))
            }
        }
        _ => "nil /* TODO expr */".to_string(),
    }
}

/// Flatten `{ article: { title: "X", body: "Y" } }` into a Go map
/// literal `map[string]string{"article[title]": "X", "article[body]":
/// "Y"}` matching the TestClient form-body shape.
fn flatten_go_params_to_form(
    expr: &Expr,
    scope: Option<&str>,
    app: &App,
    ctx: &GoTestCtx,
) -> String {
    let pairs: Vec<String> = crate::lower::flatten_params_pairs(expr, scope)
        .into_iter()
        .map(|(key, value)| {
            let v = emit_go_ctrl_test_expr(value, app, ctx);
            // Coerce non-string values via fmt.Sprintf so the
            // map[string]string form-body is satisfied.
            format!("{key:?}: fmt.Sprint({v})")
        })
        .collect();
    format!("map[string]string{{{}}}", pairs.join(", "))
}

#[derive(Clone, Copy)]
struct GoTestCtx<'a> {
    app: &'a App,
    fixture_names: &'a [crate::ident::Symbol],
    known_models: &'a [crate::ident::Symbol],
    model_attrs: &'a [crate::ident::Symbol],
}

fn emit_test_body_go(body: &Expr, ctx: GoTestCtx) -> String {
    match &*body.node {
        ExprNode::Seq { exprs } if !exprs.is_empty() => exprs
            .iter()
            .map(|e| emit_test_stmt_go(e, ctx))
            .collect::<Vec<_>>()
            .join("\n"),
        _ => emit_test_stmt_go(body, ctx),
    }
}

fn emit_test_stmt_go(e: &Expr, ctx: GoTestCtx) -> String {
    match &*e.node {
        ExprNode::Assign { target: LValue::Var { name, .. }, value } => {
            format!("{} := {}", name, emit_test_expr_go(value, ctx))
        }
        _ => emit_test_expr_go(e, ctx),
    }
}

fn emit_test_expr_go(e: &Expr, ctx: GoTestCtx) -> String {
    match &*e.node {
        ExprNode::Lit { value } => emit_literal(value),
        ExprNode::Var { name, .. } => name.to_string(),
        ExprNode::Const { path } => path
            .iter()
            .map(|s| s.to_string())
            .collect::<Vec<_>>()
            .join("."),
        ExprNode::Send { recv, method, args, block, .. } => {
            emit_test_send_go(recv.as_ref(), method.as_str(), args, block.as_ref(), ctx)
        }
        ExprNode::Hash { entries, .. } => {
            // Rough: only used inside Class.new(hash) — handled above.
            let parts: Vec<String> = entries
                .iter()
                .map(|(k, v)| format!("{}: {}", emit_test_expr_go(k, ctx), emit_test_expr_go(v, ctx)))
                .collect();
            format!("map[string]interface{{}}{{{}}}", parts.join(", "))
        }
        ExprNode::BoolOp { op, left, right, .. } => {
            use crate::expr::BoolOpKind;
            let op_s = match op {
                BoolOpKind::Or => "||",
                BoolOpKind::And => "&&",
            };
            format!(
                "{} {} {}",
                emit_test_expr_go(left, ctx),
                op_s,
                emit_test_expr_go(right, ctx)
            )
        }
        _ => format!("/* TODO: Go test emit for {:?} */", std::mem::discriminant(&*e.node)),
    }
}

fn emit_test_send_go(
    recv: Option<&Expr>,
    method: &str,
    args: &[Expr],
    block: Option<&Expr>,
    ctx: GoTestCtx,
) -> String {
    let args_s: Vec<String> = args.iter().map(|a| emit_test_expr_go(a, ctx)).collect();

    // Fixture accessor: articles(:one) → ArticlesOne()
    if recv.is_none()
        && args.len() == 1
        && ctx.fixture_names.iter().any(|s| s.as_str() == method)
    {
        if let ExprNode::Lit { value: Literal::Sym { value: sym } } = &*args[0].node {
            return format!(
                "{}{}()",
                go_field_name(method),
                go_field_name(sym.as_str())
            );
        }
    }

    // assert_difference("Class.count", delta) do ... end →
    // before/after capture + inline Go comparison. Parses the Ruby
    // string into a Go expression (`Comment.count` → `CommentCount()`).
    if recv.is_none() && method == "assert_difference" {
        if let Some(body) = block {
            if let Some(count_expr) = args
                .first()
                .and_then(|a| match &*a.node {
                    ExprNode::Lit { value: Literal::Str { value } } => {
                        rewrite_ruby_dot_call_go(value)
                    }
                    _ => None,
                })
            {
                let delta = args_s.get(1).cloned().unwrap_or_else(|| "1".into());
                let body_s = emit_block_body_go(body, ctx);
                return format!(
                    "_before := {count_expr}\n{body_s}\n_after := {count_expr}\nif _after - _before != int64({delta}) {{ t.Errorf(\"expected delta {delta}, got %d\", _after - _before) }}",
                );
            }
        }
    }

    // owner.<assoc>.create(hash) / .build(hash) — HasMany rewrite.
    // Renders as a Go IIFE so the surrounding assignment/expression
    // context still gets a value.
    if (method == "create" || method == "build") && args.len() == 1 {
        if let Some(outer_recv) = recv {
            if let ExprNode::Send {
                recv: Some(assoc_recv),
                method: assoc_method,
                args: inner_args,
                ..
            } = &*outer_recv.node
            {
                if inner_args.is_empty() {
                    if let Some(s) = try_emit_assoc_create_go(
                        assoc_recv,
                        assoc_method.as_str(),
                        args,
                        method,
                        ctx,
                    ) {
                        return s;
                    }
                }
            }
        }
    }

    // Assertion macros → Go's inline `if ... { t.Errorf(...) }` pattern.
    if recv.is_none() {
        match (method, args_s.len()) {
            ("assert_equal", 2) => {
                return format!(
                    "if {actual} != {expected} {{ t.Errorf(\"expected %v, got %v\", {expected}, {actual}) }}",
                    expected = args_s[0],
                    actual = args_s[1],
                );
            }
            ("assert_not_equal", 2) => {
                return format!(
                    "if {actual} == {expected} {{ t.Errorf(\"expected not equal to %v\", {expected}) }}",
                    expected = args_s[0],
                    actual = args_s[1],
                );
            }
            ("assert_not", 1) => {
                return format!(
                    "if {cond} {{ t.Errorf(\"expected false\") }}",
                    cond = args_s[0]
                );
            }
            ("assert", 1) => {
                return format!(
                    "if !({cond}) {{ t.Errorf(\"expected true\") }}",
                    cond = args_s[0]
                );
            }
            ("assert_nil", 1) => {
                // Go's "nil-ness" varies. Ints test against zero; pointers
                // against nil. We pick based on whether the arg expression
                // looks like a method chain (assume it's a primitive value).
                return format!(
                    "if {expr} != 0 {{ t.Errorf(\"expected zero/nil, got %v\", {expr}) }}",
                    expr = args_s[0]
                );
            }
            ("assert_not_nil", 1) => {
                return format!(
                    "if {expr} == 0 {{ t.Errorf(\"expected non-zero\") }}",
                    expr = args_s[0]
                );
            }
            _ => {}
        }
    }

    // `Class.new(hash)` → `&Class{ Field: val, ... }` struct literal.
    if let Some(r) = recv {
        if method == "new" && args.len() == 1 {
            if let ExprNode::Const { path } = &*r.node {
                if let Some(class_name) = path.last() {
                    if ctx.known_models.iter().any(|s| s == class_name) {
                        if let ExprNode::Hash { entries, .. } = &*args[0].node {
                            let pairs: Vec<String> = entries
                                .iter()
                                .filter_map(|(k, v)| {
                                    if let ExprNode::Lit {
                                        value: Literal::Sym { value: f },
                                    } = &*k.node
                                    {
                                        Some(format!(
                                            "{}: {}",
                                            go_field_name(f.as_str()),
                                            emit_test_expr_go(v, ctx)
                                        ))
                                    } else {
                                        None
                                    }
                                })
                                .collect();
                            return format!(
                                "&{}{{{}}}",
                                class_name,
                                pairs.join(", ")
                            );
                        }
                    }
                }
            }
        }
    }

    match recv {
        None => {
            if args_s.is_empty() {
                method.to_string()
            } else {
                format!("{}({})", method, args_s.join(", "))
            }
        }
        Some(r) => {
            let recv_s = emit_test_expr_go(r, ctx);
            let is_class_call = matches!(&*r.node, ExprNode::Const { .. });
            // Class-level calls flatten to `ClassMethod(args)` — Go
            // has no class methods, and our persistence emitter laid
            // down package-level `ArticleFind` / `CommentCount` /
            // etc. to stand in.
            if is_class_call {
                if args_s.is_empty() {
                    return format!("{recv_s}{}()", go_field_name(method));
                } else {
                    return format!(
                        "{recv_s}{}({})",
                        go_field_name(method),
                        args_s.join(", ")
                    );
                }
            }
            let is_attr = args_s.is_empty()
                && ctx.model_attrs.iter().any(|s| s.as_str() == method);
            if is_attr {
                format!("{recv_s}.{}", go_field_name(method))
            } else if args_s.is_empty() {
                format!("{recv_s}.{}()", go_field_name(method))
            } else {
                format!(
                    "{recv_s}.{}({})",
                    go_field_name(method),
                    args_s.join(", ")
                )
            }
        }
    }
}

/// Parse a Ruby-style `"Class.method"` expression into Go syntax.
/// Capitalized LHS → package-level function (`Comment.count` →
/// `CommentCount()`); lowercase LHS → instance field read.
fn rewrite_ruby_dot_call_go(expr: &str) -> Option<String> {
    let trimmed = expr.trim();
    let (lhs, rhs) = trimmed.split_once('.')?;
    let is_ident = |s: &str| {
        !s.is_empty()
            && s.chars().next().is_some_and(|c| c.is_alphabetic() || c == '_')
            && s.chars().all(|c| c.is_alphanumeric() || c == '_')
    };
    if !is_ident(lhs) || !is_ident(rhs) {
        return None;
    }
    let is_class = lhs.chars().next().is_some_and(|c| c.is_uppercase());
    if is_class {
        Some(format!("{lhs}{}()", go_field_name(rhs)))
    } else {
        Some(format!("{lhs}.{}", go_field_name(rhs)))
    }
}

/// Render a Ruby block body as Go statements, peeling one Lambda
/// layer (Ruby `do ... end` lowers to `ExprNode::Lambda` in the IR).
fn emit_block_body_go(e: &Expr, ctx: GoTestCtx) -> String {
    let inner = match &*e.node {
        ExprNode::Lambda { body, .. } => body,
        _ => e,
    };
    match &*inner.node {
        ExprNode::Seq { exprs } if !exprs.is_empty() => exprs
            .iter()
            .map(|s| emit_test_stmt_go(s, ctx))
            .collect::<Vec<_>>()
            .join("\n"),
        _ => emit_test_stmt_go(inner, ctx),
    }
}

fn try_emit_assoc_create_go(
    owner: &Expr,
    assoc_name: &str,
    args: &[Expr],
    outer_method: &str,
    ctx: GoTestCtx,
) -> Option<String> {
    let resolved = crate::lower::resolve_has_many(
        &crate::ident::Symbol::from(assoc_name),
        owner.ty.as_ref(),
        ctx.app,
    )?;
    let target_class = resolved.target_class.0.as_str();
    let foreign_key = go_field_name(resolved.foreign_key.as_str());

    let owner_s = emit_test_expr_go(owner, ctx);
    let hash_entries = match &args.first()?.node.as_ref() {
        ExprNode::Hash { entries, .. } => entries.clone(),
        _ => return None,
    };

    let mut pairs: Vec<String> =
        vec![format!("{foreign_key}: {owner_s}.ID")];
    for (k, v) in &hash_entries {
        if let ExprNode::Lit { value: Literal::Sym { value: field_name } } = &*k.node {
            pairs.push(format!(
                "{}: {}",
                go_field_name(field_name.as_str()),
                emit_test_expr_go(v, ctx),
            ));
        }
    }
    let struct_lit = format!("&{target_class}{{{}}}", pairs.join(", "));
    // Go IIFE — wraps the multi-statement build+save+return so the
    // caller can use it in an expression position (e.g. RHS of :=).
    if outer_method == "create" {
        Some(format!(
            "func() *{target_class} {{ r := {struct_lit}; r.Save(); return r }}()"
        ))
    } else {
        Some(format!(
            "func() *{target_class} {{ r := {struct_lit}; return r }}()"
        ))
    }
}

