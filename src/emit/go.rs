//! Go emitter.
//!
//! Second typed target. Differences from Rust that surface design choices:
//!
//! - Go has no Option / Result — nullable types are pointers (`*T`).
//! - Go has no class methods — `Post.All()` is pseudo-Go; a real mapping
//!   would use a repository pattern or package-level functions.
//! - Go requires explicit `return` for non-void functions and omits the
//!   return type entirely for void functions.
//! - Go convention: `ID`, `URL`, `HTTP` for initialism fields; PascalCase
//!   for exported identifiers, camelCase for unexported.
//! - Go uses tabs for indentation; gofmt would realign struct fields.
//!   We emit single-tab indent without alignment.
//!
//! Output is pseudo-Go — won't compile as-is. The goal is to prove that
//! types flow through to a second typed target without the Rust emitter
//! accidentally hiding ambiguities.

use std::fmt::Write;
use std::path::PathBuf;

use super::EmittedFile;
use crate::App;
use crate::dialect::{Controller, MethodDef, Model, Test, TestModule};
use crate::expr::{Expr, ExprNode, LValue, Literal};
use crate::ident::Symbol;
use crate::naming::snake_case;
use crate::ty::Ty;

const RUNTIME_SOURCE: &str = include_str!("../../runtime/go/runtime.go");
const DB_SOURCE: &str = include_str!("../../runtime/go/db.go");
/// Go HTTP runtime — Phase 4d pass-2 shape. Copied verbatim into
/// generated projects as `app/http.go` whenever any controller emits.
/// Provides ActionResponse, ActionContext, Router, plus Phase 4c
/// compile-only stubs for any leftover legacy references.
const HTTP_SOURCE: &str = include_str!("../../runtime/go/http.go");
/// Go test-support runtime — TestClient + TestResponse. Copied
/// verbatim as `app/test_support.go` whenever controllers emit.
const TEST_SUPPORT_SOURCE: &str = include_str!("../../runtime/go/test_support.go");

pub fn emit(app: &App) -> Vec<EmittedFile> {
    let mut files = Vec::new();
    files.push(emit_go_mod());
    files.push(emit_go_sum());
    if !app.models.is_empty() {
        files.push(emit_models(app));
        files.push(EmittedFile {
            path: PathBuf::from("app/runtime.go"),
            content: RUNTIME_SOURCE.to_string(),
        });
        files.push(EmittedFile {
            path: PathBuf::from("app/db.go"),
            content: DB_SOURCE.to_string(),
        });
        files.push(emit_schema_sql_go(app));
    }
    if !app.controllers.is_empty() {
        // HTTP runtime + TestClient — copied verbatim, same posture as
        // runtime.go / db.go. Provides ActionResponse, ActionContext,
        // Router, and (still) the Phase 4c stubs.
        files.push(EmittedFile {
            path: PathBuf::from("app/http.go"),
            content: HTTP_SOURCE.to_string(),
        });
        files.push(EmittedFile {
            path: PathBuf::from("app/test_support.go"),
            content: TEST_SUPPORT_SOURCE.to_string(),
        });
        let known_models: Vec<Symbol> =
            app.models.iter().map(|m| m.name.0.clone()).collect();
        for controller in &app.controllers {
            files.push(emit_controller_pass2(controller, &known_models, app));
        }
        files.push(emit_go_route_helpers(app));
        files.push(emit_go_routes(app));
        files.push(emit_go_views(app, &known_models));
    }
    if !app.fixtures.is_empty() {
        files.push(emit_go_fixtures(app));
    }
    if !app.test_modules.is_empty() {
        for tm in &app.test_modules {
            files.push(emit_go_tests(tm, app));
        }
    }
    files
}

/// `go.mod` — module name `app`, targeting Go 1.24. Depends on
/// modernc.org/sqlite for the pure-Go SQLite driver that the Phase 3
/// persistence runtime uses. The toolchain test runs `go mod tidy`
/// before building to populate go.sum.
fn emit_go_mod() -> EmittedFile {
    let content = "module app\n\ngo 1.24\n\nrequire modernc.org/sqlite v1.34.1\n";
    EmittedFile {
        path: PathBuf::from("go.mod"),
        content: content.to_string(),
    }
}

/// Empty `go.sum` placeholder — `go mod tidy` populates it on first
/// build. Emitting the file up front avoids a chicken-and-egg where
/// `go vet` refuses to run without it.
fn emit_go_sum() -> EmittedFile {
    EmittedFile {
        path: PathBuf::from("go.sum"),
        content: String::new(),
    }
}

/// `app/schema_sql.go` — Go constant wrapping the target-neutral DDL
/// produced by `lower::lower_schema`. `SetupTestDB` splits it on
/// `;\n` and execs each statement against a fresh :memory: database.
fn emit_schema_sql_go(app: &App) -> EmittedFile {
    let ddl = crate::lower::lower_schema(&app.schema);
    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "const CreateTables = `").unwrap();
    s.push_str(&ddl);
    writeln!(s, "`").unwrap();
    EmittedFile {
        path: PathBuf::from("app/schema_sql.go"),
        content: s,
    }
}

// Models ---------------------------------------------------------------

fn emit_models(app: &App) -> EmittedFile {
    let mut body = String::new();
    for model in &app.models {
        writeln!(body).unwrap();
        emit_struct(&mut body, model);
        for method in model.methods() {
            writeln!(body).unwrap();
            emit_model_method(&mut body, model, method);
        }
        let lowered = crate::lower::lower_validations(model);
        if !lowered.is_empty() {
            writeln!(body).unwrap();
            emit_validate_method_go(&mut body, model, &lowered);
        }
        // Persistence emitted for any model with table columns beyond
        // `id`. Abstract base classes (ApplicationRecord) get skipped.
        let has_table = model
            .attributes
            .fields
            .keys()
            .any(|k| k.as_str() != "id");
        if has_table {
            writeln!(body).unwrap();
            emit_persistence_methods_go(&mut body, model, !lowered.is_empty(), app);
        }
    }
    // Conditional imports: scan the emitted body for package prefixes
    // that indicate which stdlib we need. Go errors on unused imports,
    // so we only emit what's actually referenced.
    let mut imports: Vec<&str> = Vec::new();
    if body.contains("strings.") {
        imports.push("strings");
    }

    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    if !imports.is_empty() {
        writeln!(s).unwrap();
        if imports.len() == 1 {
            writeln!(s, "import \"{}\"", imports[0]).unwrap();
        } else {
            writeln!(s, "import (").unwrap();
            for imp in &imports {
                writeln!(s, "\t\"{imp}\"").unwrap();
            }
            writeln!(s, ")").unwrap();
        }
    }
    s.push_str(&body);
    EmittedFile { path: PathBuf::from("app/models.go"), content: s }
}

fn emit_struct(out: &mut String, model: &Model) {
    writeln!(out, "type {} struct {{", model.name.0).unwrap();
    for (name, ty) in &model.attributes.fields {
        writeln!(out, "\t{} {}", go_field_name(name.as_str()), go_ty(ty)).unwrap();
    }
    writeln!(out, "}}").unwrap();
}

fn emit_model_method(out: &mut String, model: &Model, m: &MethodDef) {
    let ret_ty = m.body.ty.clone().unwrap_or(Ty::Nil);
    let method_name = go_method_name(m.name.as_str());
    let ret_annot = match &ret_ty {
        Ty::Nil => String::new(),
        other => format!(" {}", go_ty(other)),
    };
    let (receiver, receiver_var) = match m.receiver {
        crate::dialect::MethodReceiver::Instance => {
            // Single-letter receiver name — Go idiom (`p *Post`).
            let first = model
                .name
                .0
                .as_str()
                .chars()
                .next()
                .unwrap_or('r')
                .to_ascii_lowercase();
            (format!("({first} *{})", model.name.0), first.to_string())
        }
        crate::dialect::MethodReceiver::Class => {
            // Class method: emit as package-level function (pseudo-Go, since
            // Go has no class methods).
            (String::new(), String::new())
        }
    };
    let space_before_name = if receiver.is_empty() { "" } else { " " };
    writeln!(
        out,
        "func {}{}{}(){} {{",
        receiver, space_before_name, method_name, ret_annot,
    )
    .unwrap();
    // Pre-emit rewrite: bare `title` inside a method body → `p.Title`
    // (receiver-var + PascalCased field). Same pattern as the other
    // typed emitters; Go makes it the receiver variable rather than
    // `self`/`this`.
    let attrs: Vec<Symbol> = model.attributes.fields.keys().cloned().collect();
    let rewritten = if !receiver_var.is_empty() {
        rewrite_bare_attrs_to_receiver(&m.body, &attrs, &receiver_var)
    } else {
        m.body.clone()
    };
    let body_s = if matches!(ret_ty, Ty::Nil) {
        emit_expr(&rewritten)
    } else {
        format!("return {}", emit_expr(&rewritten))
    };
    for line in body_s.lines() {
        writeln!(out, "\t{}", line).unwrap();
    }
    writeln!(out, "}}").unwrap();
}


// Pass-2 controller emit ----------------------------------------------
//
// Each public action becomes a top-level `func ArticlesIndex(ctx *
// ActionContext) ActionResponse` returning ActionResponse directly.
// Replaces the Phase 4c struct-method-with-no-body emit. The
// router registers these as Handler values at package init time.

fn emit_controller_pass2(c: &Controller, known_models: &[Symbol], app: &App) -> EmittedFile {
    let name = c.name.0.as_str();
    let resource = crate::lower::resource_from_controller_name(name);
    let model_class = crate::naming::singularize_camelize(&resource);
    let has_model = known_models.iter().any(|m| m.as_str() == model_class);
    let parent = crate::lower::find_nested_parent(app, name);
    let permitted = crate::lower::permitted_fields_for(c, &resource)
        .unwrap_or_else(|| crate::lower::default_permitted_fields(app, &model_class));

    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "import \"strconv\"").unwrap();

    // Track whether we actually use strconv so we can emit a blank
    // identifier reference if not (Go errors on unused imports).
    // Easier: always reference it inside one no-op (`var _ = strconv.Itoa`).
    writeln!(s).unwrap();
    writeln!(s, "var _ = strconv.Itoa").unwrap();

    let (public_actions, _private) = crate::lower::split_public_private(c);
    for action in &public_actions {
        writeln!(s).unwrap();
        let la = crate::lower::lower_action(
            action.name.as_str(),
            &resource,
            &model_class,
            has_model,
            parent.as_ref(),
            &permitted,
        );
        emit_go_action(&mut s, c.name.0.as_str(), &la);
    }

    let fname = format!("app/{}.go", snake_case(name));
    EmittedFile { path: PathBuf::from(fname), content: s }
}

fn go_action_handler_name(controller: &str, action: &str) -> String {
    let base = controller.strip_suffix("Controller").unwrap_or(controller);
    format!("{}{}", base, pascalize_word(action))
}

fn go_view_fn(model_class: &str, suffix: &str) -> String {
    let plural = crate::naming::pluralize_snake(model_class);
    format!("Render{}{}", pascalize_word(&plural), pascalize_word(suffix))
}

/// Render one LoweredAction as a top-level Go handler function.
/// Replaces the seven per-action helpers; matches on
/// `LoweredAction::kind` and reaches into the struct for the data
/// each branch needs.
fn emit_go_action(out: &mut String, controller: &str, la: &crate::lower::LoweredAction) {
    use crate::lower::ActionKind;
    let handler = go_action_handler_name(controller, &la.name);
    let model_class = la.model_class.as_str();
    let resource = la.resource.as_str();

    let emit_empty_stub = |out: &mut String, reads_ctx: bool| {
        let ctx_name = if reads_ctx { "ctx" } else { "_ctx" };
        writeln!(out, "func {handler}({ctx_name} *ActionContext) ActionResponse {{").unwrap();
        if reads_ctx {
            writeln!(out, "\t_ = ctx").unwrap();
        }
        writeln!(out, "\treturn ActionResponse{{}}").unwrap();
        writeln!(out, "}}").unwrap();
    };

    let emit_find_id = |out: &mut String| {
        writeln!(out, "\tid, _ := strconv.ParseInt(ctx.Params[\"id\"], 10, 64)").unwrap();
        writeln!(out, "\trecord := {model_class}Find(id)").unwrap();
        writeln!(out, "\tif record == nil {{ record = &{model_class}{{}} }}").unwrap();
    };

    let redirect_after_write = |out: &mut String, indent: &str| {
        if let Some(p) = &la.parent {
            writeln!(
                out,
                "{indent}parentID, _ := strconv.ParseInt(ctx.Params[\"{}_id\"], 10, 64)",
                p.singular
            )
            .unwrap();
            writeln!(
                out,
                "{indent}return ActionResponse{{Status: 303, Location: {}Path(parentID)}}",
                pascalize_word(&p.singular)
            )
            .unwrap();
        } else {
            writeln!(
                out,
                "{indent}return ActionResponse{{Status: 303, Location: {}Path(record.ID)}}",
                pascalize_word(resource)
            )
            .unwrap();
        }
    };

    match la.kind {
        ActionKind::Index => {
            let view_fn = go_view_fn(model_class, "index");
            writeln!(out, "func {handler}(_ctx *ActionContext) ActionResponse {{").unwrap();
            if la.has_model {
                writeln!(out, "\trecords := {model_class}All()").unwrap();
                writeln!(out, "\treturn ActionResponse{{Body: {view_fn}(records)}}").unwrap();
            } else {
                writeln!(out, "\treturn ActionResponse{{}}").unwrap();
            }
            writeln!(out, "}}").unwrap();
        }
        ActionKind::Show | ActionKind::Edit => {
            let suffix = if la.kind == ActionKind::Show { "show" } else { "edit" };
            let view_fn = go_view_fn(model_class, suffix);
            if !la.has_model {
                emit_empty_stub(out, true);
                return;
            }
            writeln!(out, "func {handler}(ctx *ActionContext) ActionResponse {{").unwrap();
            emit_find_id(out);
            writeln!(out, "\treturn ActionResponse{{Body: {view_fn}(record)}}").unwrap();
            writeln!(out, "}}").unwrap();
        }
        ActionKind::New => {
            let view_fn = go_view_fn(model_class, "new");
            writeln!(out, "func {handler}(_ctx *ActionContext) ActionResponse {{").unwrap();
            if la.has_model {
                writeln!(out, "\trecord := &{model_class}{{}}").unwrap();
                writeln!(out, "\treturn ActionResponse{{Body: {view_fn}(record)}}").unwrap();
            } else {
                writeln!(out, "\treturn ActionResponse{{}}").unwrap();
            }
            writeln!(out, "}}").unwrap();
        }
        ActionKind::Create => {
            if !la.has_model {
                emit_empty_stub(out, true);
                return;
            }
            writeln!(out, "func {handler}(ctx *ActionContext) ActionResponse {{").unwrap();
            writeln!(out, "\trecord := &{model_class}{{}}").unwrap();
            if let Some(p) = &la.parent {
                let fk_field = go_field_name(&format!("{}_id", p.singular));
                writeln!(
                    out,
                    "\trecord.{fk_field}, _ = strconv.ParseInt(ctx.Params[\"{}_id\"], 10, 64)",
                    p.singular
                )
                .unwrap();
            }
            for field in &la.permitted {
                let go_field = go_field_name(field);
                writeln!(
                    out,
                    "\trecord.{go_field} = ctx.Params[\"{resource}[{field}]\"]"
                )
                .unwrap();
            }
            writeln!(out, "\tif record.Save() {{").unwrap();
            redirect_after_write(out, "\t\t");
            writeln!(out, "\t}}").unwrap();
            if la.parent.is_some() {
                redirect_after_write(out, "\t");
            } else {
                let view_fn = go_view_fn(model_class, "new");
                writeln!(
                    out,
                    "\treturn ActionResponse{{Status: 422, Body: {view_fn}(record)}}"
                )
                .unwrap();
            }
            writeln!(out, "}}").unwrap();
        }
        ActionKind::Update => {
            if !la.has_model {
                emit_empty_stub(out, true);
                return;
            }
            writeln!(out, "func {handler}(ctx *ActionContext) ActionResponse {{").unwrap();
            emit_find_id(out);
            for field in &la.permitted {
                let go_field = go_field_name(field);
                writeln!(
                    out,
                    "\tif v, ok := ctx.Params[\"{resource}[{field}]\"]; ok {{ record.{go_field} = v }}"
                )
                .unwrap();
            }
            writeln!(out, "\tif record.Save() {{").unwrap();
            writeln!(
                out,
                "\t\treturn ActionResponse{{Status: 303, Location: {}Path(record.ID)}}",
                pascalize_word(resource)
            )
            .unwrap();
            writeln!(out, "\t}}").unwrap();
            let view_fn = go_view_fn(model_class, "edit");
            writeln!(
                out,
                "\treturn ActionResponse{{Status: 422, Body: {view_fn}(record)}}"
            )
            .unwrap();
            writeln!(out, "}}").unwrap();
        }
        ActionKind::Destroy => {
            if !la.has_model {
                emit_empty_stub(out, true);
                return;
            }
            writeln!(out, "func {handler}(ctx *ActionContext) ActionResponse {{").unwrap();
            writeln!(out, "\tid, _ := strconv.ParseInt(ctx.Params[\"id\"], 10, 64)").unwrap();
            writeln!(out, "\trecord := {model_class}Find(id)").unwrap();
            writeln!(out, "\tif record != nil {{ record.Destroy() }}").unwrap();
            if let Some(p) = &la.parent {
                writeln!(
                    out,
                    "\tparentID, _ := strconv.ParseInt(ctx.Params[\"{}_id\"], 10, 64)",
                    p.singular
                )
                .unwrap();
                writeln!(
                    out,
                    "\treturn ActionResponse{{Status: 303, Location: {}Path(parentID)}}",
                    pascalize_word(&p.singular)
                )
                .unwrap();
            } else {
                let plural = crate::naming::pluralize_snake(model_class);
                writeln!(
                    out,
                    "\treturn ActionResponse{{Status: 303, Location: {}Path()}}",
                    pascalize_word(&plural)
                )
                .unwrap();
            }
            writeln!(out, "}}").unwrap();
        }
        ActionKind::Unknown => {
            writeln!(
                out,
                "func {handler}(_ctx *ActionContext) ActionResponse {{"
            )
            .unwrap();
            writeln!(out, "\treturn ActionResponse{{Status: 501}}").unwrap();
            writeln!(out, "}}").unwrap();
        }
    }
}

// Pass-2 routes + route helpers ---------------------------------------

fn emit_go_route_helpers(app: &App) -> EmittedFile {
    let flat = crate::lower::flatten_routes(app);
    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "import \"strconv\"").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "var _ = strconv.Itoa").unwrap();

    use std::collections::BTreeSet;
    let mut seen: BTreeSet<String> = BTreeSet::new();
    for route in &flat {
        if !seen.insert(route.as_name.clone()) {
            continue;
        }
        let fn_name = format!("{}Path", go_field_name(&route.as_name));
        let params: Vec<String> = route
            .path_params
            .iter()
            .map(|p| format!("{} int64", go_param_name(p)))
            .collect();
        let sig = params.join(", ");
        let body = if route.path_params.is_empty() {
            format!("\treturn {:?}", route.path)
        } else {
            // Build path with strconv.FormatInt.
            let mut chars = route.path.chars().peekable();
            let mut parts: Vec<String> = Vec::new();
            let mut current = String::new();
            while let Some(c) = chars.next() {
                if c == ':' {
                    let mut ident = String::new();
                    while let Some(&nc) = chars.peek() {
                        if nc.is_alphanumeric() || nc == '_' {
                            ident.push(nc);
                            chars.next();
                        } else {
                            break;
                        }
                    }
                    if !current.is_empty() {
                        parts.push(format!("{current:?}"));
                        current.clear();
                    }
                    let go_param = go_param_name(&ident);
                    parts.push(format!("strconv.FormatInt({go_param}, 10)"));
                } else {
                    current.push(c);
                }
            }
            if !current.is_empty() {
                parts.push(format!("{current:?}"));
            }
            format!("\treturn {}", parts.join(" + "))
        };
        writeln!(s).unwrap();
        writeln!(s, "func {fn_name}({sig}) string {{").unwrap();
        for line in body.lines() {
            writeln!(s, "{line}").unwrap();
        }
        writeln!(s, "}}").unwrap();
    }
    EmittedFile {
        path: PathBuf::from("app/route_helpers.go"),
        content: s,
    }
}

fn emit_go_routes(app: &App) -> EmittedFile {
    let flat = crate::lower::flatten_routes(app);
    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "func init() {{").unwrap();
    writeln!(s, "\tRouter.Reset()").unwrap();
    for route in &flat {
        let verb = go_http_verb(&route.method);
        let handler = go_action_handler_name(route.controller.0.as_str(), route.action.as_str());
        writeln!(s, "\tRouter.{verb}({:?}, {})", route.path, handler).unwrap();
    }
    writeln!(s, "}}").unwrap();
    EmittedFile {
        path: PathBuf::from("app/routes.go"),
        content: s,
    }
}

/// Map `dialect::HttpMethod` to the Go router's Title-cased verb
/// name (`Get` / `Post` / etc.). Unknown methods fall back to `Get`
/// — the emitted code is for pass-2 tests, not production routing.
fn go_http_verb(m: &crate::dialect::HttpMethod) -> &'static str {
    use crate::dialect::HttpMethod;
    match m {
        HttpMethod::Get => "Get",
        HttpMethod::Post => "Post",
        HttpMethod::Put => "Put",
        HttpMethod::Patch => "Patch",
        HttpMethod::Delete => "Delete",
        _ => "Get",
    }
}

// Pass-2 view emit ----------------------------------------------------
//
// Walks the View IR and emits a `RenderArticlesIndex(records []*Article)
// string` function per view. Literal HTML chunks pass through as
// `_buf += "..."`; complex interpolations + unknown sends drop to a
// TODO comment + empty append. Substring assertions in controller
// tests match the surviving HTML literals.

fn emit_go_views(app: &App, known_models: &[Symbol]) -> EmittedFile {
    let attrs_by_class: std::collections::BTreeMap<String, Vec<String>> = app
        .models
        .iter()
        .map(|m| {
            (
                m.name.0.as_str().to_string(),
                m.attributes
                    .fields
                    .keys()
                    .map(|k| k.as_str().to_string())
                    .collect(),
            )
        })
        .collect();

    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "import (").unwrap();
    writeln!(s, "\t\"fmt\"").unwrap();
    writeln!(s, "\t\"strconv\"").unwrap();
    writeln!(s, ")").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "var _ = fmt.Sprint").unwrap();
    writeln!(s, "var _ = strconv.Itoa").unwrap();

    let mut emitted: std::collections::BTreeSet<String> =
        std::collections::BTreeSet::new();
    for v in &app.views {
        let fn_name = go_view_function_name(v.name.as_str());
        if !emitted.insert(fn_name.clone()) {
            continue;
        }
        writeln!(s).unwrap();
        emit_go_view_file(&mut s, v, known_models, &attrs_by_class);
    }
    // Stub missing standard CRUD views so controllers always link.
    for model in &app.models {
        if model.attributes.fields.is_empty() {
            continue;
        }
        let class = model.name.0.as_str();
        let plural = crate::naming::pluralize_snake(class);
        for suffix in ["index", "show", "new", "edit"] {
            let view_name = format!("{plural}/{suffix}");
            let fn_name = go_view_function_name(&view_name);
            if emitted.insert(fn_name.clone()) {
                let arg_ty = if suffix == "index" {
                    format!("[]*{class}")
                } else {
                    format!("*{class}")
                };
                writeln!(s).unwrap();
                writeln!(s, "func {fn_name}(_record {arg_ty}) string {{").unwrap();
                writeln!(s, "\treturn \"\"").unwrap();
                writeln!(s, "}}").unwrap();
            }
        }
    }
    EmittedFile {
        path: PathBuf::from("app/views.go"),
        content: s,
    }
}

fn go_view_function_name(name: &str) -> String {
    let mut parts: Vec<String> = Vec::new();
    for seg in name.split('/') {
        let trimmed = seg.strip_prefix('_').unwrap_or(seg);
        parts.push(pascalize_word(trimmed));
    }
    format!("Render{}", parts.join(""))
}

fn emit_go_view_file(
    out: &mut String,
    view: &crate::dialect::View,
    known_models: &[Symbol],
    attrs_by_class: &std::collections::BTreeMap<String, Vec<String>>,
) {
    let fn_name = go_view_function_name(view.name.as_str());
    let (sig, arg_name, arg_class) = go_view_signature(view.name.as_str(), known_models);
    let attrs = arg_class
        .as_ref()
        .and_then(|c| attrs_by_class.get(c).cloned())
        .unwrap_or_default();
    let resource_dir = view
        .name
        .as_str()
        .rsplit_once('/')
        .map(|(d, _): (&str, &str)| d.to_string())
        .unwrap_or_default();

    writeln!(out, "func {fn_name}({sig}) string {{").unwrap();
    writeln!(out, "\t_ = {arg_name}").unwrap();
    writeln!(out, "\t_buf := \"\"").unwrap();

    let ctx = GoViewCtx {
        arg_name: arg_name.clone(),
        arg_attrs: attrs,
        resource_dir,
    };
    let lines = emit_go_view_body(&view.body, &ctx);
    for line in &lines {
        writeln!(out, "\t{line}").unwrap();
    }
    writeln!(out, "\treturn _buf").unwrap();
    writeln!(out, "}}").unwrap();
}

struct GoViewCtx {
    arg_name: String,
    arg_attrs: Vec<String>,
    resource_dir: String,
}

impl GoViewCtx {
    fn arg_has_attr(&self, name: &str, attr: &str) -> bool {
        name == self.arg_name && self.arg_attrs.iter().any(|a| a == attr)
    }
}

fn go_view_signature(
    view_name: &str,
    known_models: &[Symbol],
) -> (String, String, Option<String>) {
    let (dir, base) = view_name.rsplit_once('/').unwrap_or(("", view_name));
    let is_partial = base.starts_with('_');
    let stem = base.trim_start_matches('_');
    let model_class = crate::naming::singularize_camelize(dir);
    let model_exists = known_models.iter().any(|m| m.as_str() == model_class);
    let singular = crate::naming::singularize(dir);

    if is_partial {
        let arg_name = if model_exists { singular.clone() } else { stem.to_string() };
        if model_exists {
            return (format!("{arg_name} *{model_class}"), arg_name, Some(model_class));
        }
        return (format!("{arg_name} interface{{}}"), arg_name, None);
    }
    match stem {
        "index" => {
            let arg_name = dir.to_string();
            if model_exists {
                return (
                    format!("{arg_name} []*{model_class}"),
                    arg_name,
                    Some(model_class),
                );
            }
            (format!("{arg_name} []interface{{}}"), arg_name, None)
        }
        _ => {
            let arg_name = singular.clone();
            if model_exists {
                return (format!("{arg_name} *{model_class}"), arg_name, Some(model_class));
            }
            (format!("{arg_name} interface{{}}"), arg_name, None)
        }
    }
}

fn emit_go_view_body(body: &Expr, ctx: &GoViewCtx) -> Vec<String> {
    let stmts: Vec<&Expr> = match &*body.node {
        ExprNode::Seq { exprs } => exprs.iter().collect(),
        _ => vec![body],
    };
    let mut out = Vec::new();
    for stmt in &stmts {
        out.extend(emit_go_view_stmt(stmt, ctx));
    }
    out
}

fn emit_go_view_stmt(stmt: &Expr, ctx: &GoViewCtx) -> Vec<String> {
    match &*stmt.node {
        // Prologue _buf assignment is emitted by emit_go_view_file.
        ExprNode::Assign { target: LValue::Var { name, .. }, value }
            if name.as_str() == "_buf" =>
        {
            if let ExprNode::Lit { value: Literal::Str { value: s } } = &*value.node {
                if s.is_empty() {
                    return Vec::new();
                }
            }
            // `_buf = _buf + X` shape.
            if let ExprNode::Send { recv: Some(recv), method, args, .. } = &*value.node {
                if method.as_str() == "+" && args.len() == 1 {
                    if let ExprNode::Var { name: rn, .. } = &*recv.node {
                        if rn.as_str() == "_buf" {
                            return emit_go_view_append(&args[0], ctx);
                        }
                    }
                }
            }
            vec!["// TODO view: unknown _buf shape".to_string()]
        }
        // Trailing `_buf` reference.
        ExprNode::Var { name, .. } if name.as_str() == "_buf" => Vec::new(),
        ExprNode::Ivar { .. } => Vec::new(),
        ExprNode::If { cond, then_branch, else_branch } => {
            let cond_go = if is_go_simple_cond(cond, ctx) {
                emit_go_view_expr_raw(cond, ctx)
            } else {
                "false".to_string()
            };
            let mut out = vec![format!("if {cond_go} {{")];
            for line in emit_go_view_body(then_branch, ctx) {
                out.push(format!("\t{line}"));
            }
            // Always emit an else branch; Go is fine with empty blocks.
            let else_lines = emit_go_view_body(else_branch, ctx);
            if !else_lines.is_empty() {
                out.push("} else {".to_string());
                for line in else_lines {
                    out.push(format!("\t{line}"));
                }
            }
            out.push("}".to_string());
            out
        }
        _ => vec!["// TODO view: unknown stmt".to_string()],
    }
}

fn emit_go_view_append(arg: &Expr, ctx: &GoViewCtx) -> Vec<String> {
    // Text chunk.
    if let ExprNode::Lit { value: Literal::Str { value: s } } = &*arg.node {
        return vec![format!("_buf += {}", go_string_literal(s))];
    }
    let inner = unwrap_to_s_go(arg);

    // `render @coll` or `render "partial", locals` — expand to a
    // partial function call. Mirrors Python's emit_py_render_call.
    if let ExprNode::Send { recv: None, method, args, block: None, .. } = &*inner.node {
        if method.as_str() == "render" {
            if args.len() == 1 {
                return emit_go_render_call(&args[0], ctx);
            }
            if args.len() == 2 {
                if let (
                    ExprNode::Lit { value: Literal::Str { value: partial } },
                    ExprNode::Hash { .. },
                ) = (&*args[0].node, &*args[1].node)
                {
                    let partial_trim = partial.trim_start_matches('_');
                    let partial_fn = format!(
                        "Render{}{}",
                        pascalize_word(&ctx.resource_dir),
                        pascalize_word(partial_trim),
                    );
                    // Go lacks kwargs; just pass nil interface to
                    // acknowledge the partial. Stubs are nilable.
                    return vec![format!("_buf += {partial_fn}(nil)")];
                }
            }
        }
    }

    // form_with — capturing helper.
    if let ExprNode::Send {
        recv: None,
        method,
        args: _form_args,
        block: Some(block),
        ..
    } = &*inner.node
    {
        if method.as_str() == "form_with" {
            return emit_go_form_with(block, ctx);
        }
    }

    if is_go_simple_cond(inner, ctx) {
        return vec![format!("_buf += {}", emit_go_view_expr_raw(inner, ctx))];
    }
    // Fall back to empty append + TODO marker.
    vec!["_buf += \"\" // TODO view: complex interpolation".to_string()]
}

/// Emit a `render @coll` / `render parent.assoc` call by expanding
/// into a Go `for` loop that concatenates the partial's rendering.
fn emit_go_render_call(arg: &Expr, _ctx: &GoViewCtx) -> Vec<String> {
    match &*arg.node {
        ExprNode::Var { name, .. } | ExprNode::Ivar { name } => {
            let singular = crate::naming::singularize(name.as_str());
            let partial_fn =
                format!("Render{}{}", pascalize_word(name.as_str()), pascalize_word(&singular));
            let coll = name.to_string();
            vec![format!(
                "for _, _r := range {coll} {{ _buf += {partial_fn}(_r) }}"
            )]
        }
        ExprNode::Send { recv: Some(r), method, args, .. }
            if args.is_empty()
                && matches!(&*r.node, ExprNode::Var { .. } | ExprNode::Ivar { .. }) =>
        {
            let assoc_plural = method.as_str();
            let singular = crate::naming::singularize(assoc_plural);
            let partial_fn = format!(
                "Render{}{}",
                pascalize_word(assoc_plural),
                pascalize_word(&singular),
            );
            let parent_name = match &*r.node {
                ExprNode::Var { name, .. } | ExprNode::Ivar { name } => name.to_string(),
                _ => unreachable!(),
            };
            vec![format!(
                "for _, _c := range {parent_name}.{}() {{ _buf += {partial_fn}(_c) }}",
                pascalize_word(assoc_plural),
            )]
        }
        _ => vec!["_buf += \"\" // TODO view: render".to_string()],
    }
}

/// `form_with` opens a `<form>` tag and wraps the inner body. We
/// emit the `<form>` markers directly around the block rendering —
/// enough for `assert_select("form")` to match substring. Matches
/// the Python snapshot-and-slice idea but keeps Go types simple.
fn emit_go_form_with(block: &Expr, ctx: &GoViewCtx) -> Vec<String> {
    let body = match &*block.node {
        ExprNode::Lambda { body, .. } => body,
        _ => block,
    };
    let mut lines = vec!["_buf += \"<form>\"".to_string()];
    for line in emit_go_view_body(body, ctx) {
        lines.push(line);
    }
    lines.push("_buf += \"</form>\"".to_string());
    lines
}

fn unwrap_to_s_go(expr: &Expr) -> &Expr {
    match &*expr.node {
        ExprNode::Send { recv: Some(r), method, args, .. }
            if (method.as_str() == "to_s" || method.as_str() == "to_str")
                && args.is_empty() =>
        {
            unwrap_to_s_go(r)
        }
        _ => expr,
    }
}

fn go_string_literal(s: &str) -> String {
    // Go double-quoted strings allow escape sequences; reuse Rust's
    // {:?} formatter which produces a compatible escaped form.
    format!("{s:?}")
}

fn is_go_simple_cond(expr: &Expr, ctx: &GoViewCtx) -> bool {
    match &*expr.node {
        ExprNode::Lit { .. } => true,
        ExprNode::Var { .. } => true,
        ExprNode::Ivar { .. } => true,
        ExprNode::Send { recv: Some(r), method, args, .. } if args.is_empty() => {
            // Collection predicates on the view's arg (`articles.any?`,
            // `articles.empty?`, etc.) — handled in expr_raw.
            let recv_name = match &*r.node {
                ExprNode::Var { name, .. } | ExprNode::Ivar { name } => {
                    Some(name.as_str())
                }
                _ => None,
            };
            if let Some(n) = recv_name {
                if n == ctx.arg_name
                    && matches!(
                        method.as_str(),
                        "any?" | "empty?" | "size" | "length" | "count"
                    )
                {
                    return true;
                }
                if ctx.arg_has_attr(n, method.as_str()) {
                    return true;
                }
            }
            false
        }
        _ => false,
    }
}

fn emit_go_view_expr_raw(expr: &Expr, ctx: &GoViewCtx) -> String {
    let unwrapped = unwrap_to_s_go(expr);
    match &*unwrapped.node {
        ExprNode::Lit { value: Literal::Str { value: s } } => go_string_literal(s),
        ExprNode::Lit { value: Literal::Int { value, .. } } => value.to_string(),
        ExprNode::Lit { value: Literal::Bool { value } } => value.to_string(),
        ExprNode::Var { name, .. } if name.as_str() == ctx.arg_name => {
            format!("fmt.Sprint({name})")
        }
        ExprNode::Var { name, .. } => format!("fmt.Sprint({name})"),
        ExprNode::Ivar { name } => format!("fmt.Sprint({name})"),
        ExprNode::Send { recv: Some(r), method, args, .. } if args.is_empty() => {
            let recv_name = match &*r.node {
                ExprNode::Var { name, .. } | ExprNode::Ivar { name } => {
                    Some(name.as_str())
                }
                _ => None,
            };
            if let Some(n) = recv_name {
                if n == ctx.arg_name {
                    match method.as_str() {
                        "any?" => return format!("len({n}) > 0"),
                        "empty?" => return format!("len({n}) == 0"),
                        "size" | "length" | "count" => return format!("len({n})"),
                        _ => {}
                    }
                }
                if ctx.arg_has_attr(n, method.as_str()) {
                    return format!(
                        "fmt.Sprint({n}.{})",
                        go_field_name(method.as_str())
                    );
                }
            }
            "\"\"".to_string()
        }
        _ => "\"\"".to_string(),
    }
}

// Body and expressions -------------------------------------------------

fn emit_expr(e: &Expr) -> String {
    match &*e.node {
        ExprNode::Lit { value } => emit_literal(value),
        ExprNode::Const { path } => {
            path.iter().map(|s| s.to_string()).collect::<Vec<_>>().join(".")
        }
        ExprNode::Var { name, .. } => name.to_string(),
        ExprNode::Ivar { name } => name.to_string(),
        ExprNode::Send { recv, method, args, .. } => emit_send(recv.as_ref(), method.as_str(), args),
        ExprNode::Assign { target: _, value } => emit_expr(value),
        ExprNode::Seq { exprs } => {
            exprs.iter().map(emit_expr).collect::<Vec<_>>().join("; ")
        }
        ExprNode::If { cond, then_branch, else_branch } => {
            let cond_s = emit_expr(cond);
            let then_s = emit_block_body(then_branch);
            let else_s = emit_block_body(else_branch);
            format!("if {cond_s} {{\n{then_s}\n}} else {{\n{else_s}\n}}")
        }
        ExprNode::Hash { entries, .. } => {
            let parts: Vec<String> = entries
                .iter()
                .map(|(k, v)| format!("{}: {}", emit_expr(k), emit_expr(v)))
                .collect();
            format!("map[string]interface{{}}{{{}}}", parts.join(", "))
        }
        ExprNode::Array { elements, .. } => {
            let parts: Vec<String> = elements.iter().map(emit_expr).collect();
            format!("[]interface{{}}{{{}}}", parts.join(", "))
        }
        ExprNode::BoolOp { op, left, right, .. } => {
            use crate::expr::BoolOpKind;
            let op_s = match op {
                BoolOpKind::Or => "||",
                BoolOpKind::And => "&&",
            };
            format!("{} {} {}", emit_expr(left), op_s, emit_expr(right))
        }
        ExprNode::StringInterp { parts } => {
            use crate::expr::InterpPart;
            let mut fmt = String::new();
            let mut args: Vec<String> = Vec::new();
            for p in parts {
                match p {
                    InterpPart::Text { value } => {
                        for c in value.chars() {
                            if c == '%' {
                                fmt.push_str("%%");
                            } else {
                                fmt.push(c);
                            }
                        }
                    }
                    InterpPart::Expr { expr } => {
                        fmt.push_str("%v");
                        args.push(emit_expr(expr));
                    }
                }
            }
            if args.is_empty() {
                format!("{fmt:?}")
            } else {
                format!("fmt.Sprintf({fmt:?}, {})", args.join(", "))
            }
        }
        other => format!("/* TODO: emit {:?} */", std::mem::discriminant(other)),
    }
}

fn emit_send(recv: Option<&Expr>, method: &str, args: &[Expr]) -> String {
    let args_s: Vec<String> = args.iter().map(emit_expr).collect();

    if method == "[]" && recv.is_some() {
        return format!("{}[{}]", emit_expr(recv.unwrap()), args_s.join(", "));
    }

    // Ruby→Go method-name mapping for string operations that have no
    // 1:1 in Go's stdlib (`strip` is `strings.TrimSpace(…)`, not
    // `.Strip()`). Only kicks in for instance dispatch on Str-typed
    // receivers; class calls and unknown types pass through.
    if let Some(r) = recv {
        if args.is_empty() && matches!(r.ty, Some(Ty::Str)) {
            if let Some(wrapped) = map_go_str_method(method, &emit_expr(r)) {
                return wrapped;
            }
        }
    }

    let go_m = go_method_name(method);
    match recv {
        None => {
            if args_s.is_empty() {
                go_m
            } else {
                format!("{}({})", go_m, args_s.join(", "))
            }
        }
        Some(r) => {
            let recv_s = emit_expr(r);
            // Struct field access vs method call: 0-arg Sends on a
            // non-Class receiver whose method isn't a known AR/stdlib
            // call render without parens (`p.Title`, not `p.Title()`).
            let is_class_call = matches!(&*r.node, ExprNode::Const { .. });
            if !is_class_call && args_s.is_empty() && !is_known_go_method(method) {
                return format!("{recv_s}.{go_m}");
            }
            format!("{}.{}({})", recv_s, go_m, args_s.join(", "))
        }
    }
}

/// AR/stdlib method names that should emit with parens on a model
/// struct receiver. Everything else on a non-Class receiver with no
/// args is treated as a field read. Grows alongside the runtime.
fn is_known_go_method(name: &str) -> bool {
    matches!(
        name,
        "save" | "save!" | "destroy" | "destroy!" | "update" | "update!"
            | "delete" | "touch" | "reload"
            | "validate" | "attributes" | "errors"
    )
}

/// Map Ruby String methods onto Go expressions that compile. `strip`
/// in Ruby is `strings.TrimSpace(s)` in Go — no method form exists.
/// Returns `Some(emit_text)` for a handled method. Unhandled methods
/// fall through to the default `.Method()` emit which may or may not
/// compile depending on the target receiver's actual methods.
fn map_go_str_method(method: &str, recv_text: &str) -> Option<String> {
    match method {
        "strip" => Some(format!("strings.TrimSpace({recv_text})")),
        "upcase" => Some(format!("strings.ToUpper({recv_text})")),
        "downcase" => Some(format!("strings.ToLower({recv_text})")),
        _ => None,
    }
}

fn emit_block_body(e: &Expr) -> String {
    let raw = match &*e.node {
        ExprNode::Seq { exprs } => exprs
            .iter()
            .map(emit_expr)
            .collect::<Vec<_>>()
            .join("\n"),
        _ => emit_expr(e),
    };
    raw.lines().map(|l| format!("\t{l}")).collect::<Vec<_>>().join("\n")
}

fn emit_literal(lit: &Literal) -> String {
    match lit {
        Literal::Nil => "nil".to_string(),
        Literal::Bool { value } => value.to_string(),
        Literal::Int { value } => value.to_string(),
        Literal::Float { value } => {
            let s = value.to_string();
            if s.contains('.') { s } else { format!("{s}.0") }
        }
        Literal::Str { value } => format!("{value:?}"),
        Literal::Sym { value } => format!("{:?}", value.as_str()),
    }
}

// Types ----------------------------------------------------------------

pub fn go_ty(ty: &Ty) -> String {
    match ty {
        Ty::Int => "int64".to_string(),
        Ty::Float => "float64".to_string(),
        Ty::Bool => "bool".to_string(),
        Ty::Str | Ty::Sym => "string".to_string(),
        Ty::Nil => "struct{}".to_string(),
        Ty::Array { elem } => format!("[]{}", go_ty(elem)),
        Ty::Hash { key, value } => format!("map[{}]{}", go_ty(key), go_ty(value)),
        Ty::Tuple { elems } => {
            // Go has no tuple; collapse to interface{} for now.
            let _ = elems;
            "interface{}".to_string()
        }
        Ty::Record { .. } => "map[string]interface{}".to_string(),
        Ty::Union { variants } => option_shape(variants).unwrap_or_else(|| {
            // Arbitrary union -> empty interface; would be a sum type emit later.
            "interface{}".to_string()
        }),
        Ty::Class { id, .. } => match id.0.as_str() {
            // Schema DateTime/Date/Time columns carry Ty::Class(Time); Go
            // has a time package (`time.Time`) but wiring that import
            // into emit adds complexity. String is the same pragmatic
            // stand-in Rust uses; real timestamps arrive when a DB
            // adapter does.
            "Time" => "string".to_string(),
            other => other.to_string(),
        },
        Ty::Fn { .. } => "func()".to_string(),
        Ty::Var { .. } => "interface{}".to_string(),
    }
}

fn option_shape(variants: &[Ty]) -> Option<String> {
    if variants.len() != 2 {
        return None;
    }
    match (&variants[0], &variants[1]) {
        (Ty::Nil, other) | (other, Ty::Nil) => Some(format!("*{}", go_ty(other))),
        _ => None,
    }
}

// Naming ---------------------------------------------------------------

fn go_field_name(ruby_name: &str) -> String {
    // Convert snake_case to PascalCase, special-casing common initialisms.
    ruby_name
        .split('_')
        .map(|part| match part {
            "id" => "ID".to_string(),
            "url" => "URL".to_string(),
            "http" => "HTTP".to_string(),
            "json" => "JSON".to_string(),
            "html" => "HTML".to_string(),
            "api" => "API".to_string(),
            other => pascalize_word(other),
        })
        .collect::<String>()
}

/// Convert a snake_case Ruby parameter name to a Go camelCase
/// identifier with the standard initialism rules. `article_id` →
/// `articleID`. Used for route-helper parameters and any other
/// place where Go's local-var naming convention applies.
fn go_param_name(ruby: &str) -> String {
    let mut out = String::new();
    for (i, part) in ruby.split('_').enumerate() {
        let upper = match part {
            "id" => "ID".to_string(),
            "url" => "URL".to_string(),
            "http" => "HTTP".to_string(),
            "json" => "JSON".to_string(),
            "html" => "HTML".to_string(),
            "api" => "API".to_string(),
            other => pascalize_word(other),
        };
        if i == 0 {
            // Lowercase the first chunk to keep it unexported.
            out.push_str(&upper.to_ascii_lowercase());
        } else {
            out.push_str(&upper);
        }
    }
    out
}

fn go_method_name(ruby_name: &str) -> String {
    ruby_name
        .split('_')
        .map(|part| match part {
            "id" => "ID".to_string(),
            other => pascalize_word(other),
        })
        .collect::<String>()
}

fn pascalize_word(word: &str) -> String {
    let mut chars = word.chars();
    match chars.next() {
        Some(c) => c.to_ascii_uppercase().to_string() + chars.as_str(),
        None => String::new(),
    }
}

/// Pre-emit rewrite for model method bodies: turn any bare-name Send
/// whose method matches a model attribute into a receiver-prefixed
/// Send. Ruby's implicit self becomes Go's explicit receiver variable
/// (`p` on `Post`, `c` on `Comment`, etc.). Emit then renders the
/// outer Send as `p.Title` — modulo go_method_name PascalCasing the
/// field, matching the struct definition.
fn rewrite_bare_attrs_to_receiver(
    e: &Expr,
    attrs: &[Symbol],
    receiver_var: &str,
) -> Expr {
    use crate::expr::{Arm, InterpPart, Pattern};
    use crate::ident::VarId;
    let rewrite = |child: &Expr| rewrite_bare_attrs_to_receiver(child, attrs, receiver_var);
    let receiver_ref = |span: crate::span::Span| Expr {
        span,
        node: Box::new(ExprNode::Var {
            id: VarId(0),
            name: Symbol::from(receiver_var),
        }),
        ty: None,
        effects: crate::effect::EffectSet::pure(),
        leading_blank_line: false,
    };
    let new_node = match &*e.node {
        ExprNode::Send { recv: None, method, args, block: None, parenthesized }
            if args.is_empty() && attrs.iter().any(|s| s == method) =>
        {
            ExprNode::Send {
                recv: Some(receiver_ref(e.span)),
                method: method.clone(),
                args: vec![],
                block: None,
                parenthesized: *parenthesized,
            }
        }
        ExprNode::Send { recv, method, args, block, parenthesized } => ExprNode::Send {
            recv: recv.as_ref().map(&rewrite),
            method: method.clone(),
            args: args.iter().map(&rewrite).collect(),
            block: block.as_ref().map(&rewrite),
            parenthesized: *parenthesized,
        },
        ExprNode::Seq { exprs } => ExprNode::Seq {
            exprs: exprs.iter().map(&rewrite).collect(),
        },
        ExprNode::Array { elements, style } => ExprNode::Array {
            elements: elements.iter().map(&rewrite).collect(),
            style: *style,
        },
        ExprNode::Hash { entries, braced } => ExprNode::Hash {
            entries: entries.iter().map(|(k, v)| (rewrite(k), rewrite(v))).collect(),
            braced: *braced,
        },
        ExprNode::If { cond, then_branch, else_branch } => ExprNode::If {
            cond: rewrite(cond),
            then_branch: rewrite(then_branch),
            else_branch: rewrite(else_branch),
        },
        ExprNode::Case { scrutinee, arms } => ExprNode::Case {
            scrutinee: rewrite(scrutinee),
            arms: arms
                .iter()
                .map(|arm| Arm {
                    pattern: arm.pattern.clone(),
                    guard: arm.guard.as_ref().map(&rewrite),
                    body: rewrite(&arm.body),
                })
                .collect(),
        },
        ExprNode::BoolOp { op, surface, left, right } => ExprNode::BoolOp {
            op: *op,
            surface: *surface,
            left: rewrite(left),
            right: rewrite(right),
        },
        ExprNode::StringInterp { parts } => ExprNode::StringInterp {
            parts: parts
                .iter()
                .map(|p| match p {
                    InterpPart::Text { value } => InterpPart::Text { value: value.clone() },
                    InterpPart::Expr { expr } => InterpPart::Expr { expr: rewrite(expr) },
                })
                .collect(),
        },
        ExprNode::Let { id, name, value, body } => ExprNode::Let {
            id: *id,
            name: name.clone(),
            value: rewrite(value),
            body: rewrite(body),
        },
        ExprNode::Lambda { params, block_param, body, block_style } => ExprNode::Lambda {
            params: params.clone(),
            block_param: block_param.clone(),
            body: rewrite(body),
            block_style: *block_style,
        },
        ExprNode::Apply { fun, args, block } => ExprNode::Apply {
            fun: rewrite(fun),
            args: args.iter().map(&rewrite).collect(),
            block: block.as_ref().map(&rewrite),
        },
        ExprNode::Assign { target, value } => {
            let new_target = match target {
                LValue::Var { id, name } => LValue::Var { id: *id, name: name.clone() },
                LValue::Ivar { name } => LValue::Ivar { name: name.clone() },
                LValue::Attr { recv, name } => LValue::Attr {
                    recv: rewrite(recv),
                    name: name.clone(),
                },
                LValue::Index { recv, index } => LValue::Index {
                    recv: rewrite(recv),
                    index: rewrite(index),
                },
            };
            ExprNode::Assign {
                target: new_target,
                value: rewrite(value),
            }
        }
        ExprNode::Yield { args } => ExprNode::Yield {
            args: args.iter().map(&rewrite).collect(),
        },
        ExprNode::Raise { value } => ExprNode::Raise { value: rewrite(value) },
        ExprNode::RescueModifier { expr, fallback } => ExprNode::RescueModifier {
            expr: rewrite(expr),
            fallback: rewrite(fallback),
        },
        // Leaves.
        ExprNode::Lit { .. }
        | ExprNode::Var { .. }
        | ExprNode::Ivar { .. }
        | ExprNode::Const { .. } => (*e.node).clone(),
    };
    let _ = Pattern::Wildcard;
    Expr {
        span: e.span,
        node: Box::new(new_node),
        ty: e.ty.clone(),
        effects: e.effects.clone(),
        leading_blank_line: e.leading_blank_line,
    }
}

