//! `rust2` library/module emit — `LibraryClass` consumer +
//! Module-mode entry points.
//!
//! Phase 2.1 scope: Module-mode `emit_module` for free-function
//! runtime files (inflector). Library-mode `emit_library_class`
//! comes in Phase 2.2 (HWIA) when struct + impl emit lands.

use std::fmt::Write;

use super::method::{emit_instance_method, emit_module_method};
use super::ty::rust_ty;
use crate::dialect::{LibraryClass, MethodDef, MethodReceiver};
use crate::expr::{Expr, ExprNode, Literal, LValue};
use crate::ty::Ty;

/// Module mode — flat list of `def self.*` helpers (e.g.
/// `inflector.rb`'s `Inflector.pluralize`). Each method emits as a
/// top-level `pub fn`. The output file IS the namespace at the
/// Rust module level (`src/inflector.rs` → `crate::inflector`); no
/// extra `mod NAME { ... }` wrapper needed.
///
/// Mirrors `crystal::emit_module` in role; differs in that crystal
/// requires explicit `module X ... end` wrapping while rust uses
/// the file-as-module convention.
pub fn emit_module(methods: &[MethodDef]) -> Result<String, String> {
    if methods.is_empty() {
        return Ok(String::new());
    }
    let mut out = String::new();
    writeln!(out, "// Generated by Roundhouse (rust2).").unwrap();
    writeln!(out).unwrap();
    let mut first = true;
    for m in methods {
        if !first {
            writeln!(out).unwrap();
        }
        first = false;
        out.push_str(&emit_module_method(m)?);
    }
    Ok(out)
}

/// Library mode — `class Foo ... end` → `pub struct Foo { ... }` +
/// `impl Foo { ... }`. Phase 2.2 lands the scaffold:
///
/// * Ivars are collected from method bodies and emitted as private
///   struct fields. Type comes from each ivar's first observed
///   assignment in the analyzed IR (the body-typer fills `e.ty`);
///   untyped ivars fall back to `serde_json::Value` via `rust_ty`.
/// * Each instance method emits as `pub fn name(&self, ...)` or
///   `pub fn name(&mut self, ...)`. The mutability heuristic walks
///   the body for `@ivar = ...` (LValue::Ivar) or `self[k] = v`
///   (LValue::Index recv=SelfRef) and picks `&mut self` if it sees
///   either.
/// * `def initialize` becomes `pub fn new(...) -> Self`; the
///   construction body is the user's `initialize` body with a final
///   `Self { fields }` return expression.
///
/// Subsequent commits drive the per-ExprNode body emit until the
/// produced Rust is `cargo check`-clean.
pub fn emit_library_class(class: &LibraryClass) -> Result<String, String> {
    // Strip the namespace prefix (`ActiveSupport::HashWithIndifferentAccess`
    // → `HashWithIndifferentAccess`). Rust uses file-as-module, so the
    // namespace is carried by the file path, not the struct name.
    let name = class
        .name
        .0
        .as_str()
        .rsplit("::")
        .next()
        .unwrap_or(class.name.0.as_str())
        .to_string();

    let ivars = collect_ivar_types(&class.methods);

    // Module-singleton classes (Ruby `class << self; attr_accessor
    // :slot; end` inside a `module X`) have only class methods —
    // their "ivars" are module-level state, not per-instance fields.
    // Rust analog: unit struct + per-slot `Mutex<Option<T>>` statics.
    // Detection: every method has Class receiver and the class isn't
    // empty (the empty-class case is just a marker, e.g. `module X;
    // end`, and falls through to the regular struct emit shape).
    let is_module_singleton = !class.methods.is_empty()
        && class
            .methods
            .iter()
            .all(|m| matches!(m.receiver, MethodReceiver::Class));
    if is_module_singleton {
        return emit_module_singleton(&name, &ivars, class);
    }

    // Pre-pass: identify methods whose body never reads `self` or
    // any `@ivar`. Those are static-safe — emit as `pub fn name(...)`
    // (no `self` receiver) so they're callable from `new` before an
    // instance exists. HWIA's `normalize_value` is the canonical
    // case: pure value-transformation, no instance state. The
    // companion call-site rewrite in `expr.rs::emit_send` routes
    // `self.method(args)` for these methods to `Self::method(args)`.
    let static_method_names: std::collections::HashSet<String> = class
        .methods
        .iter()
        .filter(|m| {
            matches!(m.receiver, MethodReceiver::Instance)
                && m.name.as_str() != "initialize"
                && !method_reads_self(&m.body)
                // Abstract-stub bodies (just `raise NotImplementedError`)
                // are the per-class contract markers. They don't read
                // self by construction, but per-model subclasses override
                // them — and callers reach the overrides through
                // instance dispatch (`record.dom_prefix()`). Classify
                // those as `&self` methods so the call site resolves.
                && !is_raise_only_body(&m.body)
        })
        .map(|m| m.name.as_str().to_string())
        .collect();

    let mut out = String::new();

    // Struct declaration. Derive `Clone` because Ruby semantics treat
    // model instances as freely cloneable (every assignment and
    // method-call argument-pass implicitly copies a reference, and
    // the framework Ruby's `attr_reader` getters and `.dup` patterns
    // expect to hand out independent copies). Each ivar type must
    // itself be Clone — every type rust2 emits today (i64, String,
    // bool, Vec<T: Clone>, HashMap<String, serde_json::Value>,
    // Option<T: Clone>, Class names → Clone'd) satisfies that. If a
    // future non-Clone ivar lands, this derive will fail at compile
    // time and we'll need a per-class override.
    writeln!(out, "#[derive(Clone)]").unwrap();
    writeln!(out, "pub struct {name} {{").unwrap();
    for (fname, ty) in &ivars {
        writeln!(out, "    {fname}: {},", rust_ty(ty)).unwrap();
    }
    out.push_str("}\n\n");

    // Impl block. Each method renders independently; `initialize`
    // routes through the constructor variant of `emit_instance_method`.
    // `def self.X` class methods emit as `pub fn name(...)` (no
    // receiver) — Rust's free-function-in-impl form. Callers reach
    // them via `Type::name(...)`, same dispatch path static-safe
    // instance methods use.
    writeln!(out, "impl {name} {{").unwrap();
    let mut first = true;
    // Make this class's ivar → field-type table available to
    // `emit_assign` (via thread-local) so RHS values can be coerced
    // when their Ty doesn't match the declared field type — most
    // commonly `self.body = ""` where field is `String` and `""` is
    // `&str`. Empty outside class-body scope; scoped per-class to
    // prevent bleeding between siblings.
    let ivar_type_map: std::collections::HashMap<String, Ty> =
        ivars.iter().cloned().collect();
    let body_result = super::expr::with_ivar_types(ivar_type_map, || super::expr::with_static_methods(static_method_names.clone(), || {
        for m in &class.methods {
            if matches!(m.name.as_str(), "[]" | "[]=") {
                continue;
            }
            if !first {
                writeln!(out).unwrap();
            }
            first = false;
            let body = match m.receiver {
                MethodReceiver::Class => {
                    // `def self.X` → `pub fn X(...)` with no receiver.
                    // Module-style call from within the impl block.
                    super::method::emit_module_method(m)?
                }
                MethodReceiver::Instance => {
                    let is_static = static_method_names.contains(m.name.as_str());
                    let mutates = method_mutates_self(&m.body);
                    emit_instance_method(m, mutates, is_static, &name, &ivars)?
                }
            };
            for line in body.lines() {
                if line.is_empty() {
                    writeln!(out).unwrap();
                } else {
                    writeln!(out, "    {line}").unwrap();
                }
            }
        }
        Ok::<(), String>(())
    }));
    body_result?;
    out.push_str("}\n");
    Ok(out)
}

/// Emit a Ruby `module X; class << self; attr_accessor :slot; end;
/// end` class as a Rust unit struct + per-slot `Mutex<Option<T>>`
/// statics. The transpiled `attr_accessor` getter/setter pair routes
/// through the static slot (see `expr.rs::in_module_singleton`).
///
/// Produces:
///
///     pub struct ActiveRecord;
///
///     static ADAPTER: std::sync::Mutex<Option<Value>> =
///         std::sync::Mutex::new(None);
///
///     impl ActiveRecord {
///         pub fn adapter() -> Value { ... }
///         pub fn set_adapter(value: Value) { ... }
///     }
fn emit_module_singleton(
    name: &str,
    ivars: &[(String, Ty)],
    class: &LibraryClass,
) -> Result<String, String> {
    let mut out = String::new();
    // Unit struct — no per-instance fields. Callers reach the slot
    // methods via `ActiveRecord::adapter()` / `ActiveRecord::set_adapter(v)`.
    writeln!(out, "pub struct {name};\n").unwrap();
    // One Mutex<Option<T>> static per ivar. Naming follows the
    // SCREAMING_SNAKE Rust static convention; trim_start_matches('_')
    // handles leading-underscore ivars.
    for (fname, ty) in ivars {
        let slot = super::expr::module_singleton_slot_name(fname);
        writeln!(
            out,
            "static {slot}: std::sync::Mutex<Option<{}>> = std::sync::Mutex::new(None);",
            rust_ty(ty),
        )
        .unwrap();
    }
    if !ivars.is_empty() {
        out.push('\n');
    }
    writeln!(out, "impl {name} {{").unwrap();
    let ivar_type_map: std::collections::HashMap<String, Ty> =
        ivars.iter().cloned().collect();
    let body_result = super::expr::with_module_singleton(true, || {
        super::expr::with_ivar_types(ivar_type_map, || {
            let mut first = true;
            for m in &class.methods {
                if matches!(m.name.as_str(), "[]" | "[]=") {
                    continue;
                }
                if !first {
                    writeln!(out).unwrap();
                }
                first = false;
                // Module-singleton methods are class methods by
                // construction (detection in `emit_library_class`).
                let body = super::method::emit_module_method(m)?;
                for line in body.lines() {
                    if line.is_empty() {
                        writeln!(out).unwrap();
                    } else {
                        writeln!(out, "    {line}").unwrap();
                    }
                }
            }
            Ok::<(), String>(())
        })
    });
    body_result?;
    out.push_str("}\n");
    Ok(out)
}

/// `true` when the method body is a bare `raise X` call. The ingest
/// pipeline parses Ruby `raise NotImplementedError, "..."` as
/// `Send { recv: None, method: "raise", args: [...] }`, NOT as
/// `ExprNode::Raise` (which is unused in this codebase). Abstract-
/// contract stubs (the `raise NotImplementedError, "..."` convention
/// in `runtime/ruby/`) satisfy this — they're meant to be overridden,
/// and call sites reach the overrides through `record.method()`
/// instance dispatch. Classifying those as static would break that
/// dispatch path (associated-function call form only).
fn is_raise_only_body(body: &Expr) -> bool {
    fn is_raise_send(e: &Expr) -> bool {
        matches!(
            &*e.node,
            ExprNode::Send { recv: None, method, .. } if method.as_str() == "raise"
        )
    }
    match &*body.node {
        ExprNode::Send { recv: None, method, .. } if method.as_str() == "raise" => true,
        ExprNode::Seq { exprs } if exprs.len() == 1 => is_raise_send(&exprs[0]),
        _ => false,
    }
}

/// Static-method probe: returns `true` when the method body
/// references `self` (any `Ivar` read/write or `SelfRef` use). The
/// pre-pass in `emit_library_class` filters this down to methods
/// that can be lifted to `pub fn name(...)` (no `&self`).
fn method_reads_self(body: &Expr) -> bool {
    fn walk(e: &Expr) -> bool {
        match &*e.node {
            ExprNode::SelfRef | ExprNode::Ivar { .. } => true,
            ExprNode::Assign { target, value } => {
                if matches!(target, LValue::Ivar { .. }) {
                    return true;
                }
                if let LValue::Attr { recv, .. } | LValue::Index { recv, .. } = target {
                    if walk(recv) { return true; }
                }
                walk(value)
            }
            ExprNode::Seq { exprs } => exprs.iter().any(walk),
            ExprNode::If { cond, then_branch, else_branch } => {
                walk(cond) || walk(then_branch) || walk(else_branch)
            }
            ExprNode::While { cond, body, .. } => walk(cond) || walk(body),
            ExprNode::Send { recv, args, block, .. } => {
                recv.as_ref().map(|r| walk(r)).unwrap_or(false)
                    || args.iter().any(walk)
                    || block.as_ref().map(|b| walk(b)).unwrap_or(false)
            }
            ExprNode::Return { value } => walk(value),
            ExprNode::StringInterp { parts } => parts.iter().any(|p| {
                if let crate::expr::InterpPart::Expr { expr } = p { walk(expr) } else { false }
            }),
            ExprNode::Hash { entries, .. } => entries.iter().any(|(k, v)| walk(k) || walk(v)),
            ExprNode::Array { elements, .. } => elements.iter().any(walk),
            // `||` / `&&` short-circuit expressions. Without this arm
            // a body like `label || @model_name` short-circuits the
            // self-detection: walk falls into the catch-all `_` arm
            // and returns false, so the method gets misclassified as
            // static-safe (FormBuilder.submit was the visible case —
            // 3 E0424 errors from the missing &self receiver).
            ExprNode::BoolOp { left, right, .. } => walk(left) || walk(right),
            ExprNode::Lambda { body, .. } => walk(body),
            ExprNode::Range { begin, end, .. } => {
                begin.as_ref().map(|b| walk(b)).unwrap_or(false)
                    || end.as_ref().map(|e| walk(e)).unwrap_or(false)
            }
            ExprNode::Yield { args } => args.iter().any(walk),
            _ => false,
        }
    }
    walk(body)
}

/// Walk all methods collecting `@ivar = …` assignments. Returns the
/// observed ivar set with each entry's inferred type (the analyzer
/// fills `Expr.ty` during typing; an unset `ty` falls back to
/// `Ty::Untyped` and renders as `serde_json::Value`).
fn collect_ivar_types(methods: &[MethodDef]) -> Vec<(String, Ty)> {
    // Phase 1: walk every method body and record every Ty observed
    // assigning to each ivar — both direct `@x =` (Ivar lvalue) and
    // `self.x = ...` (Attr lvalue with SelfRef recv, which writes the
    // same backing field on the emitted struct). Preserve first-seen
    // order so the struct's field declaration order matches the
    // source's `def initialize` order.
    let mut order: Vec<String> = Vec::new();
    let mut observed: std::collections::HashMap<String, Vec<Ty>> =
        std::collections::HashMap::new();
    for m in methods {
        walk_collect_ivars(&m.body, &mut order, &mut observed);
    }
    // Phase 2: unify each ivar's observed-type list into one Ty.
    // Rules:
    //   - {Nil, T, ...}            → Option<T> (T = first non-Nil)
    //   - {Nil}                    → Option<Untyped> (nil-only ivar,
    //                                widen to Option for setter shape)
    //   - {Hash<_,_>, Hash<K, V>}  → keep the widest (matches the
    //                                previous one-shot widening for
    //                                empty-literal seeded hashes)
    //   - {T}                      → T (single-type ivar)
    //   - {T1, T2, ...} mixed      → first observed (fallback)
    order
        .into_iter()
        .map(|name| {
            let tys = observed.remove(&name).unwrap_or_default();
            let unified = unify_ivar_tys(&tys);
            (name, unified)
        })
        .collect()
}

fn unify_ivar_tys(tys: &[Ty]) -> Ty {
    let has_nil = tys.iter().any(|t| matches!(t, Ty::Nil));
    let first_non_nil = tys.iter().find(|t| !matches!(t, Ty::Nil)).cloned();
    if has_nil {
        match first_non_nil {
            Some(t) => Ty::Union {
                variants: vec![Ty::Nil, t],
            },
            // Nil-only ivar (e.g. `@location = nil` with no other
            // assignments visible in the class body) — widen to
            // Option<Untyped> so the setter side can assign any
            // value-typed payload without an E0308. Without widening
            // the field types as `()` and every other path fails.
            None => Ty::Union {
                variants: vec![Ty::Nil, Ty::Untyped],
            },
        }
    } else {
        // No Nil observed — pick the most informative concrete type.
        // For Hash, prefer the most-specified variant (Hash{K, V} with
        // concrete K/V over Hash{Untyped, Untyped}) — matches the
        // previous single-shot widening.
        tys.iter()
            .max_by_key(|t| match t {
                Ty::Hash { key, value } => {
                    let k = !matches!(**key, Ty::Untyped);
                    let v = !matches!(**value, Ty::Untyped);
                    (k as u8) + (v as u8)
                }
                _ => 0,
            })
            .cloned()
            .unwrap_or(Ty::Untyped)
    }
}

fn walk_collect_ivars(
    e: &Expr,
    order: &mut Vec<String>,
    observed: &mut std::collections::HashMap<String, Vec<Ty>>,
) {
    fn record(
        name: &str,
        ty: Ty,
        order: &mut Vec<String>,
        observed: &mut std::collections::HashMap<String, Vec<Ty>>,
    ) {
        let k = name.to_string();
        if !order.iter().any(|n| n == &k) {
            order.push(k.clone());
        }
        observed.entry(k).or_default().push(ty);
    }
    match &*e.node {
        ExprNode::Assign { target: LValue::Ivar { name }, value } => {
            let ty = value.ty.clone().unwrap_or(Ty::Untyped);
            record(name.as_str(), ty, order, observed);
            walk_collect_ivars(value, order, observed);
        }
        // `self.x = value` — Attr lvalue with SelfRef recv. Writes
        // the same backing field as `@x = value`. Without this, an
        // ivar assigned only as `@x = nil` in `initialize` and then
        // `self.x = path` in `redirect_to` would type as `()`,
        // mismatching every `self.x = path` site.
        ExprNode::Assign {
            target: LValue::Attr { recv, name },
            value,
        } if matches!(&*recv.node, ExprNode::SelfRef) => {
            let ty = value.ty.clone().unwrap_or(Ty::Untyped);
            record(name.as_str(), ty, order, observed);
            walk_collect_ivars(value, order, observed);
        }
        // `@ivar[k] = v` — Ruby parses as Send `[]=` on Ivar recv.
        // The empty-literal `@data = {}` types as `Hash<Untyped,
        // Untyped>` from analyzer inference; subsequent index-assigns
        // tell us the real key/value types. Only record a Hash
        // observation when the ivar's prior observations already
        // contain a Hash — otherwise `@flash[:notice] = "..."` (where
        // `@flash` is the typed `Flash` struct, not a HashMap) would
        // be misread as a Hash assignment and widen the ivar type
        // away from the concrete struct.
        ExprNode::Send { recv: Some(recv), method, args, .. }
            if method.as_str() == "[]=" && args.len() == 2 =>
        {
            if let ExprNode::Ivar { name } = &*recv.node {
                let key = name.as_str();
                let already_hash = observed
                    .get(key)
                    .map(|tys| tys.iter().any(|t| matches!(t, Ty::Hash { .. })))
                    .unwrap_or(false);
                if already_hash {
                    let k_ty = args[0].ty.clone().unwrap_or(Ty::Untyped);
                    let v_ty = args[1].ty.clone().unwrap_or(Ty::Untyped);
                    record(
                        key,
                        Ty::Hash {
                            key: Box::new(k_ty),
                            value: Box::new(v_ty),
                        },
                        order,
                        observed,
                    );
                }
            }
            walk_collect_ivars(recv, order, observed);
            args.iter().for_each(|a| walk_collect_ivars(a, order, observed));
        }
        ExprNode::Seq { exprs } => {
            exprs.iter().for_each(|e| walk_collect_ivars(e, order, observed))
        }
        ExprNode::If { cond, then_branch, else_branch } => {
            walk_collect_ivars(cond, order, observed);
            walk_collect_ivars(then_branch, order, observed);
            walk_collect_ivars(else_branch, order, observed);
        }
        ExprNode::Send { recv, args, block, .. } => {
            if let Some(r) = recv { walk_collect_ivars(r, order, observed); }
            args.iter().for_each(|a| walk_collect_ivars(a, order, observed));
            if let Some(b) = block { walk_collect_ivars(b, order, observed); }
        }
        ExprNode::While { cond, body, .. } => {
            walk_collect_ivars(cond, order, observed);
            walk_collect_ivars(body, order, observed);
        }
        ExprNode::Return { value } => walk_collect_ivars(value, order, observed),
        _ => {}
    }
}

/// Receiver-kind heuristic: returns `true` when the method body
/// contains any write to instance state (`@ivar = …`, `self[k] = v`,
/// `self.attr = v`). Lands as Phase 2.2's first design choice; if the
/// heuristic misclassifies in practice the lifting path is the
/// [self-describing IR](feedback_self_describing_ir) pattern — add a
/// `mutates_self` flag to `MethodDef` and let lowerers fill it.
fn method_mutates_self(body: &Expr) -> bool {
    fn walk(e: &Expr) -> bool {
        match &*e.node {
            ExprNode::Assign { target, .. } => match target {
                LValue::Ivar { .. } => true,
                LValue::Attr { recv, .. } | LValue::Index { recv, .. } => {
                    matches!(&*recv.node, ExprNode::SelfRef | ExprNode::Ivar { .. })
                        || walk(recv)
                }
                LValue::Var { .. } => false,
            },
            // `self[k] = v` and `self.foo = v` lower as `Send`s to
            // `[]=` / setter-suffixed methods, not Assign. Same for
            // `@data[k] = v` — direct ivar mutation via index assign
            // (HWIA `set` / `delete`). Treat all of these as mutation.
            ExprNode::Send { recv: Some(recv), method, .. }
                if matches!(&*recv.node, ExprNode::SelfRef | ExprNode::Ivar { .. })
                    && (method.as_str() == "[]=" || method.as_str().ends_with('='))
                    && !is_comparison_method(method.as_str()) =>
            {
                true
            }
            ExprNode::Seq { exprs } => exprs.iter().any(walk),
            ExprNode::If { cond, then_branch, else_branch } => {
                walk(cond) || walk(then_branch) || walk(else_branch)
            }
            ExprNode::While { cond, body, .. } => walk(cond) || walk(body),
            ExprNode::Send { recv, args, block, .. } => {
                recv.as_ref().map(|r| walk(r)).unwrap_or(false)
                    || args.iter().any(walk)
                    || block.as_ref().map(|b| walk(b)).unwrap_or(false)
            }
            ExprNode::Return { value } => walk(value),
            _ => false,
        }
    }
    walk(body)
}

/// Trailing-`=` filter: `==`, `!=`, `<=`, `>=` end with `=` but are
/// comparison sends, not setters. The setter heuristic above needs
/// to exclude them.
fn is_comparison_method(m: &str) -> bool {
    matches!(m, "==" | "!=" | "<=" | ">=" | "<=>" | "===" | "=~")
}

/// Top-level constant declaration. Crystal: `NAME = VALUE`. TS:
/// `const NAME = VALUE;`. Rust picks a declaration shape per value
/// kind — `pub const NAME: T = V;` for scalars, `static NAME:
/// LazyLock<HashMap<K, V>> = LazyLock::new(|| HashMap::from([...]))`
/// for Hash literals, `static NAME: LazyLock<Regex> = LazyLock::new(|| ...)`
/// for regex literals. Fully-qualified paths (`std::sync::LazyLock`,
/// `regex::Regex`) avoid per-file imports — the rust2 emit table
/// doesn't pre-declare them and constants are emitted before classes.
pub fn format_constant(name: &str, value: &Expr) -> String {
    match &*value.node {
        ExprNode::Lit { value: Literal::Int { value: v } } => {
            format!("pub const {name}: i64 = {v}_i64;")
        }
        ExprNode::Lit { value: Literal::Float { value: v } } => {
            let s = v.to_string();
            let lit = if s.contains('.') { s } else { format!("{s}.0") };
            format!("pub const {name}: f64 = {lit};")
        }
        ExprNode::Lit { value: Literal::Bool { value: v } } => {
            format!("pub const {name}: bool = {v};")
        }
        ExprNode::Lit { value: Literal::Str { value: v } } => {
            format!("pub const {name}: &str = {v:?};")
        }
        ExprNode::Lit { value: Literal::Sym { value: v } } => {
            format!("pub const {name}: &str = {:?};", v.as_str())
        }
        ExprNode::Lit { value: Literal::Regex { pattern, .. } } => {
            // Use a raw string for the pattern so backslashes don't
            // need re-escaping. Ruby's regex syntax differs from
            // Rust's regex crate in some edge cases (lookbehind,
            // named captures); the framework Ruby's regex usage is
            // limited to character classes today, which both crates
            // share.
            format!(
                "static {name}: std::sync::LazyLock<regex::Regex> = \
                 std::sync::LazyLock::new(|| regex::Regex::new({pattern:?}).unwrap());"
            )
        }
        ExprNode::Hash { entries, .. } => format_hash_constant(name, entries),
        ExprNode::Array { elements, .. } => format_array_constant(name, elements),
        _ => format!("// TODO rust2 const: {name} = {:?}", value.node),
    }
}

fn format_hash_constant(name: &str, entries: &[(Expr, Expr)]) -> String {
    // Infer key/value types from the first entry. The body-typer's
    // Hash literal inference unifies across entries, so all keys and
    // all values share a type — sampling [0] is enough.
    let (key_ty, val_ty) = match entries.first() {
        Some((k, v)) => (rust_ty_for_constant_key(k), rust_ty_for_constant(v)),
        None => ("&'static str".to_string(), "&'static str".to_string()),
    };
    let mut pairs: Vec<String> = Vec::new();
    for (k, v) in entries {
        let k_s = render_constant_key(k);
        let v_s = render_constant_value(v);
        pairs.push(format!("({k_s}, {v_s})"));
    }
    let pairs_s = pairs.join(", ");
    format!(
        "static {name}: std::sync::LazyLock<std::collections::HashMap<{key_ty}, {val_ty}>> = \
         std::sync::LazyLock::new(|| std::collections::HashMap::from([{pairs_s}]));"
    )
}

fn format_array_constant(name: &str, elements: &[Expr]) -> String {
    let elem_ty = match elements.first() {
        Some(e) => rust_ty_for_constant(e),
        None => "&'static str".to_string(),
    };
    let items: Vec<String> = elements.iter().map(render_constant_value).collect();
    let items_s = items.join(", ");
    format!(
        "static {name}: std::sync::LazyLock<Vec<{elem_ty}>> = \
         std::sync::LazyLock::new(|| vec![{items_s}]);"
    )
}

/// Hash key type — Symbol/String keys both become `&'static str` so
/// `HashMap::get(&key)` from a runtime `&str` resolves. Other key
/// shapes fall back to the value-type rule (untested but mechanical).
fn rust_ty_for_constant_key(e: &Expr) -> String {
    match &*e.node {
        ExprNode::Lit { value: Literal::Sym { .. } } | ExprNode::Lit { value: Literal::Str { .. } } => {
            "&'static str".to_string()
        }
        _ => rust_ty_for_constant(e),
    }
}

fn rust_ty_for_constant(e: &Expr) -> String {
    match &*e.node {
        ExprNode::Lit { value: Literal::Int { .. } } => "i64".to_string(),
        ExprNode::Lit { value: Literal::Float { .. } } => "f64".to_string(),
        ExprNode::Lit { value: Literal::Bool { .. } } => "bool".to_string(),
        ExprNode::Lit { value: Literal::Str { .. } } => "&'static str".to_string(),
        ExprNode::Lit { value: Literal::Sym { .. } } => "&'static str".to_string(),
        _ => "&'static str".to_string(),
    }
}

/// Render a constant key (Hash) as a Rust expression. Symbol keys
/// become string literals (matching Ruby's source-level
/// `Symbol#to_s`); string keys quote with `{:?}`-style escaping.
fn render_constant_key(e: &Expr) -> String {
    match &*e.node {
        ExprNode::Lit { value: Literal::Sym { value } } => format!("{:?}", value.as_str()),
        ExprNode::Lit { value: Literal::Str { value } } => format!("{value:?}"),
        _ => render_constant_value(e),
    }
}

fn render_constant_value(e: &Expr) -> String {
    match &*e.node {
        ExprNode::Lit { value: Literal::Int { value } } => format!("{value}_i64"),
        ExprNode::Lit { value: Literal::Float { value } } => {
            let s = value.to_string();
            if s.contains('.') { s } else { format!("{s}.0") }
        }
        ExprNode::Lit { value: Literal::Bool { value } } => value.to_string(),
        ExprNode::Lit { value: Literal::Str { value } } => format!("{value:?}"),
        ExprNode::Lit { value: Literal::Sym { value } } => format!("{:?}", value.as_str()),
        _ => format!("/* TODO rust2 const value: {:?} */", e.node),
    }
}
