/// Module mode ŌĆö flat list of `def self.*` helpers (e.g.
/// `inflector.rb`'s `Inflector.pluralize`). Each method emits as a
/// top-level `pub fn`. The output file IS the namespace at the
/// Rust module level (`src/inflector.rs` ŌåÆ `crate::inflector`); no
/// extra `mod NAME { ... }` wrapper needed.
///
/// Mirrors `crystal::emit_module` in role; differs in that crystal
/// requires explicit `module X ... end` wrapping while rust uses
/// the file-as-module convention.
pub fn emit_module(methods: &[MethodDef]) -> Result<String, String> {
    if methods.is_empty() {
        return Ok(String::new());
    }
    let mut out = String::new();
    writeln!(out, "// Generated by Roundhouse (rust2).").unwrap();
    writeln!(out).unwrap();
    let mut first = true;
    for m in methods {
        if !first {
            writeln!(out).unwrap();
        }
        first = false;
        out.push_str(&emit_module_method(m)?);
    }
    Ok(out)
}

/// Library mode ŌĆö `class Foo ... end` ŌåÆ `pub struct Foo { ... }` +
/// `impl Foo { ... }`. Phase 2.2 lands the scaffold:
///
/// * Ivars are collected from method bodies and emitted as private
///   struct fields. Type comes from each ivar's first observed
///   assignment in the analyzed IR (the body-typer fills `e.ty`);
///   untyped ivars fall back to `serde_json::Value` via `rust_ty`.
/// * Each instance method emits as `pub fn name(&self, ...)` or
///   `pub fn name(&mut self, ...)`. The mutability heuristic walks
///   the body for `@ivar = ...` (LValue::Ivar) or `self[k] = v`
///   (LValue::Index recv=SelfRef) and picks `&mut self` if it sees
///   either.
/// * `def initialize` becomes `pub fn new(...) -> Self`; the
///   construction body is the user's `initialize` body with a final
///   `Self { fields }` return expression.
///
/// Subsequent commits drive the per-ExprNode body emit until the
/// produced Rust is `cargo check`-clean.
pub fn emit_library_class(class: &LibraryClass) -> Result<String, String> {
    // Strip the namespace prefix (`ActiveSupport::HashWithIndifferentAccess`
    // ŌåÆ `HashWithIndifferentAccess`). Rust uses file-as-module, so the
    // namespace is carried by the file path, not the struct name.
    let name = class
        .name
        .0
        .as_str()
        .rsplit("::")
        .next()
        .unwrap_or(class.name.0.as_str())
        .to_string();

    let ivars = collect_ivar_types(&class.methods);

    // Pre-pass: identify methods whose body never reads `self` or
    // any `@ivar`. Those are static-safe ŌĆö emit as `pub fn name(...)`
    // (no `self` receiver) so they're callable from `new` before an
    // instance exists. HWIA's `normalize_value` is the canonical
    // case: pure value-transformation, no instance state. The
    // companion call-site rewrite in `expr.rs::emit_send` routes
    // `self.method(args)` for these methods to `Self::method(args)`.
    let static_method_names: std::collections::HashSet<String> = class
        .methods
        .iter()
        .filter(|m| {
            matches!(m.receiver, MethodReceiver::Instance)
                && m.name.as_str() != "initialize"
                && !method_reads_self(&m.body)
        })
        .map(|m| m.name.as_str().to_string())
        .collect();

    let mut out = String::new();

    // Struct declaration.
    writeln!(out, "pub struct {name} {{").unwrap();
    for (fname, ty) in &ivars {
        writeln!(out, "    {fname}: {},", rust_ty(ty)).unwrap();
    }
    out.push_str("}\n\n");

    // Impl block. Each method renders independently; `initialize`
    // routes through the constructor variant of `emit_instance_method`.
    // `def self.X` class methods emit as `pub fn name(...)` (no
    // receiver) ŌĆö Rust's free-function-in-impl form. Callers reach
    // them via `Type::name(...)`, same dispatch path static-safe
    // instance methods use.
    writeln!(out, "impl {name} {{").unwrap();
    let mut first = true;
    // Make this class's ivar ŌåÆ field-type table available to
    // `emit_assign` (via thread-local) so RHS values can be coerced
    // when their Ty doesn't match the declared field type ŌĆö most
    // commonly `self.body = ""` where field is `String` and `""` is
    // `&str`. Empty outside class-body scope; scoped per-class to
    // prevent bleeding between siblings.
    let ivar_type_map: std::collections::HashMap<String, Ty> =
        ivars.iter().cloned().collect();
    let body_result = super::expr::with_ivar_types(ivar_type_map, || super::expr::with_static_methods(static_method_names.clone(), || {
        for m in &class.methods {
            if matches!(m.name.as_str(), "[]" | "[]=") {
                continue;
            }
            if !first {
                writeln!(out).unwrap();
            }
            first = false;
            let body = match m.receiver {
                MethodReceiver::Class => {
                    // `def self.X` ŌåÆ `pub fn X(...)` with no receiver.
                    // Module-style call from within the impl block.
                    super::method::emit_module_method(m)?
                }
                MethodReceiver::Instance => {
                    let is_static = static_method_names.contains(m.name.as_str());
                    let mutates = method_mutates_self(&m.body);
                    emit_instance_method(m, mutates, is_static, &name, &ivars)?
                }
            };
            for line in body.lines() {
                if line.is_empty() {
                    writeln!(out).unwrap();
                } else {
                    writeln!(out, "    {line}").unwrap();
                }
            }
        }
        Ok::<(), String>(())
    }));
    body_result?;
    out.push_str("}\n");
    Ok(out)
}

/// Static-method probe: returns `true` when the method body
/// references `self` (any `Ivar` read/write or `SelfRef` use). The
/// pre-pass in `emit_library_class` filters this down to methods
/// that can be lifted to `pub fn name(...)` (no `&self`).
fn method_reads_self(body: &Expr) -> bool {
    fn walk(e: &Expr) -> bool {
        match &*e.node {
            ExprNode::SelfRef | ExprNode::Ivar { .. } => true,
            ExprNode::Assign { target, value } => {
                if matches!(target, LValue::Ivar { .. }) {
                    return true;
                }
                if let LValue::Attr { recv, .. } | LValue::Index { recv, .. } = target {
                    if walk(recv) { return true; }
                }
                walk(value)
            }
            ExprNode::Seq { exprs } => exprs.iter().any(walk),
            ExprNode::If { cond, then_branch, else_branch } => {
                walk(cond) || walk(then_branch) || walk(else_branch)
            }
            ExprNode::While { cond, body, .. } => walk(cond) || walk(body),
            ExprNode::Send { recv, args, block, .. } => {
                recv.as_ref().map(|r| walk(r)).unwrap_or(false)
                    || args.iter().any(walk)
                    || block.as_ref().map(|b| walk(b)).unwrap_or(false)
            }
            ExprNode::Return { value } => walk(value),
            ExprNode::StringInterp { parts } => parts.iter().any(|p| {
                if let crate::expr::InterpPart::Expr { expr } = p { walk(expr) } else { false }
            }),
            ExprNode::Hash { entries, .. } => entries.iter().any(|(k, v)| walk(k) || walk(v)),
            ExprNode::Array { elements, .. } => elements.iter().any(walk),
            _ => false,
        }
    }
    walk(body)
}

/// Walk all methods collecting `@ivar = ŌĆ”` assignments. Returns the
/// observed ivar set with each entry's inferred type (the analyzer
/// fills `Expr.ty` during typing; an unset `ty` falls back to
/// `Ty::Untyped` and renders as `serde_json::Value`).
fn collect_ivar_types(methods: &[MethodDef]) -> Vec<(String, Ty)> {
    let mut seen: Vec<(String, Ty)> = Vec::new();
    for m in methods {
        walk_collect_ivars(&m.body, &mut seen);
    }
    seen
}

fn walk_collect_ivars(e: &Expr, out: &mut Vec<(String, Ty)>) {
    match &*e.node {
        ExprNode::Assign { target: LValue::Ivar { name }, value } => {
            let key = name.as_str().to_string();
            if !out.iter().any(|(n, _)| n == &key) {
                let ty = value.ty.clone().unwrap_or(Ty::Untyped);
                out.push((key, ty));
            }
            walk_collect_ivars(value, out);
        }
        // `@ivar[k] = v` ŌĆö Ruby parses as Send `[]=` on Ivar recv.
        // The empty-literal `@data = {}` types as `Hash<Untyped,
        // Untyped>` from analyzer inference; subsequent index-assigns
        // tell us the real key/value types. Widen the registered
        // entry from the args' types when it's Hash-typed; ignore
        // when the ivar is a class instance (its own `[]=` happens
        // to share the name). Matches Crystal's widening pass.
        ExprNode::Send { recv: Some(recv), method, args, .. }
            if method.as_str() == "[]=" && args.len() == 2 =>
        {
            if let ExprNode::Ivar { name } = &*recv.node {
                let key = name.as_str().to_string();
                if let Some(entry) = out.iter_mut().find(|(n, _)| n == &key) {
                    if let Ty::Hash { .. } = &entry.1 {
                        let k_ty = args[0].ty.clone().unwrap_or(Ty::Untyped);
                        let v_ty = args[1].ty.clone().unwrap_or(Ty::Untyped);
                        entry.1 = Ty::Hash {
                            key: Box::new(k_ty),
                            value: Box::new(v_ty),
                        };
                    }
                }
            }
            walk_collect_ivars(recv, out);
            args.iter().for_each(|a| walk_collect_ivars(a, out));
        }
        ExprNode::Seq { exprs } => exprs.iter().for_each(|e| walk_collect_ivars(e, out)),
        ExprNode::If { cond, then_branch, else_branch } => {
            walk_collect_ivars(cond, out);
            walk_collect_ivars(then_branch, out);
            walk_collect_ivars(else_branch, out);
        }
        ExprNode::Send { recv, args, block, .. } => {
            if let Some(r) = recv { walk_collect_ivars(r, out); }
            args.iter().for_each(|a| walk_collect_ivars(a, out));
            if let Some(b) = block { walk_collect_ivars(b, out); }
        }
        ExprNode::While { cond, body, .. } => {
            walk_collect_ivars(cond, out);
            walk_collect_ivars(body, out);
        }
        ExprNode::Return { value } => walk_collect_ivars(value, out),
        _ => {}
    }
}

/// Receiver-kind heuristic: returns `true` when the method body
/// contains any write to instance state (`@ivar = ŌĆ”`, `self[k] = v`,
/// `self.attr = v`). Lands as Phase 2.2's first design choice; if the
/// heuristic misclassifies in practice the lifting path is the
/// [self-describing IR](feedback_self_describing_ir) pattern ŌĆö add a
/// `mutates_self` flag to `MethodDef` and let lowerers fill it.
fn method_mutates_self(body: &Expr) -> bool {
    fn walk(e: &Expr) -> bool {
        match &*e.node {
            ExprNode::Assign { target, .. } => match target {
                LValue::Ivar { .. } => true,
                LValue::Attr { recv, .. } | LValue::Index { recv, .. } => {
                    matches!(&*recv.node, ExprNode::SelfRef | ExprNode::Ivar { .. })
                        || walk(recv)
                }
                LValue::Var { .. } => false,
            },
            // `self[k] = v` and `self.foo = v` lower as `Send`s to
            // `[]=` / setter-suffixed methods, not Assign. Same for
            // `@data[k] = v` ŌĆö direct ivar mutation via index assign
            // (HWIA `set` / `delete`). Treat all of these as mutation.
            ExprNode::Send { recv: Some(recv), method, .. }
                if matches!(&*recv.node, ExprNode::SelfRef | ExprNode::Ivar { .. })
                    && (method.as_str() == "[]=" || method.as_str().ends_with('='))
                    && !is_comparison_method(method.as_str()) =>
            {
                true
            }
            ExprNode::Seq { exprs } => exprs.iter().any(walk),
            ExprNode::If { cond, then_branch, else_branch } => {
                walk(cond) || walk(then_branch) || walk(else_branch)
            }
            ExprNode::While { cond, body, .. } => walk(cond) || walk(body),
            ExprNode::Send { recv, args, block, .. } => {
                recv.as_ref().map(|r| walk(r)).unwrap_or(false)
                    || args.iter().any(walk)
                    || block.as_ref().map(|b| walk(b)).unwrap_or(false)
            }
            ExprNode::Return { value } => walk(value),
            _ => false,
        }
    }
    walk(body)
}

/// Trailing-`=` filter: `==`, `!=`, `<=`, `>=` end with `=` but are
/// comparison sends, not setters. The setter heuristic above needs
/// to exclude them.
fn is_comparison_method(m: &str) -> bool {
    matches!(m, "==" | "!=" | "<=" | ">=" | "<=>" | "===" | "=~")
}

