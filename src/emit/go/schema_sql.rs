//! `app/schema_sql.go` emission — Go constant wrapping the
//! target-neutral DDL produced by `emit::shared::schema_sql`.

use std::fmt::Write;
use std::path::PathBuf;

use super::super::EmittedFile;
use crate::App;

/// `app/schema_sql.go` — Go constant wrapping the target-neutral DDL
/// produced by `emit::shared::schema_sql`. `SetupTestDB` splits it on
/// `;\n` and execs each statement against a fresh :memory: database.
pub(super) fn emit_schema_sql(app: &App) -> EmittedFile {
    let ddl = crate::emit::shared::schema_sql::render_schema_sql(&app.schema);
    let mut s = String::new();
    writeln!(s, "// Generated by Roundhouse.").unwrap();
    writeln!(s, "package app").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "const CreateTables = `").unwrap();
    s.push_str(&ddl);
    writeln!(s, "`").unwrap();
    EmittedFile {
        path: PathBuf::from("app/schema_sql.go"),
        content: s,
    }
}
