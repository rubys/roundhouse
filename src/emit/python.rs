//
// Phase 4c: controllers emit as Python classes with one `async def`
// per action. Action bodies render through the shared `SendKind`
// classifier from `lower::controller`; the Python render table
// handles HTTP surface (`http.render` / `redirect_to` / `head`),
// `respond_to` as a lambda, params access, model / association
// shapes, and bang stripping (Python forbids `!` in identifiers).
//
// Private helpers get a leading-underscore name prefix (`_set_article`)
// so Python's unused-method warnings stay quiet — `@unittest.skip`-ped
// controller tests never invoke them at runtime.

/// Pass-2 controller emit — one module per controller under
/// `app/controllers/`. Each action is a module-level function
/// returning `http.ActionResponse`. The `Router` in
/// `app/http.py` resolves handlers via `getattr(module, action)`,
/// so controllers are registered by module reference rather than
/// as class-level methods.
fn emit_controllers_pass2(app: &App) -> Vec<EmittedFile> {
    let known_models: Vec<Symbol> =
        app.models.iter().map(|m| m.name.0.clone()).collect();
    let mut files = Vec::new();
    files.push(EmittedFile {
        path: PathBuf::from("app/controllers/__init__.py"),
        content: String::new(),
    });
    for c in &app.controllers {
        files.push(emit_controller_file_pass2(c, &known_models, app));
    }
    files
}

fn emit_controller_file_pass2(
    c: &Controller,
    known_models: &[Symbol],
    app: &App,
) -> EmittedFile {
    let name = c.name.0.as_str();
    let file_stem = crate::naming::snake_case(name);
    let resource = crate::lower::resource_from_controller_name(name);
    let model_class = crate::naming::singularize_camelize(&resource);
    let has_model = known_models.iter().any(|m| m.as_str() == model_class);
    let parent = crate::lower::find_nested_parent(app, name);
    let permitted = crate::lower::permitted_fields_for(c, &resource)
        .unwrap_or_else(|| crate::lower::default_permitted_fields(app, &model_class));

    let mut s = String::new();
    writeln!(s, "# Generated by Roundhouse.").unwrap();
    writeln!(s, "from __future__ import annotations").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "from app import http, views").unwrap();
    writeln!(s, "from app.route_helpers import *  # noqa: F401, F403").unwrap();
    if !known_models.is_empty() {
        writeln!(s, "from app.models import *  # noqa: F401, F403").unwrap();
    }
    writeln!(s).unwrap();

    let (public_actions, _private) = crate::lower::split_public_private(c);
    for action in &public_actions {
        let la = crate::lower::lower_action(
            action.name.as_str(),
            &resource,
            &model_class,
            has_model,
            parent.as_ref(),
            &permitted,
        );
        emit_py_action(&mut s, &la);
        writeln!(s).unwrap();
    }

    EmittedFile {
        path: PathBuf::from(format!("app/controllers/{file_stem}.py")),
        content: s,
    }
}

fn py_view_fn(model_class: &str, suffix: &str) -> String {
    let plural = crate::naming::pluralize_snake(model_class);
    format!("render_{plural}_{}", suffix.to_lowercase())
}

/// Render one LoweredAction as a Python `def`. Mangles `new` to
/// `new_` (matches the router's `_resolve_handler` mapping) so the
/// action name doesn't collide with Python's `Model.new(...)`
/// idioms.
fn emit_py_action(out: &mut String, la: &crate::lower::LoweredAction) {
    use crate::lower::ActionKind;
    let name = if la.name == "new" { "new_" } else { la.name.as_str() };
    let model_class = la.model_class.as_str();
    let resource = la.resource.as_str();

    let find_record = |out: &mut String| {
        writeln!(out, "    record_id = int(context.params[\"id\"])").unwrap();
        writeln!(
            out,
            "    record = {model_class}.find(record_id) or {model_class}()"
        )
        .unwrap();
    };

    match la.kind {
        ActionKind::Index => {
            let view_fn = py_view_fn(model_class, "index");
            writeln!(
                out,
                "def {name}(_context: http.ActionContext) -> http.ActionResponse:"
            )
            .unwrap();
            if la.has_model {
                writeln!(out, "    records = {model_class}.all()").unwrap();
                writeln!(
                    out,
                    "    return http.ActionResponse(body=views.{view_fn}(records))"
                )
                .unwrap();
            } else {
                writeln!(out, "    return http.ActionResponse(body=\"\")").unwrap();
            }
        }
        ActionKind::Show | ActionKind::Edit => {
            let suffix = if la.kind == ActionKind::Show { "show" } else { "edit" };
            let view_fn = py_view_fn(model_class, suffix);
            writeln!(
                out,
                "def {name}(context: http.ActionContext) -> http.ActionResponse:"
            )
            .unwrap();
            if la.has_model {
                find_record(out);
                writeln!(
                    out,
                    "    return http.ActionResponse(body=views.{view_fn}(record))"
                )
                .unwrap();
            } else {
                writeln!(out, "    return http.ActionResponse(body=\"\")").unwrap();
            }
        }
        ActionKind::New => {
            let view_fn = py_view_fn(model_class, "new");
            writeln!(
                out,
                "def {name}(_context: http.ActionContext) -> http.ActionResponse:"
            )
            .unwrap();
            if la.has_model {
                writeln!(out, "    record = {model_class}()").unwrap();
                writeln!(
                    out,
                    "    return http.ActionResponse(body=views.{view_fn}(record))"
                )
                .unwrap();
            } else {
                writeln!(out, "    return http.ActionResponse(body=\"\")").unwrap();
            }
        }
        ActionKind::Create => {
            writeln!(
                out,
                "def {name}(context: http.ActionContext) -> http.ActionResponse:"
            )
            .unwrap();
            if !la.has_model {
                writeln!(out, "    return http.ActionResponse(body=\"\")").unwrap();
                return;
            }
            writeln!(out, "    record = {model_class}()").unwrap();
            if let Some(p) = &la.parent {
                writeln!(
                    out,
                    "    record.{}_id = int(context.params[\"{}_id\"])",
                    p.singular, p.singular
                )
                .unwrap();
            }
            for field in &la.permitted {
                writeln!(
                    out,
                    "    record.{field} = context.params.get(\"{resource}[{field}]\", \"\")"
                )
                .unwrap();
            }
            writeln!(out, "    if record.save():").unwrap();
            if let Some(p) = &la.parent {
                writeln!(
                    out,
                    "        return http.ActionResponse(status=303, location={}_path(int(context.params[\"{}_id\"])))",
                    p.singular, p.singular,
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "        return http.ActionResponse(status=303, location={resource}_path(record.id))"
                )
                .unwrap();
            }
            if let Some(p) = &la.parent {
                writeln!(
                    out,
                    "    return http.ActionResponse(status=303, location={}_path(int(context.params[\"{}_id\"])))",
                    p.singular, p.singular,
                )
                .unwrap();
            } else {
                let view_fn = py_view_fn(model_class, "new");
                writeln!(
                    out,
                    "    return http.ActionResponse(status=422, body=views.{view_fn}(record))"
                )
                .unwrap();
            }
        }
        ActionKind::Update => {
            writeln!(
                out,
                "def {name}(context: http.ActionContext) -> http.ActionResponse:"
            )
            .unwrap();
            if !la.has_model {
                writeln!(out, "    return http.ActionResponse(body=\"\")").unwrap();
                return;
            }
            find_record(out);
            for field in &la.permitted {
                writeln!(
                    out,
                    "    if \"{resource}[{field}]\" in context.params: record.{field} = context.params[\"{resource}[{field}]\"]"
                )
                .unwrap();
            }
            writeln!(out, "    if record.save():").unwrap();
            writeln!(
                out,
                "        return http.ActionResponse(status=303, location={resource}_path(record.id))"
            )
            .unwrap();
            let edit_view = py_view_fn(model_class, "edit");
            writeln!(
                out,
                "    return http.ActionResponse(status=422, body=views.{edit_view}(record))"
            )
            .unwrap();
        }
        ActionKind::Destroy => {
            writeln!(
                out,
                "def {name}(context: http.ActionContext) -> http.ActionResponse:"
            )
            .unwrap();
            if !la.has_model {
                writeln!(out, "    return http.ActionResponse(body=\"\")").unwrap();
                return;
            }
            writeln!(out, "    record_id = int(context.params[\"id\"])").unwrap();
            writeln!(out, "    record = {model_class}.find(record_id)").unwrap();
            writeln!(out, "    if record is not None: record.destroy()").unwrap();
            if let Some(p) = &la.parent {
                writeln!(
                    out,
                    "    return http.ActionResponse(status=303, location={}_path(int(context.params[\"{}_id\"])))",
                    p.singular, p.singular,
                )
                .unwrap();
            } else {
                let plural = crate::naming::pluralize_snake(model_class);
                writeln!(
                    out,
                    "    return http.ActionResponse(status=303, location={plural}_path())"
                )
                .unwrap();
            }
        }
        ActionKind::Unknown => {
            writeln!(
                out,
                "def {name}(_context: http.ActionContext) -> http.ActionResponse:"
            )
            .unwrap();
            writeln!(out, "    return http.ActionResponse(status=501)").unwrap();
        }
    }
}

// Legacy pass-1 emit retained for any code that still references
// `emit_controllers`; the public entry point is `emit_controllers_pass2`.
#[allow(dead_code)]
fn emit_controllers(app: &App, known_models: &[Symbol]) -> EmittedFile {
    let mut s = String::new();
    writeln!(s, "# Generated by Roundhouse.").unwrap();
    writeln!(s, "from __future__ import annotations").unwrap();
    writeln!(s, "from . import http").unwrap();
    if !known_models.is_empty() {
        writeln!(s, "from .models import *  # noqa: F401, F403").unwrap();
    }
    for controller in &app.controllers {
        writeln!(s).unwrap();
        emit_controller(&mut s, controller, known_models);
    }
    EmittedFile { path: PathBuf::from("app/controllers.py"), content: s }
}

fn emit_controller(out: &mut String, c: &Controller, known_models: &[Symbol]) {
    let name = c.name.0.as_str();
    writeln!(out, "class {name}:").unwrap();

    let (public_actions, private_actions) = crate::lower::split_public_private(c);
    if public_actions.is_empty() && private_actions.is_empty() {
        writeln!(out, "    pass").unwrap();
        return;
    }

    let ctx = PyCtrlCtx { known_models };

    let mut first = true;
    for action in &public_actions {
        if !first {
            writeln!(out).unwrap();
        }
        first = false;
        emit_action(out, action, &ctx, false);
    }
    for action in &private_actions {
        if !first {
            writeln!(out).unwrap();
        }
        first = false;
        emit_action(out, action, &ctx, true);
    }
}

fn emit_action(out: &mut String, a: &Action, ctx: &PyCtrlCtx, private: bool) {
    let raw = a.name.as_str();
    // Private helpers get a leading `_` — Python convention for
    // "internal to the class." Public actions keep their Rails name.
    let rendered_name = if private {
        format!("_{raw}")
    } else {
        raw.to_string()
    };
    writeln!(out, "    async def {rendered_name}(self):").unwrap();

    // Prime before_action ivars: any `@foo` the body reads without
    // first assigning gets `self.foo = Foo()` at the top. Same
    // posture as the other Phase-4c targets.
    let walked = crate::lower::walk_controller_ivars(&a.body);
    let primed_ivars = walked.ivars_read_without_assign();
    for ivar in &primed_ivars {
        let default = py_ivar_default(ivar.as_str(), ctx.known_models);
        writeln!(out, "        self.{} = {default}", ivar.as_str()).unwrap();
    }

    let body_text = emit_py_controller_body(&a.body, ctx);
    if body_text.is_empty() && primed_ivars.is_empty() {
        writeln!(out, "        pass").unwrap();
    } else {
        for line in body_text.lines() {
            if line.is_empty() {
                writeln!(out).unwrap();
            } else {
                writeln!(out, "        {line}").unwrap();
            }
        }
    }
}

/// Python default value for a controller action's primed ivar.
/// Singular known model → `Model()`, plural → `[]`, unresolved →
/// `None`.
fn py_ivar_default(name: &str, known_models: &[Symbol]) -> String {
    let singular_class = crate::naming::singularize_camelize(name);
    let is_plural = singular_class.to_lowercase() != name.to_lowercase();
    if known_models.iter().any(|m| m.as_str() == singular_class) {
        if is_plural {
            return "[]".to_string();
        }
        return format!("{singular_class}()");
    }
    "None".to_string()
}

#[derive(Clone, Copy)]
struct PyCtrlCtx<'a> {
    known_models: &'a [Symbol],
}

/// Emit a controller action body as Python statements. Ivar writes
/// land as `self.foo = expr`; expression statements terminate with a
/// newline (Python doesn't use `;`).
fn emit_py_controller_body(body: &Expr, ctx: &PyCtrlCtx) -> String {
    match &*body.node {
        ExprNode::Seq { exprs } if exprs.is_empty() => String::new(),
        ExprNode::Seq { exprs } => exprs
            .iter()
            .map(|e| emit_py_controller_stmt(e, ctx))
            .collect::<Vec<_>>()
            .join("\n"),
        _ => emit_py_controller_stmt(body, ctx),
    }
}

fn emit_py_controller_stmt(e: &Expr, ctx: &PyCtrlCtx) -> String {
    match &*e.node {
        ExprNode::Assign { target: LValue::Var { name, .. }, value } => {
            format!("{} = {}", name, emit_py_controller_expr(value, ctx))
        }
        ExprNode::Assign { target: LValue::Ivar { name }, value } => {
            format!("self.{} = {}", name, emit_py_controller_expr(value, ctx))
        }
        ExprNode::If { cond, then_branch, else_branch } => {
            let cond_s = emit_py_controller_expr(cond, ctx);
            let then_s = emit_py_block_body(then_branch, ctx);
            let else_s = emit_py_block_body(else_branch, ctx);
            format!("if {cond_s}:\n{}\nelse:\n{}", indent_py(&then_s), indent_py(&else_s))
        }
        _ => emit_py_controller_expr(e, ctx),
    }
}

fn emit_py_controller_expr(e: &Expr, ctx: &PyCtrlCtx) -> String {
    match &*e.node {
        ExprNode::Send { recv, method, args, block, .. } => {
            if let Some(s) = emit_py_controller_send(
                recv.as_ref(),
                method.as_str(),
                args,
                block.as_ref(),
                ctx,
            ) {
                return s;
            }
            // Fall through to generic emit_expr for unclassified
            // Sends, but re-render args/recv with ctx-aware dispatch
            // so nested rewrites apply.
            let args_s: Vec<String> = args
                .iter()
                .map(|a| emit_py_controller_expr(a, ctx))
                .collect();
            match recv {
                None => {
                    if args.is_empty() {
                        method.to_string()
                    } else {
                        format!("{}({})", method, args_s.join(", "))
                    }
                }
                Some(r) => {
                    let recv_s = emit_py_controller_expr(r, ctx);
                    if args.is_empty() {
                        format!("{recv_s}.{method}()")
                    } else {
                        format!("{recv_s}.{method}({})", args_s.join(", "))
                    }
                }
            }
        }
        ExprNode::Ivar { name } => format!("self.{name}"),
        _ => emit_expr(e),
    }
}

fn emit_py_block_body(e: &Expr, ctx: &PyCtrlCtx) -> String {
    let body = match &*e.node {
        ExprNode::Lambda { body, .. } => body,
        _ => e,
    };
    match &*body.node {
        ExprNode::Seq { exprs } if !exprs.is_empty() => exprs
            .iter()
            .map(|s| emit_py_controller_stmt(s, ctx))
            .collect::<Vec<_>>()
            .join("\n"),
        _ => emit_py_controller_stmt(body, ctx),
    }
}

fn indent_py(s: &str) -> String {
    s.lines()
        .map(|l| if l.is_empty() { String::new() } else { format!("    {l}") })
        .collect::<Vec<_>>()
        .join("\n")
}

/// Python render table over `SendKind` from `lower::controller`.
fn emit_py_controller_send(
    recv: Option<&Expr>,
    method: &str,
    args: &[Expr],
    block: Option<&Expr>,
    ctx: &PyCtrlCtx,
) -> Option<String> {
    use crate::lower::SendKind;
    let args_s: Vec<String> =
        args.iter().map(|a| emit_py_controller_expr(a, ctx)).collect();

    let kind = crate::lower::classify_controller_send(
        recv,
        method,
        args,
        block,
        ctx.known_models,
    )?;
    Some(match kind {
        SendKind::ParamsAccess => "http.params()".to_string(),
        SendKind::ParamsExpect { .. } => {
            format!("http.params().expect({})", args_s.join(", "))
        }
        SendKind::ParamsIndex { .. } => {
            let arg = args_s.first().cloned().unwrap_or_default();
            format!("http.params()[{arg}]")
        }

        SendKind::ModelNew { class } => format!("{}()", class.as_str()),

        SendKind::ModelFind { class, .. } => {
            // Python `Model.find(x)` may return None; the declared
            // Phase-4c flow doesn't unwrap since actions never run.
            let arg = args_s.first().cloned().unwrap_or_default();
            format!("{}.find({arg})", class.as_str())
        }

        SendKind::AssocLookup { target, .. } => format!("{}()", target.as_str()),

        SendKind::QueryChain { target: Some(target) } => {
            // List comprehension would be idiomatic later; for 4c a
            // bare empty list with a comment preserves the target
            // hint. Same rationale discussed for TS's `[] as Post[]`.
            format!("[]  # {}[] placeholder", target.as_str())
        }
        SendKind::QueryChain { target: None } => "[]".to_string(),

        SendKind::PathOrUrlHelper => "\"\"".to_string(),

        SendKind::BangStrip { recv, stripped_method, .. } => {
            // Python forbids `!` in identifiers. Strip like Rust / Go
            // / Elixir / TS do. No parens-vs-none distinction: Python
            // methods always require `()`, so emit as a call.
            let recv_s = emit_py_controller_expr(recv, ctx);
            format!("{recv_s}.{stripped_method}()")
        }

        SendKind::InstanceUpdate => "False".to_string(),

        SendKind::Render { .. } => format!("http.render({})", args_s.join(", ")),
        SendKind::RedirectTo { .. } => {
            format!("http.redirect_to({})", args_s.join(", "))
        }
        SendKind::Head { .. } => format!("http.head({})", args_s.join(", ")),

        // Python lambdas can't hold statements, and `respond_to` /
        // `format.html` block bodies contain if/else control flow.
        // Flatten inline like Elixir: the HTML branch body emits
        // directly at the call site; the JSON branch becomes a TODO
        // comment. The `http.respond_to` / `fr.html` stubs stay
        // callable for hand-written code that wants the real shape.
        SendKind::RespondToBlock { body } => emit_py_block_body(body, ctx),

        SendKind::FormatHtml { body } => emit_py_block_body(body, ctx),

        SendKind::FormatJson => "# TODO: JSON branch (Phase 4e)".to_string(),
    })
}

// Routes ---------------------------------------------------------------

fn emit_routes(app: &App) -> EmittedFile {
    let mut s = String::new();
    writeln!(s, "# Generated by Roundhouse.").unwrap();
    writeln!(s, "from __future__ import annotations").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "from app.http import Router").unwrap();

    // Collect controller module references the routes touch.
    let mut controllers: Vec<String> = Vec::new();
    for entry in &app.routes.entries {
        collect_controller_refs_py(entry, &mut controllers);
    }
    for ctrl in &controllers {
        let stem = crate::naming::snake_case(ctrl);
        writeln!(s, "from app.controllers import {stem} as {ctrl}").unwrap();
    }
    writeln!(s).unwrap();

    // Reset before registering — re-imports during tests (pytest,
    // unittest rerun) shouldn't accumulate duplicate entries.
    writeln!(s, "Router.reset()").unwrap();

    for entry in &app.routes.entries {
        emit_py_route_spec(&mut s, entry);
    }

    EmittedFile { path: PathBuf::from("app/routes.py"), content: s }
}

fn collect_controller_refs_py(spec: &RouteSpec, out: &mut Vec<String>) {
    let mut push = |name: String, out: &mut Vec<String>| {
        if !out.iter().any(|c| c == &name) {
            out.push(name);
        }
    };
    match spec {
        RouteSpec::Explicit { controller, .. } => {
            push(controller.0.to_string(), out);
        }
        RouteSpec::Root { target } => {
            if let Some((c, _)) = target.split_once('#') {
                push(controller_class_name(c), out);
            }
        }
        RouteSpec::Resources { name, nested, .. } => {
            push(controller_class_name(name.as_str()), out);
            for child in nested {
                collect_controller_refs_py(child, out);
            }
        }
    }
}

fn emit_py_route_spec(out: &mut String, spec: &RouteSpec) {
    match spec {
        RouteSpec::Explicit { method, path, controller, action, .. } => {
            let verb = match method {
                crate::dialect::HttpMethod::Get => "get",
                crate::dialect::HttpMethod::Post => "post",
                crate::dialect::HttpMethod::Put => "put",
                crate::dialect::HttpMethod::Patch => "patch",
                crate::dialect::HttpMethod::Delete => "delete",
                _ => "get",
            };
            writeln!(
                out,
                "Router.{verb}({:?}, {}, {:?})",
                path,
                controller.0,
                action.as_str(),
            )
            .unwrap();
        }
        RouteSpec::Root { target } => {
            let (controller, action) = target
                .split_once('#')
                .map(|(c, a)| (controller_class_name(c), a.to_string()))
                .unwrap_or_else(|| (target.clone(), "index".to_string()));
            writeln!(out, "Router.root({controller}, {action:?})").unwrap();
        }
        RouteSpec::Resources { name, only, except: _, nested } => {
            let controller = controller_class_name(name.as_str());
            let mut opts: Vec<String> = Vec::new();
            if !only.is_empty() {
                let parts: Vec<String> =
                    only.iter().map(|s| format!("{:?}", s.as_str())).collect();
                opts.push(format!("only=[{}]", parts.join(", ")));
            }
            if !nested.is_empty() {
                let mut nested_parts: Vec<String> = Vec::new();
                for child in nested {
                    if let Some(part) = py_nested_spec_entry(child) {
                        nested_parts.push(part);
                    }
                }
                if !nested_parts.is_empty() {
                    opts.push(format!("nested=[{}]", nested_parts.join(", ")));
                }
            }
            if opts.is_empty() {
                writeln!(
                    out,
                    "Router.resources({:?}, {})",
                    name.as_str(),
                    controller
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "Router.resources({:?}, {}, {})",
                    name.as_str(),
                    controller,
                    opts.join(", ")
                )
                .unwrap();
            }
        }
    }
}

fn py_nested_spec_entry(spec: &RouteSpec) -> Option<String> {
    let RouteSpec::Resources { name, only, except: _, nested: _ } = spec else {
        return None;
    };
    let controller = controller_class_name(name.as_str());
    let mut parts: Vec<String> = Vec::new();
    parts.push(format!("\"name\": {:?}", name.as_str()));
    parts.push(format!("\"controller\": {controller}"));
    if !only.is_empty() {
        let items: Vec<String> =
            only.iter().map(|s| format!("{:?}", s.as_str())).collect();
        parts.push(format!("\"only\": [{}]", items.join(", ")));
    }
    Some(format!("{{{}}}", parts.join(", ")))
}

/// Emit `app/route_helpers.py` — one `<as_name>_path(args)`
/// function per unique route `as_name`, derived from the
/// flattened route table. Mirrors the Rust/TS/Crystal twins with
/// Python naming (snake_case, no `Path` suffix).
fn emit_py_route_helpers(app: &App) -> EmittedFile {
    let flat = crate::lower::flatten_routes(app);
    let mut s = String::new();
    writeln!(s, "# Generated by Roundhouse.").unwrap();
    writeln!(s, "from __future__ import annotations").unwrap();
    writeln!(s).unwrap();
    use std::collections::BTreeSet;
    let mut seen: BTreeSet<String> = BTreeSet::new();
    for route in &flat {
        if !seen.insert(route.as_name.clone()) {
            continue;
        }
        let fn_name = format!("{}_path", route.as_name);
        let sig_params: Vec<String> = route
            .path_params
            .iter()
            .map(|p| format!("{p}: int = 0"))
            .collect();
        let sig = sig_params.join(", ");
        let body = if route.path_params.is_empty() {
            format!("return {:?}", route.path)
        } else {
            // Build an f-string with ${param} replaced by {param}.
            let mut tmpl = String::new();
            let mut chars = route.path.chars().peekable();
            while let Some(c) = chars.next() {
                if c == ':' {
                    let mut ident = String::new();
                    while let Some(&nc) = chars.peek() {
                        if nc.is_alphanumeric() || nc == '_' {
                            ident.push(nc);
                            chars.next();
                        } else {
                            break;
                        }
                    }
                    tmpl.push('{');
                    tmpl.push_str(&ident);
                    tmpl.push('}');
                } else {
                    tmpl.push(c);
                }
            }
            format!("return f{:?}", tmpl)
        };
        writeln!(s, "def {fn_name}({sig}) -> str: {body}").unwrap();
    }
    EmittedFile {
        path: PathBuf::from("app/route_helpers.py"),
        content: s,
    }
}


// Views ----------------------------------------------------------------

/// Emit `app/views.py` — one flat module with all view functions.
/// Python's filenames can't contain dots-inside-stems cleanly
/// (`articles.index.py` works but `articles/index.html.py`
/// doesn't), so every view fn collapses into a single module. The
/// controllers import as `from app import views` and call
/// `views.render_articles_index(records)` etc.
fn emit_py_views(app: &App) -> Vec<EmittedFile> {
    if app.views.is_empty() && app.controllers.is_empty() {
        return Vec::new();
    }
    let known_models: Vec<Symbol> =
        app.models.iter().map(|m| m.name.0.clone()).collect();
    let attrs_by_class: std::collections::BTreeMap<String, Vec<String>> = app
        .models
        .iter()
        .map(|m| {
            (
                m.name.0.as_str().to_string(),
                m.attributes
                    .fields
                    .keys()
                    .map(|k| k.as_str().to_string())
                    .collect(),
            )
        })
        .collect();

    let mut s = String::new();
    writeln!(s, "# Generated by Roundhouse.").unwrap();
    writeln!(s, "from __future__ import annotations").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "from typing import Any").unwrap();
    writeln!(s, "from app import view_helpers as Helpers").unwrap();
    writeln!(s, "from app.route_helpers import *  # noqa: F401, F403").unwrap();
    if !known_models.is_empty() {
        writeln!(s, "from app.models import *  # noqa: F401, F403").unwrap();
    }
    writeln!(s).unwrap();

    let mut emitted_names: std::collections::BTreeSet<String> =
        std::collections::BTreeSet::new();

    for v in &app.views {
        let fn_name = py_view_function_name(v.name.as_str());
        if !emitted_names.insert(fn_name.clone()) {
            continue;
        }
        emit_view_file_pass2_py(&mut s, v, &known_models, &attrs_by_class);
        writeln!(s).unwrap();
    }

    // Stub missing standard CRUD views so controllers can always
    // import. Same posture as TS's _stubs.ts.
    for model in &app.models {
        if model.attributes.fields.is_empty() {
            continue;
        }
        let class = model.name.0.as_str();
        let plural = crate::naming::pluralize_snake(class);
        for (_, suffix) in [
            ("Index", "index"),
            ("Show", "show"),
            ("New", "new"),
            ("Edit", "edit"),
        ] {
            let view_name = format!("{plural}/{suffix}");
            let fn_name = py_view_function_name(&view_name);
            if emitted_names.insert(fn_name.clone()) {
                writeln!(s, "def {fn_name}(_record: Any) -> str:").unwrap();
                writeln!(s, "    return \"\"").unwrap();
                writeln!(s).unwrap();
            }
        }
    }

    vec![EmittedFile {
        path: PathBuf::from("app/views.py"),
        content: s,
    }]
}

/// `articles/index` → `render_articles_index`. Slash → underscore;
/// partial-prefix underscores get stripped (`_article` → `article`).
fn py_view_function_name(name: &str) -> String {
    let mut parts: Vec<String> = Vec::new();
    for seg in name.split('/') {
        let trimmed = seg.strip_prefix('_').unwrap_or(seg);
        parts.push(trimmed.to_string());
    }
    format!("render_{}", parts.join("_"))
}

fn emit_view_file_pass2_py(
    out: &mut String,
    view: &crate::dialect::View,
    known_models: &[Symbol],
    attrs_by_class: &std::collections::BTreeMap<String, Vec<String>>,
) {
    // Python doesn't rewrite ivars → locals in the AST; instead,
    // `emit_py_view_expr_raw` / `emit_py_view_stmt_pass2` treat
    // `Ivar { name }` the same as `Var { name }`. The arg name
    // from `py_view_signature` matches the ivar name in ERB bodies
    // (e.g. signature `articles: list[Article]` with ivar `@articles`).
    let fn_name = py_view_function_name(view.name.as_str());

    let (sig, arg_name, arg_model) =
        py_view_signature(view.name.as_str(), known_models);
    let attrs = arg_model
        .as_ref()
        .and_then(|c| attrs_by_class.get(c).cloned())
        .unwrap_or_default();

    writeln!(out, "def {fn_name}({sig}) -> str:").unwrap();
    writeln!(out, "    _buf = \"\"").unwrap();

    let mut locals: Vec<String> = vec!["_buf".to_string(), arg_name.clone()];
    let ivar_names = py_collect_ivar_names(&view.body);
    for n in &ivar_names {
        if !locals.iter().any(|x| x == n) {
            locals.push(n.clone());
        }
    }
    let resource_dir = view
        .name
        .as_str()
        .rsplit_once('/')
        .map(|(d, _): (&str, &str)| d.to_string())
        .unwrap_or_default();
    let ctx = PyViewCtx {
        locals,
        arg_name: arg_name.clone(),
        arg_attrs: attrs,
        resource_dir,
    };

    let body_lines = emit_py_view_body(&view.body, &ctx);
    for line in body_lines {
        writeln!(out, "    {line}").unwrap();
    }
    writeln!(out, "    return _buf").unwrap();
}

struct PyViewCtx {
    locals: Vec<String>,
    arg_name: String,
    arg_attrs: Vec<String>,
    resource_dir: String,
}

impl PyViewCtx {
    fn is_local(&self, n: &str) -> bool {
        self.locals.iter().any(|x| x == n)
    }
    fn arg_has_attr(&self, name: &str, attr: &str) -> bool {
        name == self.arg_name && self.arg_attrs.iter().any(|a| a == attr)
    }
}

fn py_view_signature(
    view_name: &str,
    known_models: &[Symbol],
) -> (String, String, Option<String>) {
    let (dir, base) = view_name.rsplit_once('/').unwrap_or(("", view_name));
    let is_partial = base.starts_with('_');
    let stem = base.trim_start_matches('_');
    let model_class = crate::naming::singularize_camelize(dir);
    let model_exists = known_models.iter().any(|m| m.as_str() == model_class);
    let singular = crate::naming::singularize(dir);

    if is_partial {
        let arg_name = if model_exists { singular.clone() } else { stem.to_string() };
        if model_exists {
            return (format!("{arg_name}: {model_class}"), arg_name, Some(model_class));
        }
        return (format!("{arg_name}: Any"), arg_name, None);
    }
    match stem {
        "index" => {
            let arg_name = dir.to_string();
            if model_exists {
                return (
                    format!("{arg_name}: list[{model_class}]"),
                    arg_name,
                    Some(model_class),
                );
            }
            return (format!("{arg_name}: list[Any]"), arg_name, None);
        }
        _ => {
            let arg_name = singular.clone();
            if model_exists {
                return (format!("{arg_name}: {model_class}"), arg_name, Some(model_class));
            }
            return (format!("{arg_name}: Any"), arg_name, None);
        }
    }
}

fn emit_py_view_body(body: &Expr, ctx: &PyViewCtx) -> Vec<String> {
    let stmts: Vec<&Expr> = match &*body.node {
        ExprNode::Seq { exprs } => exprs.iter().collect(),
        _ => vec![body],
    };
    let mut out = Vec::new();
    for stmt in &stmts {
        out.extend(emit_py_view_stmt_pass2(stmt, ctx));
    }
    if out.is_empty() {
        out.push("pass".to_string());
    }
    out
}

fn emit_py_view_stmt_pass2(stmt: &Expr, ctx: &PyViewCtx) -> Vec<String> {
    match &*stmt.node {
        // Prologue `_buf = ""` — already written by `emit_view_file_pass2_py`.
        ExprNode::Assign { target: LValue::Var { name, .. }, value }
            if name.as_str() == "_buf" =>
        {
            if let ExprNode::Lit { value: Literal::Str { value: s } } = &*value.node {
                if s.is_empty() {
                    return Vec::new();
                }
            }
            // `_buf = _buf + X` — the working shape.
            if let ExprNode::Send { recv: Some(recv), method, args, .. } = &*value.node {
                if method.as_str() == "+" && args.len() == 1 {
                    if let ExprNode::Var { name: rn, .. } = &*recv.node {
                        if rn.as_str() == "_buf" {
                            let chunk = emit_py_view_append_pass2(&args[0], ctx);
                            return chunk
                                .lines()
                                .map(|l| l.to_string())
                                .collect();
                        }
                    }
                }
            }
            vec!["pass  # TODO ERB: _buf shape".to_string()]
        }
        // Epilogue: bare `_buf` — dropped; we emit `return _buf` at the end.
        ExprNode::Var { name, .. } if name.as_str() == "_buf" => Vec::new(),
        // Ivar read at statement level (rare) — drop; the view walker
        // only cares about `_buf` assigns.
        ExprNode::Ivar { .. } => Vec::new(),
        ExprNode::If { cond, then_branch, else_branch } => {
            let cond_py = if is_py_simple_expr(cond, ctx) {
                emit_py_view_expr_raw(cond, ctx)
            } else {
                "False".to_string()
            };
            let mut out = vec![format!("if {cond_py}:")];
            let then_lines = emit_py_view_body(then_branch, ctx);
            for line in then_lines {
                out.push(format!("    {line}"));
            }
            let has_else = !matches!(
                &*else_branch.node,
                ExprNode::Lit { value: Literal::Nil }
            );
            if has_else {
                out.push("else:".to_string());
                for line in emit_py_view_body(else_branch, ctx) {
                    out.push(format!("    {line}"));
                }
            }
            out
        }
        ExprNode::Send { recv: Some(recv), method, args, block: Some(block), .. }
            if method.as_str() == "each" && args.is_empty() =>
        {
            if !is_py_simple_expr(recv, ctx) {
                return vec!["pass  # TODO ERB: each over complex coll".to_string()];
            }
            let ExprNode::Lambda { params, body, .. } = &*block.node else {
                return vec!["pass  # unexpected each block".to_string()];
            };
            let coll_s = emit_py_view_expr_raw(recv, ctx);
            let var = params
                .first()
                .map(|p| p.as_str().to_string())
                .unwrap_or_else(|| "item".into());
            let inner_ctx = PyViewCtx {
                locals: {
                    let mut l = ctx.locals.clone();
                    if !l.iter().any(|x| x == &var) {
                        l.push(var.clone());
                    }
                    l
                },
                arg_name: ctx.arg_name.clone(),
                arg_attrs: ctx.arg_attrs.clone(),
                resource_dir: ctx.resource_dir.clone(),
            };
            let mut out = vec![format!("for {var} in {coll_s}:")];
            let inner_lines = emit_py_view_body(body, &inner_ctx);
            for line in inner_lines {
                out.push(format!("    {line}"));
            }
            out
        }
        _ => vec!["pass  # TODO ERB: unknown stmt".to_string()],
    }
}

fn emit_py_view_append_pass2(arg: &Expr, ctx: &PyViewCtx) -> String {
    // Text chunk.
    if let ExprNode::Lit { value: Literal::Str { value: s } } = &*arg.node {
        return format!("_buf += {}", py_string_literal(s));
    }
    let inner = unwrap_to_s_py(arg);

    // `render @coll` or `render "partial", locals` — expand.
    if let ExprNode::Send { recv: None, method, args, block: None, .. } = &*inner.node {
        if method.as_str() == "render" {
            if args.len() == 1 {
                return emit_py_render_call(&args[0], ctx);
            }
            if args.len() == 2 {
                if let (
                    ExprNode::Lit { value: Literal::Str { value: partial } },
                    ExprNode::Hash { entries, .. },
                ) = (&*args[0].node, &*args[1].node)
                {
                    let partial_fn = format!(
                        "render_{}_{}",
                        ctx.resource_dir,
                        partial.trim_start_matches('_'),
                    );
                    if let Some((_, v)) = entries.first() {
                        if is_py_simple_expr(v, ctx) {
                            let arg_expr = emit_py_view_expr_raw(v, ctx);
                            return format!("_buf += {partial_fn}({arg_expr})");
                        }
                    }
                    return format!("_buf += {partial_fn}(None)");
                }
            }
        }
    }

    // form_with / content_for — capturing helpers.
    if let ExprNode::Send {
        recv: None,
        method,
        args,
        block: Some(block),
        ..
    } = &*inner.node
    {
        if is_py_capturing_helper(method.as_str()) {
            return emit_py_captured_helper(method.as_str(), args, block, ctx);
        }
    }

    // Simple interpolation.
    if is_py_simple_expr(inner, ctx) {
        return format!(
            "_buf += str({})",
            emit_py_view_expr_raw(inner, ctx),
        );
    }

    "_buf += \"\"  # TODO ERB: complex interpolation".to_string()
}

fn is_py_capturing_helper(method: &str) -> bool {
    matches!(method, "form_with" | "content_for")
}

fn emit_py_captured_helper(
    method: &str,
    args: &[Expr],
    block: &Expr,
    ctx: &PyViewCtx,
) -> String {
    let ExprNode::Lambda { params, body, .. } = &*block.node else {
        return format!("_buf += \"\"  # TODO ERB: {method}");
    };
    let cls_expr = args
        .iter()
        .find_map(|a| py_extract_kwarg(a, "class"))
        .filter(|e| is_py_simple_expr(e, ctx))
        .map(|e| emit_py_view_expr_raw(e, ctx))
        .unwrap_or_else(|| "\"\"".to_string());
    match method {
        "form_with" => {
            // Snapshot-and-slice capture: track where the form
            // block starts in `_buf`, append to `_buf` inline
            // (block body emits `_buf += ...` statements at the
            // same indent level), then wrap the appended span via
            // `form_wrap`. Avoids nested `def` which can't share
            // indent with the outer statement stream.
            let pname = params.first().map(|p| p.as_str()).unwrap_or("form");
            let inner_ctx = PyViewCtx {
                locals: {
                    let mut l = ctx.locals.clone();
                    l.push(pname.to_string());
                    l
                },
                arg_name: ctx.arg_name.clone(),
                arg_attrs: ctx.arg_attrs.clone(),
                resource_dir: ctx.resource_dir.clone(),
            };
            let mut lines = vec![
                "_form_begin = len(_buf)".to_string(),
                format!("{pname} = Helpers.FormBuilder(None)"),
            ];
            for line in emit_py_view_body(body, &inner_ctx) {
                lines.push(line);
            }
            lines.push(format!(
                "_buf = _buf[:_form_begin] + Helpers.form_wrap(None, {cls_expr}, _buf[_form_begin:])"
            ));
            lines.join("\n")
        }
        _ => {
            let _ = cls_expr;
            "_buf += \"\"".to_string()
        }
    }
}

fn emit_py_render_call(arg: &Expr, ctx: &PyViewCtx) -> String {
    match &*arg.node {
        ExprNode::Var { name, .. } | ExprNode::Ivar { name }
            if ctx.is_local(name.as_str()) =>
        {
            let singular = crate::naming::singularize(name.as_str());
            let partial_fn = format!("render_{}_{singular}", name.as_str());
            let coll = name.to_string();
            format!("_buf += \"\".join({partial_fn}(_r) for _r in {coll})")
        }
        ExprNode::Send { recv: Some(r), method, args, .. }
            if args.is_empty()
                && matches!(&*r.node, ExprNode::Var { .. } | ExprNode::Ivar { .. }) =>
        {
            let assoc_plural = method.as_str();
            let singular = crate::naming::singularize(assoc_plural);
            let partial_fn = format!("render_{assoc_plural}_{singular}");
            let parent_name = match &*r.node {
                ExprNode::Var { name, .. } | ExprNode::Ivar { name } => name.to_string(),
                _ => unreachable!(),
            };
            format!(
                "_buf += \"\".join({partial_fn}(_c) for _c in {parent_name}.{assoc_plural})"
            )
        }
        _ => "_buf += \"\"  # TODO ERB: render".to_string(),
    }
}

fn py_extract_kwarg<'a>(arg: &'a Expr, key: &str) -> Option<&'a Expr> {
    if let ExprNode::Hash { entries, .. } = &*arg.node {
        for (k, v) in entries {
            if let ExprNode::Lit { value: Literal::Sym { value } } = &*k.node {
                if value.as_str() == key {
                    return Some(v);
                }
            }
        }
    }
    None
}

fn unwrap_to_s_py(expr: &Expr) -> &Expr {
    if let ExprNode::Send { recv: Some(inner), method, args, .. } = &*expr.node {
        if method.as_str() == "to_s" && args.is_empty() {
            return inner;
        }
    }
    expr
}

fn py_string_literal(s: &str) -> String {
    // Python's repr() gives a safely-escaped string literal.
    // Prefer double-quoted form when the string has no embedded
    // double-quote.
    if !s.contains('"') && !s.contains('\\') && !s.contains('\n') {
        return format!("\"{}\"", s);
    }
    format!("{:?}", s)
}

fn is_py_simple_expr(expr: &Expr, ctx: &PyViewCtx) -> bool {
    match &*expr.node {
        ExprNode::Lit { .. } => true,
        ExprNode::Var { name, .. } | ExprNode::Ivar { name } => {
            ctx.is_local(name.as_str())
        }
        ExprNode::Send { recv: Some(r), method, args, block, .. } => {
            if !args.is_empty() || block.is_some() {
                return false;
            }
            let clean = method.as_str().trim_end_matches('?').trim_end_matches('!');
            if clean.is_empty() {
                return false;
            }
            if let ExprNode::Var { name, .. } | ExprNode::Ivar { name } = &*r.node {
                if ctx.arg_has_attr(name.as_str(), clean) {
                    return true;
                }
                if ctx.is_local(name.as_str())
                    && matches!(
                        method.as_str(),
                        "any?" | "none?" | "present?" | "empty?"
                    )
                {
                    return true;
                }
            }
            false
        }
        ExprNode::StringInterp { parts } => parts.iter().all(|p| match p {
            crate::expr::InterpPart::Text { .. } => true,
            crate::expr::InterpPart::Expr { expr } => is_py_simple_expr(expr, ctx),
        }),
        _ => false,
    }
}

fn emit_py_view_expr_raw(expr: &Expr, ctx: &PyViewCtx) -> String {
    match &*expr.node {
        ExprNode::Lit { value } => emit_literal(value),
        ExprNode::Var { name, .. } | ExprNode::Ivar { name } => name.to_string(),
        ExprNode::Send { recv: Some(r), method, args, .. } => {
            let method_s = method.as_str();
            if args.is_empty() {
                if let ExprNode::Var { name, .. } | ExprNode::Ivar { name } = &*r.node {
                    if ctx.is_local(name.as_str()) {
                        match method_s {
                            "any?" | "present?" => return format!("(len({name}) > 0)"),
                            "none?" | "empty?" => return format!("(len({name}) == 0)"),
                            _ => {}
                        }
                    }
                }
            }
            let recv_s = emit_py_view_expr_raw(r, ctx);
            let clean = method_s.trim_end_matches('?').trim_end_matches('!');
            if args.is_empty() {
                format!("{recv_s}.{clean}")
            } else {
                let args_s: Vec<String> = args
                    .iter()
                    .map(|a| emit_py_view_expr_raw(a, ctx))
                    .collect();
                format!("{recv_s}.{clean}({})", args_s.join(", "))
            }
        }
        ExprNode::StringInterp { parts } => {
            use crate::expr::InterpPart;
            let mut out = String::from("f\"");
            for p in parts {
                match p {
                    InterpPart::Text { value } => {
                        for c in value.chars() {
                            if c == '"' || c == '\\' {
                                out.push('\\');
                            }
                            out.push(c);
                        }
                    }
                    InterpPart::Expr { expr } => {
                        out.push('{');
                        out.push_str(&emit_py_view_expr_raw(expr, ctx));
                        out.push('}');
                    }
                }
            }
            out.push('"');
            out
        }
        _ => "\"\"".to_string(),
    }
}

fn py_collect_ivar_names(expr: &Expr) -> Vec<String> {
    let mut out: Vec<String> = Vec::new();
    py_collect_ivars_into(expr, &mut out);
    out
}

fn py_collect_ivars_into(expr: &Expr, out: &mut Vec<String>) {
    match &*expr.node {
        ExprNode::Ivar { name } => {
            let n = name.to_string();
            if !out.iter().any(|existing| existing == &n) {
                out.push(n);
            }
        }
        ExprNode::Assign { target, value } => {
            if let LValue::Ivar { name } = target {
                let n = name.to_string();
                if !out.iter().any(|existing| existing == &n) {
                    out.push(n);
                }
            }
            py_collect_ivars_into(value, out);
        }
        ExprNode::Send { recv, args, block, .. } => {
            if let Some(r) = recv {
                py_collect_ivars_into(r, out);
            }
            for a in args {
                py_collect_ivars_into(a, out);
            }
            if let Some(b) = block {
                py_collect_ivars_into(b, out);
            }
        }
        ExprNode::Seq { exprs } | ExprNode::Array { elements: exprs, .. } => {
            for e in exprs {
                py_collect_ivars_into(e, out);
            }
        }
        ExprNode::Hash { entries, .. } => {
            for (k, v) in entries {
                py_collect_ivars_into(k, out);
                py_collect_ivars_into(v, out);
            }
        }
        ExprNode::If { cond, then_branch, else_branch } => {
            py_collect_ivars_into(cond, out);
            py_collect_ivars_into(then_branch, out);
            py_collect_ivars_into(else_branch, out);
        }
        ExprNode::BoolOp { left, right, .. } => {
            py_collect_ivars_into(left, out);
            py_collect_ivars_into(right, out);
        }
        ExprNode::Lambda { body, .. } => py_collect_ivars_into(body, out),
        ExprNode::StringInterp { parts } => {
            for p in parts {
                if let crate::expr::InterpPart::Expr { expr } = p {
                    py_collect_ivars_into(expr, out);
                }
            }
        }
        _ => {}
    }
}

fn collect_flat_routes(
    spec: &RouteSpec,
    out: &mut Vec<(String, String, String, String)>,
    scope_prefix: Option<(&str, &str)>,
) {
    match spec {
        RouteSpec::Explicit { method, path, controller, action, .. } => {
            let verb = match method {
                crate::dialect::HttpMethod::Get => "GET",
                crate::dialect::HttpMethod::Post => "POST",
                crate::dialect::HttpMethod::Put => "PUT",
                crate::dialect::HttpMethod::Patch => "PATCH",
                crate::dialect::HttpMethod::Delete => "DELETE",
                crate::dialect::HttpMethod::Head => "HEAD",
                crate::dialect::HttpMethod::Options => "OPTIONS",
                crate::dialect::HttpMethod::Any => "ANY",
            };
            let full_path = match scope_prefix {
                Some((parent, _)) => format!("/{parent}/:{parent}_id{path}"),
                None => path.clone(),
            };
            out.push((
                verb.to_string(),
                full_path,
                controller.0.to_string(),
                action.to_string(),
            ));
        }
        RouteSpec::Root { target } => {
            if let Some((c, a)) = target.split_once('#') {
                out.push(("GET".into(), "/".into(), controller_class_name(c), a.into()));
            }
        }
        RouteSpec::Resources { name, only, except, nested } => {
            let resource_path = format!("/{name}");
            let controller = controller_class_name(name.as_str());
            for (action, verb, suffix) in standard_resource_actions() {
                let action = *action;
                let verb = *verb;
                let suffix = *suffix;
                if !only.is_empty() && !only.iter().any(|s| s.as_str() == action) {
                    continue;
                }
                if except.iter().any(|s| s.as_str() == action) {
                    continue;
                }
                let path = format!("{resource_path}{suffix}");
                let full_path = match scope_prefix {
                    Some((parent, _)) => format!("/{parent}/:{parent}_id{path}"),
                    None => path,
                };
                out.push((verb.into(), full_path, controller.clone(), action.into()));
            }
            let singular =
                crate::naming::singularize_camelize(name.as_str()).to_lowercase();
            for child in nested {
                collect_flat_routes(child, out, Some((&singular, name.as_str())));
            }
        }
    }
}

fn standard_resource_actions() -> &'static [(&'static str, &'static str, &'static str)] {
    &[
        ("index", "GET", ""),
        ("new", "GET", "/new"),
        ("create", "POST", ""),
        ("show", "GET", "/:id"),
        ("edit", "GET", "/:id/edit"),
        ("update", "PATCH", "/:id"),
        ("destroy", "DELETE", "/:id"),
    ]
}

fn controller_class_name(short: &str) -> String {
    let mut s = crate::naming::camelize(short);
    s.push_str("Controller");
    s
}

// Bodies + expressions -------------------------------------------------

fn emit_body(body: &Expr, return_ty: &Ty) -> String {
    let is_void = matches!(return_ty, Ty::Nil);
    match &*body.node {
        ExprNode::Assign { target: LValue::Ivar { .. }, value } => {
            if is_void {
                emit_expr(value)
            } else {
                format!("return {}", emit_expr(value))
            }
        }
        ExprNode::Seq { exprs } if !exprs.is_empty() => {
            let mut lines: Vec<String> = Vec::new();
            for (i, e) in exprs.iter().enumerate() {
                if i > 0 && e.leading_blank_line {
                    lines.push(String::new());
                }
                lines.push(emit_stmt(e, i == exprs.len() - 1, is_void));
            }
            lines.join("\n")
        }
        _ => {
            if is_void {
                emit_expr(body)
            } else {
                format!("return {}", emit_expr(body))
            }
        }
    }
}

fn emit_stmt(e: &Expr, is_last: bool, void_return: bool) -> String {
    match &*e.node {
        ExprNode::Assign { target: LValue::Var { name, .. }, value } => {
            format!("{} = {}", name, emit_expr(value))
        }
        ExprNode::Assign { target: LValue::Ivar { name }, value } => {
            format!("self.{} = {}", name, emit_expr(value))
        }
        _ => {
            if is_last && !void_return {
                format!("return {}", emit_expr(e))
            } else {
                emit_expr(e)
            }
        }
    }
}

fn emit_expr(e: &Expr) -> String {
    match &*e.node {
        ExprNode::Lit { value } => emit_literal(value),
        ExprNode::Const { path } => {
            path.iter().map(|s| s.to_string()).collect::<Vec<_>>().join(".")
        }
        ExprNode::Var { name, .. } => name.to_string(),
        ExprNode::Ivar { name } => format!("self.{name}"),
        ExprNode::Send { recv, method, args, .. } => {
            emit_send(recv.as_ref(), method.as_str(), args)
        }
        ExprNode::Assign { target: _, value } => emit_expr(value),
        ExprNode::Seq { exprs } => {
            exprs.iter().map(emit_expr).collect::<Vec<_>>().join("; ")
        }
        ExprNode::If { cond, then_branch, else_branch } => {
            // Python `if … : \n body \n else: \n body`. No braces,
            // no trailing `end`. The inner emitter produces the
            // branch expression as a single line — the scaffold
            // doesn't try to reformat multi-line if bodies.
            let cond_s = emit_expr(cond);
            let then_s = emit_expr(then_branch);
            let else_s = emit_expr(else_branch);
            format!("if {cond_s}:\n    {then_s}\nelse:\n    {else_s}")
        }
        ExprNode::BoolOp { op, left, right, .. } => {
            use crate::expr::BoolOpKind;
            let op_s = match op {
                BoolOpKind::Or => "or",
                BoolOpKind::And => "and",
            };
            format!("{} {op_s} {}", emit_expr(left), emit_expr(right))
        }
        ExprNode::Array { elements, .. } => {
            let parts: Vec<String> = elements.iter().map(emit_expr).collect();
            format!("[{}]", parts.join(", "))
        }
        ExprNode::Hash { entries, .. } => {
            let parts: Vec<String> = entries
                .iter()
                .map(|(k, v)| {
                    // Symbol keys become string keys in Python dicts
                    // (since Python has no atom type). Rails-style
                    // `{foo: 1}` → `{"foo": 1}`.
                    if let ExprNode::Lit { value: Literal::Sym { value } } = &*k.node {
                        format!("{:?}: {}", value.as_str(), emit_expr(v))
                    } else {
                        format!("{}: {}", emit_expr(k), emit_expr(v))
                    }
                })
                .collect();
            format!("{{{}}}", parts.join(", "))
        }
        ExprNode::StringInterp { parts } => {
            // Python f-string — `f"text {expr} more"`. Reserved chars
            // inside the `{}` are rare for the Ruby expressions we
            // ingest, so no escape bookkeeping yet; if a fixture
            // triggers one we add it then.
            use crate::expr::InterpPart;
            let mut out = String::from("f\"");
            for p in parts {
                match p {
                    InterpPart::Text { value } => {
                        for c in value.chars() {
                            match c {
                                '"' => out.push_str("\\\""),
                                '\\' => out.push_str("\\\\"),
                                '{' => out.push_str("{{"),
                                '}' => out.push_str("}}"),
                                '\n' => out.push_str("\\n"),
                                other => out.push(other),
                            }
                        }
                    }
                    InterpPart::Expr { expr } => {
                        out.push('{');
                        out.push_str(&emit_expr(expr));
                        out.push('}');
                    }
                }
            }
            out.push('"');
            out
        }
        other => format!("# TODO: emit {:?}", std::mem::discriminant(other)),
    }
}

fn emit_send(recv: Option<&Expr>, method: &str, args: &[Expr]) -> String {
    let args_s: Vec<String> = args.iter().map(emit_expr).collect();
    if method == "[]" && recv.is_some() {
        return format!("{}[{}]", emit_expr(recv.unwrap()), args_s.join(", "));
    }
    match recv {
        None => {
            if args_s.is_empty() {
                method.to_string()
            } else {
                format!("{}({})", method, args_s.join(", "))
            }
        }
        Some(r) => {
            let recv_s = emit_expr(r);
            if args_s.is_empty() {
                // Bare `recv.method` (no parens) is how Python accesses
                // attributes. For method calls with no args we emit
                // `recv.method()` — matches Python idiom and avoids
                // confusing a 0-arity call with an attribute read.
                format!("{recv_s}.{method}()")
            } else {
                format!("{recv_s}.{method}({})", args_s.join(", "))
            }
        }
    }
}

fn emit_literal(lit: &Literal) -> String {
    match lit {
        Literal::Nil => "None".to_string(),
        Literal::Bool { value } => {
            if *value { "True".to_string() } else { "False".to_string() }
        }
        Literal::Int { value } => value.to_string(),
        Literal::Float { value } => {
            let s = value.to_string();
            if s.contains('.') { s } else { format!("{s}.0") }
        }
        Literal::Str { value } => format!("{value:?}"),
        // Symbols have no direct Python equivalent; emit as string
        // literals. Enum detection would refine this into a typed
        // Enum subclass later.
        Literal::Sym { value } => format!("{:?}", value.as_str()),
    }
}

// Types ----------------------------------------------------------------

pub fn python_ty(ty: &Ty) -> String {
    match ty {
        Ty::Int => "int".to_string(),
        Ty::Float => "float".to_string(),
        Ty::Bool => "bool".to_string(),
        Ty::Str | Ty::Sym => "str".to_string(),
        Ty::Nil => "None".to_string(),
        Ty::Array { elem } => format!("list[{}]", python_ty(elem)),
        Ty::Hash { key, value } => format!("dict[{}, {}]", python_ty(key), python_ty(value)),
        Ty::Tuple { elems } => {
            let parts: Vec<String> = elems.iter().map(python_ty).collect();
            format!("tuple[{}]", parts.join(", "))
        }
        Ty::Record { .. } => "dict[str, object]".to_string(),
        Ty::Union { variants } => {
            // PEP 604 union syntax: `A | B | C`. Python 3.10+.
            let parts: Vec<String> = variants.iter().map(python_ty).collect();
            parts.join(" | ")
        }
        Ty::Class { id, .. } => match id.0.as_str() {
            "Time" => "str".to_string(),
            other => other.to_string(),
        },
        Ty::Fn { .. } => "object".to_string(),
        Ty::Var { .. } => "object".to_string(),
    }
}

// Fixtures + tests ---------------------------------------------------

fn emit_py_fixtures(app: &App) -> EmittedFile {
    let lowered = crate::lower::lower_fixtures(app);
    let mut s = String::new();
    writeln!(s, "# Generated by Roundhouse.").unwrap();
    writeln!(s, "from __future__ import annotations").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "from app import db").unwrap();
    writeln!(s, "from app.schema_sql import CREATE_TABLES").unwrap();
    writeln!(s, "from app.models import {}", app
        .models
        .iter()
        .map(|m| m.name.0.as_str().to_string())
        .collect::<Vec<_>>()
        .join(", ")).unwrap();

    writeln!(s).unwrap();
    writeln!(s, "FIXTURE_IDS: dict[tuple[str, str], int] = {{}}").unwrap();

    // Fixtures.setup — called from each TestCase's setUp. Opens a
    // fresh :memory: DB, runs schema DDL, clears the fixture id
    // map, and reloads all fixtures in declaration order.
    writeln!(s).unwrap();
    writeln!(s, "def setup() -> None:").unwrap();
    writeln!(s, "    db.setup_test_db(CREATE_TABLES)").unwrap();
    writeln!(s, "    FIXTURE_IDS.clear()").unwrap();
    for f in &lowered.fixtures {
        let ns = f.name.as_str();
        writeln!(s, "    {ns}_load_all()").unwrap();
    }

    writeln!(s).unwrap();
    writeln!(s, "def fixture_id(fixture: str, label: str) -> int:").unwrap();
    writeln!(s, "    key = (fixture, label)").unwrap();
    writeln!(s, "    if key not in FIXTURE_IDS:").unwrap();
    writeln!(s, "        raise RuntimeError(f\"fixture {{fixture}}/{{label}} not loaded\")").unwrap();
    writeln!(s, "    return FIXTURE_IDS[key]").unwrap();

    for fixture in &lowered.fixtures {
        let class_name = fixture.class.0.as_str();
        let ns = fixture.name.as_str();

        // <ns>_load_all — insert each record via Class.save (which
        // runs validate + INSERT) then capture the assigned id via
        // the mutated self.id attribute.
        writeln!(s).unwrap();
        writeln!(s, "def {ns}_load_all() -> None:").unwrap();
        for record in &fixture.records {
            let label = record.label.as_str();
            let kwargs: Vec<String> = record
                .fields
                .iter()
                .map(|f| {
                    let col = f.column.as_str();
                    let val = match &f.value {
                        crate::lower::LoweredFixtureValue::Literal { ty, raw } => {
                            py_literal_for(raw, ty)
                        }
                        crate::lower::LoweredFixtureValue::FkLookup {
                            target_fixture,
                            target_label,
                        } => format!(
                            "fixture_id({:?}, {:?})",
                            target_fixture.as_str(),
                            target_label.as_str(),
                        ),
                    };
                    format!("        {col}={val}")
                })
                .collect();
            writeln!(s, "    r = {class_name}(").unwrap();
            writeln!(s, "{}", kwargs.join(",\n")).unwrap();
            writeln!(s, "    )").unwrap();
            writeln!(
                s,
                "    if not r.save():\n        raise RuntimeError(\"fixture {ns}/{label} failed to save\")"
            )
            .unwrap();
            writeln!(
                s,
                "    FIXTURE_IDS[({ns:?}, {label:?})] = r.id",
            )
            .unwrap();
        }

        // Named-record getters — lookup id + Class.find.
        for record in &fixture.records {
            let label = record.label.as_str();
            writeln!(s).unwrap();
            writeln!(s, "def {ns}_{label}() -> {class_name}:").unwrap();
            writeln!(s, "    id = fixture_id({ns:?}, {label:?})").unwrap();
            writeln!(s, "    record = {class_name}.find(id)").unwrap();
            writeln!(
                s,
                "    if record is None:\n        raise RuntimeError(\"fixture {ns}/{label} not loaded\")"
            )
            .unwrap();
            writeln!(s, "    return record").unwrap();
        }
    }

    EmittedFile {
        path: PathBuf::from("tests/fixtures.py"),
        content: s,
    }
}

fn py_literal_for(value: &str, ty: &Ty) -> String {
    match ty {
        Ty::Str | Ty::Sym => format!("{value:?}"),
        Ty::Int => {
            if value.parse::<i64>().is_ok() {
                value.to_string()
            } else {
                format!("0  # TODO: coerce {value:?}")
            }
        }
        Ty::Float => {
            if value.parse::<f64>().is_ok() {
                value.to_string()
            } else {
                format!("0.0  # TODO: coerce {value:?}")
            }
        }
        Ty::Bool => match value {
            "true" | "1" => "True".into(),
            "false" | "0" => "False".into(),
            _ => format!("False  # TODO: coerce {value:?}"),
        },
        Ty::Class { id, .. } if id.0.as_str() == "Time" => format!("{value:?}"),
        _ => format!("{value:?}"),
    }
}

fn emit_py_test(tm: &TestModule, app: &App) -> EmittedFile {
    let fixture_names: Vec<Symbol> =
        app.fixtures.iter().map(|f| f.name.clone()).collect();
    let known_models: Vec<Symbol> =
        app.models.iter().map(|m| m.name.0.clone()).collect();
    let mut attrs_set: std::collections::BTreeSet<Symbol> =
        std::collections::BTreeSet::new();
    for m in &app.models {
        for attr in m.attributes.fields.keys() {
            attrs_set.insert(attr.clone());
        }
    }
    let model_attrs: Vec<Symbol> = attrs_set.into_iter().collect();

    let ctx = PyTestCtx {
        app,
        fixture_names: &fixture_names,
        known_models: &known_models,
        model_attrs: &model_attrs,
    };

    let is_controller_test = tm.name.0.as_str().ends_with("ControllerTest");

    let mut s = String::new();
    writeln!(s, "# Generated by Roundhouse.").unwrap();
    writeln!(s, "from __future__ import annotations").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "import unittest").unwrap();
    writeln!(s).unwrap();
    if is_controller_test {
        // Side-effect import: app.routes registers handlers on the
        // Router at module load. Needed before any dispatch.
        writeln!(s, "import app.routes  # noqa: F401").unwrap();
        writeln!(s, "from app.test_support import TestClient").unwrap();
        writeln!(s, "from app.route_helpers import *  # noqa: F401, F403").unwrap();
    }
    // Import every model so Class.new struct-literals resolve, plus all
    // fixture accessors.
    if !known_models.is_empty() {
        writeln!(
            s,
            "from app.models import {}",
            known_models
                .iter()
                .map(|m| m.as_str().to_string())
                .collect::<Vec<_>>()
                .join(", ")
        )
        .unwrap();
    }
    if !app.fixtures.is_empty() {
        writeln!(s, "from tests import fixtures").unwrap();
    }

    writeln!(s).unwrap();
    writeln!(s, "class {}(unittest.TestCase):", tm.name.0.as_str()).unwrap();

    if !app.fixtures.is_empty() {
        // setUp runs before each test method — opens a fresh :memory:
        // DB and reloads all fixtures, matching Rails' transactional-
        // fixture isolation.
        writeln!(s).unwrap();
        writeln!(s, "    def setUp(self):").unwrap();
        writeln!(s, "        fixtures.setup()").unwrap();
    }

    for test in &tm.tests {
        let fn_name = format!("test_{}", test_name_snake_py(&test.name));
        writeln!(s).unwrap();
        if is_controller_test {
            writeln!(s, "    def {fn_name}(self):").unwrap();
            let body = emit_py_controller_test_body(test, app, &ctx);
            if body.is_empty() {
                writeln!(s, "        pass").unwrap();
            } else {
                for line in body.lines() {
                    writeln!(s, "        {line}").unwrap();
                }
            }
        } else if test_needs_runtime_unsupported_py(test) {
            writeln!(
                s,
                "    @unittest.skip(\"Phase 3: needs persistence runtime\")"
            )
            .unwrap();
            writeln!(s, "    def {fn_name}(self):").unwrap();
            writeln!(s, "        pass").unwrap();
        } else {
            writeln!(s, "    def {fn_name}(self):").unwrap();
            let body = emit_py_test_body(&test.body, ctx);
            if body.is_empty() {
                writeln!(s, "        pass").unwrap();
            } else {
                for line in body.lines() {
                    writeln!(s, "        {line}").unwrap();
                }
            }
        }
    }

    // unittest discover default pattern is `test_*.py`; convert
    // `ArticleTest` → `article_test` → `article` → `test_article`.
    let stem = crate::naming::snake_case(tm.name.0.as_str()).replace("_test", "");
    EmittedFile {
        path: PathBuf::from(format!("tests/test_{stem}.py")),
        content: s,
    }
}

/// Emit a single controller test body. Walks the test AST via the
/// shared `test_body_stmts` helper and dispatches each statement
/// through the shared `classify_controller_test_send` classifier
/// plus a Python render table.
fn emit_py_controller_test_body(test: &Test, app: &App, ctx: &PyTestCtx) -> String {
    let mut out = String::new();
    out.push_str("client = TestClient()\n");
    // Prime ivars read without assignment: `@article` → `article = fixtures.articles_one()`.
    let walked = crate::lower::walk_controller_ivars(&test.body);
    for ivar in walked.ivars_read_without_assign() {
        // Match convention: `@article` with fixture prefix `articles`
        // binds to `fixtures.articles_one()`. The fixture_names in ctx
        // are plural fixture names.
        let plural = crate::naming::pluralize_snake(ivar.as_str());
        if ctx.fixture_names.iter().any(|s| s.as_str() == plural) {
            out.push_str(&format!(
                "{} = fixtures.{}_one()\n",
                ivar.as_str(),
                plural,
            ));
        }
    }

    for stmt in crate::lower::test_body_stmts(&test.body) {
        let rendered = emit_py_ctrl_test_stmt(stmt, app, ctx);
        out.push_str(&rendered);
        out.push('\n');
    }
    out
}

fn emit_py_ctrl_test_stmt(stmt: &Expr, app: &App, ctx: &PyTestCtx) -> String {
    match &*stmt.node {
        ExprNode::Send { recv: None, method, args, block, .. } => {
            emit_py_ctrl_test_send(method.as_str(), args, block.as_ref(), app, ctx)
        }
        ExprNode::Send { recv: Some(r), method, args, .. } => {
            if method.as_str() == "reload" {
                let recv_s = match &*r.node {
                    ExprNode::Ivar { name } | ExprNode::Var { name, .. } => name.to_string(),
                    _ => emit_py_ctrl_test_expr(r, app, ctx),
                };
                return format!("{recv_s}.reload()");
            }
            let recv_s = emit_py_ctrl_test_expr(r, app, ctx);
            let args_s: Vec<String> =
                args.iter().map(|a| emit_py_ctrl_test_expr(a, app, ctx)).collect();
            if args_s.is_empty() {
                // Attribute read vs method call — for controller
                // tests we always parens (simpler than tracking).
                format!("{recv_s}.{method}()")
            } else {
                format!("{recv_s}.{method}({})", args_s.join(", "))
            }
        }
        ExprNode::Assign { target: LValue::Var { name, .. }, value } => {
            format!("{name} = {}", emit_py_ctrl_test_expr(value, app, ctx))
        }
        ExprNode::Assign { target: LValue::Ivar { name }, value } => {
            format!("{name} = {}", emit_py_ctrl_test_expr(value, app, ctx))
        }
        _ => emit_py_ctrl_test_expr(stmt, app, ctx),
    }
}

fn emit_py_ctrl_test_send(
    method: &str,
    args: &[Expr],
    block: Option<&Expr>,
    app: &App,
    ctx: &PyTestCtx,
) -> String {
    use crate::lower::{AssertSelectKind, ControllerTestSend};
    match crate::lower::classify_controller_test_send(method, args, block) {
        Some(ControllerTestSend::HttpGet { url }) => {
            let u = emit_py_url_expr(url, app, ctx);
            format!("resp = client.get({u})")
        }
        Some(ControllerTestSend::HttpWrite { method, url, params }) => {
            let u = emit_py_url_expr(url, app, ctx);
            let body = params
                .map(|h| flatten_py_params_to_form(h, None, app, ctx))
                .unwrap_or_else(|| "{}".to_string());
            format!("resp = client.{method}({u}, {body})")
        }
        Some(ControllerTestSend::HttpDelete { url }) => {
            let u = emit_py_url_expr(url, app, ctx);
            format!("resp = client.delete({u})")
        }
        Some(ControllerTestSend::AssertResponse { sym }) => match sym.as_str() {
            "success" => "resp.assert_ok()".to_string(),
            "unprocessable_entity" => "resp.assert_unprocessable()".to_string(),
            other => format!("resp.assert_status(200)  # TODO: {other:?}"),
        },
        Some(ControllerTestSend::AssertRedirectedTo { url }) => {
            let u = emit_py_url_expr(url, app, ctx);
            format!("resp.assert_redirected_to({u})")
        }
        Some(ControllerTestSend::AssertSelect { selector, kind }) => {
            emit_py_assert_select_classified(selector, kind, app, ctx)
        }
        Some(ControllerTestSend::AssertDifference { method: _, count_expr, delta, block }) => {
            emit_py_assert_difference_classified(count_expr, delta, block, app, ctx)
        }
        Some(ControllerTestSend::AssertEqual { expected, actual }) => {
            let e = emit_py_ctrl_test_expr(expected, app, ctx);
            let a = emit_py_ctrl_test_expr(actual, app, ctx);
            format!("self.assertEqual({a}, {e})")
        }
        None => {
            let args_s: Vec<String> =
                args.iter().map(|a| emit_py_ctrl_test_expr(a, app, ctx)).collect();
            if args_s.is_empty() {
                format!("{method}()")
            } else {
                format!("{method}({})", args_s.join(", "))
            }
        }
    }
}

fn emit_py_url_expr(expr: &Expr, app: &App, ctx: &PyTestCtx) -> String {
    use crate::lower::UrlArg;
    let Some(helper) = crate::lower::classify_url_expr(expr) else {
        return emit_py_ctrl_test_expr(expr, app, ctx);
    };
    let helper_name = format!("{}_path", helper.helper_base);
    let args_s: Vec<String> = helper
        .args
        .iter()
        .map(|a| match a {
            UrlArg::IvarOrVarId(name) => format!("{name}.id"),
            UrlArg::ModelLast(class) => format!("{}.last().id", class.as_str()),
            UrlArg::Raw(e) => emit_py_ctrl_test_expr(e, app, ctx),
        })
        .collect();
    format!("{helper_name}({})", args_s.join(", "))
}

fn emit_py_assert_select_classified(
    selector_expr: &Expr,
    kind: crate::lower::AssertSelectKind<'_>,
    app: &App,
    ctx: &PyTestCtx,
) -> String {
    use crate::lower::AssertSelectKind;
    let ExprNode::Lit { value: Literal::Str { value: selector } } =
        &*selector_expr.node
    else {
        return format!(
            "resp.assert_select({})  # TODO: dynamic selector",
            emit_py_ctrl_test_expr(selector_expr, app, ctx),
        );
    };
    match kind {
        AssertSelectKind::Text(expr) => {
            let text = emit_py_ctrl_test_expr(expr, app, ctx);
            format!("resp.assert_select_text({selector:?}, {text})")
        }
        AssertSelectKind::Minimum(expr) => {
            let n = emit_py_ctrl_test_expr(expr, app, ctx);
            format!("resp.assert_select_min({selector:?}, {n})")
        }
        AssertSelectKind::SelectorBlock(b) => {
            let mut out = String::new();
            out.push_str(&format!("resp.assert_select({selector:?})\n"));
            let inner_body = match &*b.node {
                ExprNode::Lambda { body, .. } => body,
                _ => b,
            };
            for stmt in crate::lower::test_body_stmts(inner_body) {
                out.push_str(&emit_py_ctrl_test_stmt(stmt, app, ctx));
                out.push('\n');
            }
            out.trim_end().to_string()
        }
        AssertSelectKind::SelectorOnly => {
            format!("resp.assert_select({selector:?})")
        }
    }
}

fn emit_py_assert_difference_classified(
    count_expr_str: String,
    expected_delta: i64,
    block: Option<&Expr>,
    app: &App,
    ctx: &PyTestCtx,
) -> String {
    // `Article.count` → `Article.count()` in Python.
    let count_expr = count_expr_str
        .split_once('.')
        .map(|(cls, m)| format!("{cls}.{m}()"))
        .unwrap_or_else(|| count_expr_str.clone());

    let mut out = String::new();
    out.push_str(&format!("_before = {count_expr}\n"));
    if let Some(b) = block {
        let inner_body = match &*b.node {
            ExprNode::Lambda { body, .. } => body,
            _ => b,
        };
        for stmt in crate::lower::test_body_stmts(inner_body) {
            out.push_str(&emit_py_ctrl_test_stmt(stmt, app, ctx));
            out.push('\n');
        }
    }
    out.push_str(&format!("_after = {count_expr}\n"));
    out.push_str(&format!("self.assertEqual(_after - _before, {expected_delta})"));
    out
}

fn emit_py_ctrl_test_expr(expr: &Expr, app: &App, ctx: &PyTestCtx) -> String {
    match &*expr.node {
        ExprNode::Lit { value } => emit_literal(value),
        ExprNode::Ivar { name } => name.to_string(),
        ExprNode::Var { name, .. } => name.to_string(),
        ExprNode::Const { path } => {
            path.iter().map(|s| s.to_string()).collect::<Vec<_>>().join(".")
        }
        ExprNode::Send { recv: Some(r), method, args, .. } => {
            let m = method.as_str();
            if m == "last" && args.is_empty() {
                if let ExprNode::Const { path } = &*r.node {
                    let class = path.last().map(|s| s.as_str().to_string()).unwrap_or_default();
                    return format!("{class}.last()");
                }
            }
            if m == "count" && args.is_empty() {
                if let ExprNode::Const { path } = &*r.node {
                    let class = path.last().map(|s| s.as_str().to_string()).unwrap_or_default();
                    return format!("{class}.count()");
                }
            }
            if args.is_empty() {
                // Attribute access for ivar.attr / var.attr shapes;
                // method call otherwise. Python has no null-coalescing
                // the way TS does, so use attribute by default.
                let recv_s = match &*r.node {
                    ExprNode::Ivar { name } | ExprNode::Var { name, .. } => name.to_string(),
                    _ => emit_py_ctrl_test_expr(r, app, ctx),
                };
                return format!("{recv_s}.{m}");
            }
            let recv_s = emit_py_ctrl_test_expr(r, app, ctx);
            let args_s: Vec<String> =
                args.iter().map(|a| emit_py_ctrl_test_expr(a, app, ctx)).collect();
            format!("{recv_s}.{m}({})", args_s.join(", "))
        }
        ExprNode::Send { recv: None, method, args, .. } => {
            if method.as_str().ends_with("_url") || method.as_str().ends_with("_path") {
                return emit_py_url_expr(expr, app, ctx);
            }
            let args_s: Vec<String> =
                args.iter().map(|a| emit_py_ctrl_test_expr(a, app, ctx)).collect();
            if args_s.is_empty() {
                method.to_string()
            } else {
                format!("{method}({})", args_s.join(", "))
            }
        }
        _ => format!("None  # TODO expr"),
    }
}

/// Flatten `{ article: { title: "X", body: "Y" } }` into a Python
/// dict literal `{ "article[title]": "X", "article[body]": "Y" }`
/// — matching the TestClient's form-body shape.
fn flatten_py_params_to_form(
    expr: &Expr,
    scope: Option<&str>,
    app: &App,
    ctx: &PyTestCtx,
) -> String {
    let pairs: Vec<String> = crate::lower::flatten_params_pairs(expr, scope)
        .into_iter()
        .map(|(key, value)| {
            let val = emit_py_ctrl_test_expr(value, app, ctx);
            format!("{key:?}: str({val})")
        })
        .collect();
    format!("{{{}}}", pairs.join(", "))
}

fn test_name_snake_py(desc: &str) -> String {
    let mut s: String = desc
        .chars()
        .map(|c| if c.is_alphanumeric() { c.to_ascii_lowercase() } else { '_' })
        .collect();
    while s.contains("__") {
        s = s.replace("__", "_");
    }
    s.trim_matches('_').to_string()
}

#[derive(Clone, Copy)]
struct PyTestCtx<'a> {
    app: &'a App,
    fixture_names: &'a [Symbol],
    known_models: &'a [Symbol],
    model_attrs: &'a [Symbol],
}

fn emit_py_test_body(body: &Expr, ctx: PyTestCtx) -> String {
    match &*body.node {
        ExprNode::Seq { exprs } if !exprs.is_empty() => exprs
            .iter()
            .map(|e| emit_py_test_stmt(e, ctx))
            .collect::<Vec<_>>()
            .join("\n"),
        _ => emit_py_test_stmt(body, ctx),
    }
}

fn emit_py_test_stmt(e: &Expr, ctx: PyTestCtx) -> String {
    match &*e.node {
        ExprNode::Assign { target: LValue::Var { name, .. }, value } => {
            format!("{} = {}", name, emit_py_test_expr(value, ctx))
        }
        _ => emit_py_test_expr(e, ctx),
    }
}

fn emit_py_test_expr(e: &Expr, ctx: PyTestCtx) -> String {
    match &*e.node {
        ExprNode::Lit { value } => emit_literal(value),
        ExprNode::Var { name, .. } => name.to_string(),
        ExprNode::Const { path } => path
            .iter()
            .map(|s| s.to_string())
            .collect::<Vec<_>>()
            .join("."),
        ExprNode::Send { recv, method, args, block, .. } => {
            emit_py_test_send(recv.as_ref(), method.as_str(), args, block.as_ref(), ctx)
        }
        ExprNode::BoolOp { op, left, right, .. } => {
            use crate::expr::BoolOpKind;
            let op_s = match op {
                BoolOpKind::Or => "or",
                BoolOpKind::And => "and",
            };
            format!(
                "{} {op_s} {}",
                emit_py_test_expr(left, ctx),
                emit_py_test_expr(right, ctx)
            )
        }
        _ => format!("# TODO: Python test emit for {:?}", std::mem::discriminant(&*e.node)),
    }
}

fn emit_py_test_send(
    recv: Option<&Expr>,
    method: &str,
    args: &[Expr],
    block: Option<&Expr>,
    ctx: PyTestCtx,
) -> String {
    let args_s: Vec<String> = args.iter().map(|a| emit_py_test_expr(a, ctx)).collect();

    // Fixture accessor: articles(:one) → fixtures.articles_one()
    if recv.is_none()
        && args.len() == 1
        && ctx.fixture_names.iter().any(|s| s.as_str() == method)
    {
        if let ExprNode::Lit { value: Literal::Sym { value: sym } } = &*args[0].node {
            return format!("fixtures.{}_{}()", method, sym.as_str());
        }
    }

    // assert_difference("Class.count", delta) do ... end → inline
    // before/after capture + self.assertEqual on the delta.
    // Callers see this as a bare expression; we need statement form,
    // so the Stage layer (emit_py_test_stmt) handles newline wrapping.
    if recv.is_none() && method == "assert_difference" {
        if let Some(body) = block {
            if let Some(count_expr) = args
                .first()
                .and_then(|a| match &*a.node {
                    ExprNode::Lit { value: Literal::Str { value } } => {
                        rewrite_ruby_dot_call_py(value)
                    }
                    _ => None,
                })
            {
                let delta = args_s.get(1).cloned().unwrap_or_else(|| "1".into());
                let body_s = emit_block_body_py(body, ctx);
                return format!(
                    "_before = {count_expr}\n{body_s}\n_after = {count_expr}\nself.assertEqual(_after - _before, {delta})"
                );
            }
        }
    }

    // owner.<assoc>.create(hash) / .build(hash) — HasMany rewrite.
    // Python: a lambda-call with a walrus-ish shape won't work
    // cleanly, so we use a helper module-level function wouldn't
    // either. Easiest: expand to a small helper-less form. We can
    // construct Class(article_id=..., commenter=..., body=...) and
    // call save on the result. Since this sits in expression
    // position (assign rhs), wrap in a `(lambda: ...)()` one-shot.
    if (method == "create" || method == "build") && args.len() == 1 {
        if let Some(outer_recv) = recv {
            if let ExprNode::Send {
                recv: Some(assoc_recv),
                method: assoc_method,
                args: inner_args,
                ..
            } = &*outer_recv.node
            {
                if inner_args.is_empty() {
                    if let Some(s) = try_emit_assoc_create_py(
                        assoc_recv,
                        assoc_method.as_str(),
                        args,
                        method,
                        ctx,
                    ) {
                        return s;
                    }
                }
            }
        }
    }

    // Assertion macros → unittest's TestCase methods.
    if recv.is_none() {
        match (method, args_s.len()) {
            ("assert_equal", 2) => {
                return format!(
                    "self.assertEqual({}, {})",
                    args_s[1], args_s[0]
                );
            }
            ("assert_not_equal", 2) => {
                return format!(
                    "self.assertNotEqual({}, {})",
                    args_s[1], args_s[0]
                );
            }
            ("assert_not", 1) => return format!("self.assertFalse({})", args_s[0]),
            ("assert", 1) => return format!("self.assertTrue({})", args_s[0]),
            ("assert_nil", 1) => return format!("self.assertIsNone({})", args_s[0]),
            ("assert_not_nil", 1) => {
                return format!("self.assertIsNotNone({})", args_s[0])
            }
            _ => {}
        }
    }

    // `Class.new(hash)` → `Class(**{...})` or `Class(field=val, ...)`
    if let Some(r) = recv {
        if method == "new" && args.len() == 1 {
            if let ExprNode::Const { path } = &*r.node {
                if let Some(class_name) = path.last() {
                    if ctx.known_models.iter().any(|s| s == class_name) {
                        if let ExprNode::Hash { entries, .. } = &*args[0].node {
                            let pairs: Vec<String> = entries
                                .iter()
                                .filter_map(|(k, v)| {
                                    if let ExprNode::Lit {
                                        value: Literal::Sym { value: f },
                                    } = &*k.node
                                    {
                                        Some(format!(
                                            "{}={}",
                                            f.as_str(),
                                            emit_py_test_expr(v, ctx)
                                        ))
                                    } else {
                                        None
                                    }
                                })
                                .collect();
                            return format!("{class_name}({})", pairs.join(", "));
                        }
                    }
                }
            }
        }
    }

    match recv {
        None => {
            if args_s.is_empty() {
                method.to_string()
            } else {
                format!("{}({})", method, args_s.join(", "))
            }
        }
        Some(r) => {
            let recv_s = emit_py_test_expr(r, ctx);
            // Attribute read (no parens) vs method call.
            let is_attr = args_s.is_empty()
                && ctx.model_attrs.iter().any(|s| s.as_str() == method);
            if is_attr {
                format!("{recv_s}.{method}")
            } else if args_s.is_empty() {
                format!("{recv_s}.{method}()")
            } else {
                format!("{recv_s}.{method}({})", args_s.join(", "))
            }
        }
    }
}

