use std::collections::BTreeSet;
use std::fmt::Write;
use std::path::PathBuf;

use crate::App;
use crate::dialect::RouteSpec;
use crate::emit::EmittedFile;
use crate::naming::camelize;

pub fn emit_routes(app: &App) -> EmittedFile {
    let mut out = String::new();
    writeln!(out, "// Generated by Roundhouse.").unwrap();
    writeln!(out, "import {{ Router }} from \"juntos\";").unwrap();

    let controllers = collect_controllers(&app.routes.entries);
    for c in &controllers {
        let snake = crate::naming::snake_case(c);
        writeln!(
            out,
            "import {{ {c} }} from \"../app/controllers/{snake}.js\";",
        )
        .unwrap();
    }
    writeln!(out).unwrap();

    for entry in &app.routes.entries {
        emit_one_route(&mut out, entry);
    }

    EmittedFile { path: PathBuf::from("src/routes.ts"), content: out }
}

fn collect_controllers(entries: &[RouteSpec]) -> Vec<String> {
    let mut set: BTreeSet<String> = BTreeSet::new();
    for entry in entries {
        walk_controllers(entry, &mut set);
    }
    set.into_iter().collect()
}

fn walk_controllers(spec: &RouteSpec, out: &mut BTreeSet<String>) {
    match spec {
        RouteSpec::Root { target } => {
            let controller = target.split_once('#').map(|(c, _)| c).unwrap_or(target);
            out.insert(format!("{}Controller", camelize(controller)));
        }
        RouteSpec::Resources { name, nested, .. } => {
            out.insert(format!("{}Controller", camelize(name.as_str())));
            for child in nested {
                walk_controllers(child, out);
            }
        }
        RouteSpec::Explicit { controller, .. } => {
            out.insert(controller.0.as_str().to_string());
        }
    }
}

fn emit_one_route(out: &mut String, spec: &RouteSpec) {
    match spec {
        RouteSpec::Root { target } => {
            let (controller, action) = target
                .split_once('#')
                .unwrap_or((target.as_str(), "index"));
            writeln!(
                out,
                "Router.root(\"/\", {}Controller, \"{action}\");",
                camelize(controller)
            )
            .unwrap();
        }
        RouteSpec::Resources { name, only, except, nested } => {
            let controller = format!("{}Controller", camelize(name.as_str()));
            let opts = render_resource_opts(only, except, nested);
            if opts.is_empty() {
                writeln!(out, "Router.resources(\"{name}\", {controller});").unwrap();
            } else {
                writeln!(out, "Router.resources(\"{name}\", {controller}, {opts});").unwrap();
            }
        }
        RouteSpec::Explicit { method, path, controller, action, .. } => {
            let verb = http_verb_lower(method);
            writeln!(
                out,
                "Router.{verb}(\"{path}\", {controller}, \"{action}\");",
                controller = controller.0,
            )
            .unwrap();
        }
    }
}

fn render_resource_opts(
    only: &[crate::ident::Symbol],
    except: &[crate::ident::Symbol],
    nested: &[RouteSpec],
) -> String {
    let mut parts: Vec<String> = Vec::new();
    if !only.is_empty() {
        parts.push(format!("only: [{}]", quote_syms(only)));
    }
    if !except.is_empty() {
        parts.push(format!("except: [{}]", quote_syms(except)));
    }
    if !nested.is_empty() {
        let nested_entries: Vec<String> =
            nested.iter().map(render_nested_entry).collect();
        parts.push(format!("nested: [{}]", nested_entries.join(", ")));
    }
    if parts.is_empty() {
        String::new()
    } else {
        format!("{{{}}}", parts.join(", "))
    }
}

fn render_nested_entry(spec: &RouteSpec) -> String {
    match spec {
        RouteSpec::Resources { name, only, except, nested } => {
            let controller = format!("{}Controller", camelize(name.as_str()));
            let mut parts =
                vec![format!("name: \"{name}\""), format!("controller: {controller}")];
            if !only.is_empty() {
                parts.push(format!("only: [{}]", quote_syms(only)));
            }
            if !except.is_empty() {
                parts.push(format!("except: [{}]", quote_syms(except)));
            }
            if !nested.is_empty() {
                let inner: Vec<String> = nested.iter().map(render_nested_entry).collect();
                parts.push(format!("nested: [{}]", inner.join(", ")));
            }
            format!("{{{}}}", parts.join(", "))
        }
        _ => String::new(),
    }
}

fn quote_syms(syms: &[crate::ident::Symbol]) -> String {
    syms.iter()
        .map(|s| format!("\"{}\"", s.as_str()))
        .collect::<Vec<_>>()
        .join(", ")
}

fn http_verb_lower(m: &crate::dialect::HttpMethod) -> &'static str {
    use crate::dialect::HttpMethod::*;
    match m {
        Get => "get",
        Post => "post",
        Put => "put",
        Patch => "patch",
        Delete => "delete",
        Head => "head",
        Options => "options",
        Any => "any",
    }
}
