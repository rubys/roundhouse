//! Library-shape emission — kind-agnostic LibraryClass walker.
//!
//! After Phase B of the TS rewrite, this is the *only* code path that
//! turns an IR class into a TS file. All inputs (lowered models,
//! lowered views, lowered controllers, `app.library_classes`) reach
//! the emitter as `LibraryClass` and flow through `emit_class_file`.
//! No per-output-kind machinery: schema/routes/importmap are expected
//! to land here once their lowerers exist.
//!
//! `emit_class_file` does two things: collect imports (juntos exports
//! + sibling models / library_classes referenced in method bodies)
//! and delegate to `super::emit_library_class` for the class body.
//! That body renderer (in `typescript.rs`) handles attr_reader/writer
//! collapse, identifier sanitization, super translation, and
//! Ty-signature-driven param + return types.

