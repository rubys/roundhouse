//! Library-shape emission — for transpiled-shape input where model
//! bodies already contain explicit getters/setters/lifecycle hooks.
//!
//! Complements the Rails-DSL-shape emission in `model.rs`: that path
//! expects class-level DSL calls (has_many, validates, broadcasts_to)
//! and generates accessors/validations from schema metadata. This
//! path takes the classes as written — method bodies are emitted
//! directly, and `def x; @attributes[:x]; end` + `def x=(v); ...; end`
//! pairs are recognized and emitted as TS `get/set` accessor pairs.

use std::collections::HashMap;
use std::fmt::Write;
use std::path::PathBuf;

use super::super::EmittedFile;
use super::expr::{emit_body, emit_expr};
use super::ty::ts_ty;
use crate::App;
use crate::dialect::{MethodDef, MethodReceiver, Model};
use crate::expr::{Expr, ExprNode, InterpPart, LValue, RescueClause};
use crate::span::Span;
use crate::ident::Symbol;
use crate::ty::Ty;

pub(super) fn emit_library_classes(app: &App) -> Vec<EmittedFile> {
    app.models.iter().map(emit_library_class).collect()
}

fn emit_library_class(model: &Model) -> EmittedFile {
    let name = model.name.0.as_str();
    let file_stem = crate::naming::snake_case(name);
    let mut s = String::new();

    writeln!(s, "// Generated by Roundhouse (library shape).").unwrap();

    let parent = model
        .parent
        .as_ref()
        .map(|p| {
            let raw = p.0.as_str();
            // Ruby's `ActiveRecord::Base` maps to Juntos's `ActiveRecord`.
            // Other qualified names flatten to their last segment (Ruby's
            // `Foo::Bar` becomes TS's `Bar` after import).
            if raw == "ActiveRecord::Base" {
                "ActiveRecord".to_string()
            } else if let Some(last) = raw.rsplit("::").next() {
                last.to_string()
            } else {
                raw.to_string()
            }
        })
        .unwrap_or_else(|| "ApplicationRecord".to_string());

    // Imports. Always import the parent. Include Juntos-provided helpers
    // that transpiled-shape models commonly reference (CollectionProxy,
    // modelRegistry). Cross-model references (Article <-> Comment)
    // surface in method bodies; a more careful scan can narrow this
    // later — for now pull in sibling models unconditionally.
    let mut juntos_imports: Vec<&str> = vec![parent.as_str(), "CollectionProxy", "modelRegistry"];
    juntos_imports.dedup();
    writeln!(
        s,
        "import {{ {} }} from \"juntos\";",
        juntos_imports.join(", ")
    )
    .unwrap();
    writeln!(s).unwrap();

    writeln!(s, "export class {name} extends {parent} {{").unwrap();

    // Pair up getter/setter methods. A pair has a base method `x` and a
    // setter method `x=`. Both are emitted as `get x() / set x(v)` in
    // TS. Unpaired methods (no `=` counterpart) emit as plain methods.
    let methods: Vec<&MethodDef> = model.methods().collect();
    let mut setters: HashMap<String, &MethodDef> = HashMap::new();
    for m in &methods {
        let n = m.name.as_str();
        if let Some(base) = n.strip_suffix('=') {
            setters.insert(base.to_string(), m);
        }
    }

    let mut first = true;
    for m in &methods {
        let n = m.name.as_str();
        if n.ends_with('=') {
            // Setters are paired with their getters below; skip the
            // standalone pass.
            continue;
        }
        if !first {
            writeln!(s).unwrap();
        }
        first = false;
        if let Some(setter) = setters.get(n) {
            emit_getter_setter_pair(&mut s, m, setter);
        } else {
            emit_plain_method(&mut s, m);
        }
    }

    writeln!(s, "}}").unwrap();
    writeln!(s).unwrap();
    writeln!(s, "modelRegistry[\"{name}\"] = {name};").unwrap();

    EmittedFile {
        path: PathBuf::from(format!("app/models/{file_stem}.ts")),
        content: s,
    }
}

fn emit_getter_setter_pair(s: &mut String, getter: &MethodDef, setter: &MethodDef) {
    let name = sanitize_identifier(getter.name.as_str());
    let getter_ret = getter.body.ty.clone().unwrap_or(Ty::Nil);
    let ret_s = ts_ty(&getter_ret);

    let getter_body = rewrite_for_class_method(&getter.body, getter.name.as_str());
    writeln!(s, "  get {name}(): {ret_s} {{").unwrap();
    let body = emit_body(&getter_body, &getter_ret);
    for line in body.lines() {
        writeln!(s, "    {line}").unwrap();
    }
    writeln!(s, "  }}").unwrap();
    writeln!(s).unwrap();

    // Use the setter's declared parameter name so it matches the
    // references in the method body (Ruby `def x=(v); ...; v; end`
    // expects `v` in both the signature and the body).
    let param_name = setter
        .params
        .first()
        .map(|p| p.as_str().to_string())
        .unwrap_or_else(|| "value".to_string());
    writeln!(s, "  set {name}({param_name}: {ret_s}) {{").unwrap();
    let setter_body = rewrite_for_class_method(&setter.body, setter.name.as_str());
    let body = emit_body(&setter_body, &Ty::Nil);
    for line in body.lines() {
        writeln!(s, "    {line}").unwrap();
    }
    writeln!(s, "  }}").unwrap();
}

fn emit_plain_method(s: &mut String, m: &MethodDef) {
    let name = sanitize_identifier(m.name.as_str());
    let ret_ty = m.body.ty.clone().unwrap_or(Ty::Nil);
    let ret_s = ts_ty(&ret_ty);
    let is_static = matches!(m.receiver, MethodReceiver::Class);
    let static_prefix = if is_static { "static " } else { "" };

    let params: Vec<String> = m
        .params
        .iter()
        .map(|p| format!("{}: unknown", p.as_str()))
        .collect();
    writeln!(
        s,
        "  {static_prefix}{name}({}): {ret_s} {{",
        params.join(", ")
    )
    .unwrap();
    let method_body = rewrite_for_class_method(&m.body, m.name.as_str());
    let body = emit_body(&method_body, &ret_ty);
    for line in body.lines() {
        writeln!(s, "    {line}").unwrap();
    }
    writeln!(s, "  }}").unwrap();
}

/// Ruby allows `?` and `!` suffixes on method names; TS does not.
/// Strip them for the output identifier. Juntos's runtime drops them
/// too (`Article.exists(id)` not `Article.exists?(id)`).
fn sanitize_identifier(ruby_name: &str) -> String {
    ruby_name
        .trim_end_matches('?')
        .trim_end_matches('!')
        .to_string()
}

/// IR transformation applied to each class method body before emission.
/// Two pieces:
///  1. `Send { recv: None, ... }` (bare method call / implicit self)
///     gets `SelfRef` as an explicit receiver, so the TS output
///     becomes `this.method(...)` instead of a dangling `method(...)`.
///  2. `Super { args }` becomes a Send on a synthetic `super` receiver
///     with the enclosing method's name. TS needs
///     `super.methodName(args)`, not bare `super(...)`.
///
/// `enclosing_method` is the Ruby method name (may end in `=` for
/// setters). The `=` is dropped for the super-call target so the
/// method name is a valid TS identifier.
fn rewrite_for_class_method(e: &Expr, enclosing_method: &str) -> Expr {
    let method_for_super = enclosing_method.trim_end_matches('=').to_string();
    rewrite(e, &method_for_super)
}

fn rewrite(e: &Expr, super_method: &str) -> Expr {
    let new_node = match &*e.node {
        ExprNode::Send { recv: None, method, args, block, parenthesized } => ExprNode::Send {
            recv: Some(Expr::new(Span::synthetic(), ExprNode::SelfRef)),
            method: method.clone(),
            args: args.iter().map(|a| rewrite(a, super_method)).collect(),
            block: block.as_ref().map(|b| rewrite(b, super_method)),
            parenthesized: *parenthesized,
        },
        ExprNode::Send { recv: Some(r), method, args, block, parenthesized } => ExprNode::Send {
            recv: Some(rewrite(r, super_method)),
            method: method.clone(),
            args: args.iter().map(|a| rewrite(a, super_method)).collect(),
            block: block.as_ref().map(|b| rewrite(b, super_method)),
            parenthesized: *parenthesized,
        },
        ExprNode::Super { args } => {
            let super_recv = Expr::new(
                Span::synthetic(),
                ExprNode::Const { path: vec![Symbol::from("super")] },
            );
            let args_vec: Vec<Expr> = match args {
                Some(v) => v.iter().map(|a| rewrite(a, super_method)).collect(),
                None => vec![],
            };
            ExprNode::Send {
                recv: Some(super_recv),
                method: Symbol::from(super_method.to_string()),
                args: args_vec,
                block: None,
                parenthesized: true,
            }
        }
        ExprNode::Seq { exprs } => ExprNode::Seq {
            exprs: exprs.iter().map(|c| rewrite(c, super_method)).collect(),
        },
        ExprNode::If { cond, then_branch, else_branch } => ExprNode::If {
            cond: rewrite(cond, super_method),
            then_branch: rewrite(then_branch, super_method),
            else_branch: rewrite(else_branch, super_method),
        },
        ExprNode::BoolOp { op, surface, left, right } => ExprNode::BoolOp {
            op: *op,
            surface: *surface,
            left: rewrite(left, super_method),
            right: rewrite(right, super_method),
        },
        ExprNode::Array { elements, style } => ExprNode::Array {
            elements: elements.iter().map(|c| rewrite(c, super_method)).collect(),
            style: *style,
        },
        ExprNode::Hash { entries, braced } => ExprNode::Hash {
            entries: entries
                .iter()
                .map(|(k, v)| (rewrite(k, super_method), rewrite(v, super_method)))
                .collect(),
            braced: *braced,
        },
        ExprNode::StringInterp { parts } => ExprNode::StringInterp {
            parts: parts
                .iter()
                .map(|p| match p {
                    InterpPart::Text { value } => InterpPart::Text { value: value.clone() },
                    InterpPart::Expr { expr } => InterpPart::Expr {
                        expr: rewrite(expr, super_method),
                    },
                })
                .collect(),
        },
        ExprNode::Lambda { params, block_param, body, block_style } => ExprNode::Lambda {
            params: params.clone(),
            block_param: block_param.clone(),
            body: rewrite(body, super_method),
            block_style: *block_style,
        },
        ExprNode::Apply { fun, args, block } => ExprNode::Apply {
            fun: rewrite(fun, super_method),
            args: args.iter().map(|a| rewrite(a, super_method)).collect(),
            block: block.as_ref().map(|b| rewrite(b, super_method)),
        },
        ExprNode::Assign { target, value } => {
            let new_target = match target {
                LValue::Var { id, name } => LValue::Var { id: *id, name: name.clone() },
                LValue::Ivar { name } => LValue::Ivar { name: name.clone() },
                LValue::Attr { recv, name } => LValue::Attr {
                    recv: rewrite(recv, super_method),
                    name: name.clone(),
                },
                LValue::Index { recv, index } => LValue::Index {
                    recv: rewrite(recv, super_method),
                    index: rewrite(index, super_method),
                },
            };
            ExprNode::Assign {
                target: new_target,
                value: rewrite(value, super_method),
            }
        }
        ExprNode::Yield { args } => ExprNode::Yield {
            args: args.iter().map(|a| rewrite(a, super_method)).collect(),
        },
        ExprNode::Raise { value } => ExprNode::Raise {
            value: rewrite(value, super_method),
        },
        ExprNode::RescueModifier { expr, fallback } => ExprNode::RescueModifier {
            expr: rewrite(expr, super_method),
            fallback: rewrite(fallback, super_method),
        },
        ExprNode::Return { value } => ExprNode::Return {
            value: rewrite(value, super_method),
        },
        ExprNode::BeginRescue { body, rescues, else_branch, ensure, implicit } => {
            ExprNode::BeginRescue {
                body: rewrite(body, super_method),
                rescues: rescues
                    .iter()
                    .map(|rc| RescueClause {
                        classes: rc.classes.iter().map(|c| rewrite(c, super_method)).collect(),
                        binding: rc.binding.clone(),
                        body: rewrite(&rc.body, super_method),
                    })
                    .collect(),
                else_branch: else_branch.as_ref().map(|b| rewrite(b, super_method)),
                ensure: ensure.as_ref().map(|b| rewrite(b, super_method)),
                implicit: *implicit,
            }
        }
        ExprNode::Case { scrutinee, arms } => ExprNode::Case {
            scrutinee: rewrite(scrutinee, super_method),
            arms: arms
                .iter()
                .map(|arm| crate::expr::Arm {
                    pattern: arm.pattern.clone(),
                    guard: arm.guard.as_ref().map(|g| rewrite(g, super_method)),
                    body: rewrite(&arm.body, super_method),
                })
                .collect(),
        },
        ExprNode::Let { id, name, value, body } => ExprNode::Let {
            id: *id,
            name: name.clone(),
            value: rewrite(value, super_method),
            body: rewrite(body, super_method),
        },
        // Leaves pass through unchanged.
        ExprNode::Lit { .. }
        | ExprNode::Var { .. }
        | ExprNode::Ivar { .. }
        | ExprNode::Const { .. }
        | ExprNode::SelfRef => (*e.node).clone(),
    };

    // Silence unused-fn warning on the old passthrough helper.
    let _ = emit_expr as fn(_) -> _;

    Expr {
        span: e.span,
        node: Box::new(new_node),
        ty: e.ty.clone(),
        effects: e.effects.clone(),
        leading_blank_line: e.leading_blank_line,
        diagnostic: e.diagnostic.clone(),
    }
}
