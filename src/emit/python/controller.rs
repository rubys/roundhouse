//! Controllers + actions.
//!
//! Phase 4c: controllers emit as Python classes with one `async def`
//! per action. Action bodies render through the shared `SendKind`
//! classifier from `lower::controller`; the Python render table
//! handles HTTP surface (`http.render` / `redirect_to` / `head`),
//! `respond_to` as a lambda, params access, model / association
//! shapes, and bang stripping (Python forbids `!` in identifiers).
//!
//! Private helpers get a leading-underscore name prefix (`_set_article`)
//! so Python's unused-method warnings stay quiet — `@unittest.skip`-ped
//! controller tests never invoke them at runtime.

