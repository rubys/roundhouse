//! Go emitter smoke test.

use std::path::{Path, PathBuf};

use roundhouse::analyze::Analyzer;
use roundhouse::emit::go;
use roundhouse::ingest::ingest_app;

fn fixture_path() -> &'static Path {
    Path::new("fixtures/tiny-blog")
}

fn analyzed_app() -> roundhouse::App {
    let mut app = ingest_app(fixture_path()).expect("ingest");
    Analyzer::new(&app).analyze(&mut app);
    app
}

#[test]
fn models_file_is_emitted() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let paths: Vec<_> = files.iter().map(|f| f.path.display().to_string()).collect();
    assert!(paths.contains(&"app/models.go".to_string()), "got {paths:?}");
}

#[test]
fn models_output_is_stable() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let models = files
        .iter()
        .find(|f| f.path == PathBuf::from("app/models.go"))
        .unwrap();
    let expected = "\
// Generated by Roundhouse.
package app

import \"strings\"

type Comment struct {
\tID int64
\tBody string
\tPostID int64
}

func (c *Comment) Save() bool {
\tif c.PostID == 0 || PostFind(c.PostID) == nil { return false }
\tdb := Conn()
\tif c.ID == 0 {
\t\tres, err := db.Exec(\"INSERT INTO comments (body, post_id) VALUES (?, ?)\", c.Body, c.PostID)
\t\tif err != nil { panic(err) }
\t\tid, _ := res.LastInsertId()
\t\tc.ID = id
\t} else {
\t\t_, err := db.Exec(\"UPDATE comments SET body = ?, post_id = ? WHERE id = ?\", c.Body, c.PostID, c.ID)
\t\tif err != nil { panic(err) }
\t}
\treturn true
}

func (c *Comment) Destroy() {
\tif _, err := Conn().Exec(\"DELETE FROM comments WHERE id = ?\", c.ID); err != nil { panic(err) }
}

func CommentCount() int64 {
\tvar n int64
\tif err := Conn().QueryRow(\"SELECT COUNT(*) FROM comments\").Scan(&n); err != nil { panic(err) }
\treturn n
}

func CommentFind(id int64) *Comment {
\trow := Conn().QueryRow(\"SELECT id, body, post_id FROM comments WHERE id = ?\", id)
\tvar r Comment
\tif err := row.Scan(&r.ID, &r.Body, &r.PostID); err != nil { return nil }
\treturn &r
}

type Post struct {
\tID int64
\tTitle string
}

func (p *Post) NormalizeTitle() string {
\treturn strings.TrimSpace(p.Title)
}

func (p *Post) Validate() []ValidationError {
\tvar errors []ValidationError
\tif p.Title == \"\" {
\t\terrors = append(errors, NewValidationError(\"title\", \"can't be blank\"))
\t}
\treturn errors
}

func (p *Post) Save() bool {
\tif len(p.Validate()) > 0 { return false }
\tdb := Conn()
\tif p.ID == 0 {
\t\tres, err := db.Exec(\"INSERT INTO posts (title) VALUES (?)\", p.Title)
\t\tif err != nil { panic(err) }
\t\tid, _ := res.LastInsertId()
\t\tp.ID = id
\t} else {
\t\t_, err := db.Exec(\"UPDATE posts SET title = ? WHERE id = ?\", p.Title, p.ID)
\t\tif err != nil { panic(err) }
\t}
\treturn true
}

func (p *Post) Destroy() {
\tif _, err := Conn().Exec(\"DELETE FROM posts WHERE id = ?\", p.ID); err != nil { panic(err) }
}

func PostCount() int64 {
\tvar n int64
\tif err := Conn().QueryRow(\"SELECT COUNT(*) FROM posts\").Scan(&n); err != nil { panic(err) }
\treturn n
}

func PostFind(id int64) *Post {
\trow := Conn().QueryRow(\"SELECT id, title FROM posts WHERE id = ?\", id)
\tvar r Post
\tif err := row.Scan(&r.ID, &r.Title); err != nil { return nil }
\treturn &r
}
";
    assert_eq!(models.content, expected);
}

#[test]
fn controller_file_is_emitted() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let paths: Vec<_> = files.iter().map(|f| f.path.display().to_string()).collect();
    assert!(
        paths.contains(&"app/controllers/posts_controller.go".to_string()),
        "got {paths:?}"
    );
}

#[test]
fn index_action_returns_slice_of_post() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let ctrl = files
        .iter()
        .find(|f| f.path == PathBuf::from("app/controllers/posts_controller.go"))
        .unwrap();
    assert!(
        ctrl.content.contains("func (c *PostsController) Index() []Post"),
        "expected Index returning []Post; got:\n{}",
        ctrl.content
    );
    assert!(
        ctrl.content.contains("return Post.All()"),
        "expected `return Post.All()`; got:\n{}",
        ctrl.content
    );
}

#[test]
fn destroy_action_is_void_with_no_return_type() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let ctrl = files
        .iter()
        .find(|f| f.path == PathBuf::from("app/controllers/posts_controller.go"))
        .unwrap();
    // Void function: no return-type annotation.
    assert!(
        ctrl.content.contains("func (c *PostsController) Destroy() {"),
        "expected void Destroy; got:\n{}",
        ctrl.content
    );
    // Short-var decl for locals + ivars.
    assert!(
        ctrl.content.contains("post := Post.Find(Params[\"id\"])"),
        "expected short-var decl; got:\n{}",
        ctrl.content
    );
    assert!(
        ctrl.content.contains("post.Destroy()"),
        "expected instance call; got:\n{}",
        ctrl.content
    );
}

#[test]
fn controller_output_is_stable() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let ctrl = files
        .iter()
        .find(|f| f.path == PathBuf::from("app/controllers/posts_controller.go"))
        .unwrap();
    let expected = "\
// Generated by Roundhouse.
package app

type PostsController struct{}

func (c *PostsController) Index() []Post {
\treturn Post.All()
}

func (c *PostsController) Show() Post {
\treturn Post.Find(Params[\"id\"])
}

func (c *PostsController) Create() {
\tpost := Post.New(PostParams)
\tif post.Save() {
\t\tRedirectTo(post)
\t} else {
\t\tRender(\"new\")
\t}
}

func (c *PostsController) Destroy() {
\tpost := Post.Find(Params[\"id\"])
\tpost.Destroy()
\tRedirectTo(PostsPath)
}
";
    assert_eq!(ctrl.content, expected);
}
