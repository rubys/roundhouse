#[test]
fn controller_file_is_emitted() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let paths: Vec<_> = files.iter().map(|f| f.path.display().to_string()).collect();
    assert!(
        paths.contains(&"app/posts_controller.go".to_string()),
        "got {paths:?}"
    );
}

#[test]
fn index_action_returns_response_and_calls_all() {
    // Pass-2: every action is a free function returning ActionResponse.
    // `Post.all` lowers to the package-level `PostAll()`; the template
    // renders through the view function and wraps in ActionResponse.
    let app = analyzed_app();
    let files = go::emit(&app);
    let ctrl = files
        .iter()
        .find(|f| f.path == PathBuf::from("app/posts_controller.go"))
        .unwrap();
    assert!(
        ctrl.content.contains("func PostsIndex(_ctx *ActionContext) ActionResponse"),
        "expected PostsIndex free function; got:\n{}",
        ctrl.content
    );
    // Walker path: `@posts = Post.all` → `posts := PostAll()`
    // (natural ivar-preserving local name, not scaffold's hardcoded
    // `records`).
    assert!(
        ctrl.content.contains("posts := PostAll()"),
        "expected posts := PostAll(); got:\n{}",
        ctrl.content
    );
}

#[test]
fn destroy_action_looks_up_and_calls_destroy() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let ctrl = files
        .iter()
        .find(|f| f.path == PathBuf::from("app/posts_controller.go"))
        .unwrap();
    // Pass-2: destroy resolves the id via ctx.Params, then calls
    // Destroy and returns a 303 redirect back to the index.
    assert!(
        ctrl.content.contains("func PostsDestroy(ctx *ActionContext) ActionResponse"),
        "expected PostsDestroy free function; got:\n{}",
        ctrl.content
    );
    // Walker path: id is extracted inline via parseInt64 helper
    // at the ModelFind call site, then nil-guarded.
    assert!(
        ctrl.content.contains("parseInt64(ctx.Params[\"id\"])"),
        "expected inline parseInt64 for id; got:\n{}",
        ctrl.content
    );
    assert!(
        ctrl.content.contains("post.Destroy()"),
        "expected post.Destroy(); got:\n{}",
        ctrl.content
    );
    assert!(
        ctrl.content.contains("ActionResponse{Status: 303, Location: PostsPath()}"),
        "expected redirect back to PostsPath(); got:\n{}",
        ctrl.content
    );
}

#[test]
fn controller_output_is_stable() {
    let app = analyzed_app();
    let files = go::emit(&app);
    let ctrl = files
        .iter()
        .find(|f| f.path == PathBuf::from("app/posts_controller.go"))
        .unwrap();
    let expected = "\
// Generated by Roundhouse.
package app

import \"strconv\"

var _ = strconv.Itoa

func PostsIndex(_ctx *ActionContext) ActionResponse {
\tposts := PostAll()
\treturn ActionResponse{Body: RenderPostsIndex(posts)}
}

func PostsShow(ctx *ActionContext) ActionResponse {
\tpost := PostFind(parseInt64(ctx.Params[\"id\"]))
\tif post == nil { post = &Post{} }
\treturn ActionResponse{Body: RenderPostsShow(post)}
}

func PostsCreate(ctx *ActionContext) ActionResponse {
\tpost := &Post{}
\tpost.Title = ctx.Params[\"post[title]\"]
\tif post.Save() {
\t\treturn ActionResponse{Status: 303, Location: PostPath(post.ID)}
\t} else {
\t\treturn ActionResponse{Body: RenderPostsNew(post)}
\t}
}

func PostsDestroy(ctx *ActionContext) ActionResponse {
\tpost := PostFind(parseInt64(ctx.Params[\"id\"]))
\tif post == nil { post = &Post{} }
\tpost.Destroy()
\treturn ActionResponse{Status: 303, Location: PostsPath()}
}
";
    assert_eq!(ctrl.content, expected);
}
